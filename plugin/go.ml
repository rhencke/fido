  | MLcons (_, r, []) when String.equal (global_basename r) "TBool"   -> "false"
  | MLcons (_, r, []) when String.equal (global_basename r) "TString" -> "\"\""
  | MLcons (_, r, [_])    when String.equal (global_basename r) "TChan"  -> "nil"
