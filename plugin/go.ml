(* ---- Record (Go struct) support ----
   A Rocq Record is a Go value-struct (both have value/copy semantics, so no
   aliasing model is needed).  A record's [ind_kind] carries its projection refs;
   [collect_records] gathers them up front.  At use sites a projection becomes
   [x.Field] and the constructor a struct literal [T{...}]; the projection
   DEFINITIONS are suppressed (field access replaces them). *)
let record_proj_field : (string, string) Hashtbl.t = Hashtbl.create 16  (* proj path -> field name *)
let record_ctor_names : (string, unit) Hashtbl.t = Hashtbl.create 16    (* constructor basename -> () *)
let record_typenames  : (string, unit) Hashtbl.t = Hashtbl.create 16    (* record type basename -> () *)
let record_ctor_ftypes : (string, ml_type list) Hashtbl.t = Hashtbl.create 16
  (* constructor basename -> field types (for typed-closure dictionary entries) *)
let record_ctor_fields : (string, string list) Hashtbl.t = Hashtbl.create 16
  (* constructor basename -> ordered Go field names (for KEYED struct literals T{F: v}) *)
(* review #4 P1 #4: a top-level function's PARAMETER types (path -> param ml_types), populated by
   [collect_decls].  At a call site a wide int64-carried value flowing into a NARROW param needs the
   [uint8(…)] cast — same as a struct field, but the destination type comes from the callee signature.
   Only applied when the param count equals the visible-arg count (i.e. no erased tag/witness/type
   args mis-align the zip — those calls decline the cast, staying at the prior bare emission). *)
let func_param_types : (string, ml_type list) Hashtbl.t = Hashtbl.create 64
(* DEFINED TYPE over a primitive (Go [type MyT <prim>]): a 2-field record whose 2nd field is a
   [GoTypeTag] PHANTOM (kept by extraction so Coq does NOT unbox the single value field, which
   keeps the type a distinct method-receiver).  We emit [type <Name> <under>] (NOT a struct),
   the ctor as a cast [<Name>(v)], and the value projection as a cast [<under>(x)]. *)
let defined_prim_under : (string, ml_type) Hashtbl.t = Hashtbl.create 16  (* record typename -> underlying ml_type *)
let defined_prim_proj  : (string, ml_type) Hashtbl.t = Hashtbl.create 16  (* value-proj path -> underlying ml_type *)
let deftype_under_of_ftypes ftypes =   (* Some <value field type> if [val_t; GoTypeTag _] or [val_t; unit] *)
  match ftypes with
  (* The 2nd field is a KEPT phantom that stops Coq unboxing the single value field (so the
     defined type stays a distinct method-receiver); it is DROPPED in extraction either way.
     A [unit] phantom (vs the original [GoTypeTag _]) is axiom-free COMPARABLE — [tt = tt] is
     trivial, whereas [GoTypeTag GoI64] uniqueness is not provable without an axiom (Type-indexed
     family) — so a [unit]-phantom defined type can be a map KEY / compared. *)
  | [vt; Tglob (r, _)] when String.equal (global_basename r) "GoTypeTag"
                        || String.equal (global_basename r) "unit" -> Some vt
  | _ -> None
(* Methods: a top-level function whose first visible param is a record (struct) is
   lowered as a Go value-receiver method.  [collect_decls] registers each such
   function by its [global_path]; uses then become [recv.Method(rest)]. *)
let method_paths      : (string, unit) Hashtbl.t = Hashtbl.create 16    (* method proj path -> () *)
let method_arity      : (string, int) Hashtbl.t  = Hashtbl.create 16    (* method path -> visible param count (incl. receiver) *)
let method_recvtype   : (string, string) Hashtbl.t = Hashtbl.create 16  (* method path -> Go receiver type name (for method expressions T.M) *)
let embedded_proj     : (string, unit) Hashtbl.t = Hashtbl.create 16    (* embedded-field projection path -> () (Go struct embedding) *)
let enum_typenames    : (string, unit) Hashtbl.t = Hashtbl.create 16    (* enum type basename -> () (a nullary-ctor inductive → Go [type T int] + iota consts) *)
let enum_ctors        : (string, unit) Hashtbl.t = Hashtbl.create 16    (* enum ctor basename -> () (emit as the const name) *)

