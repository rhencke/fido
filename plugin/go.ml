(* [from_builtins r]: the ref is DEFINED in Fido's [builtins.v] module (dirpath component
   ["builtins"]), NOT in a user theory like [main.v] (module ["main"]).  Coq references point at
   the DEFINING module regardless of where they are used, so this holds for a builtin no matter
   which theory references it.  Closes the basename-shadowing hole (code review): a user
   [Definition u8_add] in [main.v] no longer gets mis-recognized as the builtin. *)
let from_builtins r =
  (* EXACT dirpath-component match, not a substring scan: a [DirPath.repr] component must equal
     "builtins" verbatim.  The old substring scan accepted ANY path CONTAINING the text — so a user
     module [mybuiltins] / [builtins_helpers] would be mis-recognized as Fido's [builtins.v] and a
     same-basename user definition mis-lowered to an intrinsic (code review P0 #6).  Comparing the
     actual path components closes that: [mybuiltins] is the single component "mybuiltins" <> "builtins". *)
  match (try Some (Nametab.dirpath_of_global r.glob) with Not_found -> None) with
  | None -> false
  | Some dp -> List.exists (fun c -> String.equal (Id.to_string c) "builtins") (DirPath.repr dp)

(* A Fido builtin/combinator is matched by its basename AND a check that it lives in [builtins.v]
   — so a user theory CANNOT shadow a builtin name (the basename match alone used to trust that).
   Stdlib refs are package-qualified and use [ref_has_suffix]. *)
let named n r = String.equal (global_basename r) n && from_builtins r

let ref_has_suffix r suffix =
  let p = global_path r in
  let n = String.length p and m = String.length suffix in
  n >= m && String.equal suffix (String.sub p (n - m) m)

let path_contains r sub =
  let p = global_path r in
  let lp = String.length p and ls = String.length sub in
  let rec scan i = i + ls <= lp && (String.equal (String.sub p i ls) sub || scan (i + 1)) in
  ls > 0 && scan 0

(* [Z]/[positive]/[N] arithmetic helpers (BinInt.Z.*, BinPos.Pos.*, …) pulled in by
   the FULL-WIDTH int64 model's bodies ([wrap64] = [Z.modulo]…).  Those bodies are
   PROOF-ONLY (the ops lower to bare Go int64 by name), so the [Z] helpers are never
   referenced by emitted Go — but extraction still drags their (recursive, match-on-
   constructor) decls into the program.  Suppress them by module so the printer never
   tries to lower a [Z] eliminator.  Safe: nothing in the EMITTED Go calls them. *)
let is_zarith_helper r =
  List.exists (path_contains r)
    ["BinInt"; "BinPos"; "BinNat"; "BinNums"; "PArith"; "NArith";
     (* SpecFloat binary32/64 rounding machinery + the Sint63→Z helper, dragged in by the
        PROOF-ONLY float32 op bodies ([f32_add] = [SF2Prim (SFadd …)]).  Suppressed BY MODULE
        (not by basename — that collided: SpecFloat's parity [is_even] vs a user [is_even]).
        The float ops lower to native Go [float32]/[float64] by name, so nothing emitted calls
        these.  ([Sint63] also carries [to_Z], the narrow→int64 widening's blocker.) *)
     "SpecFloat"; "FloatOps"; "FloatLemmas"; "PrimFloat"; "Sint63"; "Int63"; "Cyclic"]
  (* Generic eliminators leaked from the [Z] bodies but living in
     [Corelib.Init.Datatypes] (so not caught by module): [CompOpp] (from
     [Z.compare]), [fst]/[snd] (from [Z.quotrem], the truncating div/mod).  Only ever
     reachable from the suppressed [GoI64] bodies, so never emitted. *)
  || List.mem (global_basename r) ["CompOpp"; "comparison"; "fst"; "snd"]

