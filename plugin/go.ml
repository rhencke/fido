           (* P0 #2: widen the operand to the int carrier (a narrow-typed int8 would overflow `& 0xff`). *)
           fw_wrap s w (str "^int(" ++ pp_expr state env x ++ str ")")
       (* full-width int64 unary complement [i64_not x] → [^x].  Go's [^] on an
