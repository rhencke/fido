(* Distinct fixed-width numeric types: each [GoU<N>]/[GoI<N>] is a single-field
   record over the [int] carrier (builtins.v) — DISTINCT in Rocq (mixing a uint8
   with an int is a type error: Go spec "Numeric types"), but the wrapper is
   ERASED in extraction (exactly like [existT]/[any]): the type IS its [int64]
   carrier, and the constructor [MkU<N>] / projection [<u|i><N>raw] are identity.
   So a well-typed distinct-type term lowers to the same int64+mask Go we already
   emit — compilable BY CONSTRUCTION, no Go-level wrapper to leak. *)
let all_digits s = String.length s > 0 && String.for_all (fun c -> c >= '0' && c <= '9') s
let is_ui_digits s =
  String.length s >= 2 && (s.[0] = 'U' || s.[0] = 'I')
  && all_digits (String.sub s 1 (String.length s - 1))
let str_prefix pre s = String.length s >= String.length pre && String.sub s 0 (String.length pre) = pre
let str_suffix suf s = let ls = String.length s and lf = String.length suf in
  ls >= lf && String.sub s (ls - lf) lf = suf
let is_numint_typename s =                   (* "GoU8" / "GoI16" — the numeric wrappers *)
  str_prefix "Go" s && String.length s > 2 && is_ui_digits (String.sub s 2 (String.length s - 2))
(* Proof-only records that must NOT become Go value-structs: the numeric wrappers
   (erased to their int64 carrier) plus the substrate records whose single field is
   proof-only world state — [Sess] (a session = an [IO] wrapped with phantom protocol
   indices; the ops lower by name).  Their type decl, constructor, and projection are
   all suppressed; uses are recognised by operation name. *)
let is_erased_record_typename s =
  is_numint_typename s || String.equal s "Sess" || String.equal s "World"
  || String.equal s "Ref" || String.equal s "Ptr" || String.equal s "SliceH"
  || String.equal s "SPtr" || String.equal s "StructRep2"   (* struct-pointer machinery (Bs.2) *)
  || String.equal s "GoArray"   (* fixed-size array (B4): size-erased; ops recognized by name *)
  || String.equal s "GoChan" || String.equal s "GoMap"
  || String.equal s "Tagged"   (* the GoAny type-tag typeclass (single-field) *)
let is_numint_type r =                      (* GoU8 / GoI16 *)
  let n = global_basename r in str_prefix "Go" n && is_ui_digits (String.sub n 2 (String.length n - 2))
let is_numint_ctor r =                      (* MkU8 / MkI16 *)
  let n = global_basename r in str_prefix "Mk" n && is_ui_digits (String.sub n 2 (String.length n - 2))
let is_numint_proj r =                      (* u8raw / i16raw / i64raw : (u|i) digits "raw" *)
  let n = global_basename r in let len = String.length n in
  len >= 5 && (n.[0] = 'u' || n.[0] = 'i') && str_suffix "raw" n
  && all_digits (String.sub n 1 (len - 4))

(* Full-width int64 ops ([i64_add]/[i64_lit]/…).  [GoI64]/[MkI64]/[i64raw] already
   ride the numint machinery above (erased type/ctor/proj, rendered int64), but the
   OPS are NOT masked (a [GoI64] is a real Go int64, wrapping natively at 2^64): the
   arithmetic/comparison ops lower to BARE Go operators via [binop_of], and [i64_lit]
   folds its [Z] literal.  The width-64 op names fall through [parse_fixed_width]
   (capped at 32), so they never get the narrow masked treatment. *)
let is_i64_op r name = String.equal (global_basename r) ("i64_" ^ name)
let is_i64_lit r = is_i64_op r "lit"
let is_any_i64_op r =
  List.exists (is_i64_op r)
    ["lit"; "add"; "sub"; "mul"; "add_nz"; "sub_nz"; "mul_nz"; "eqb"; "ltb"; "leb";
     "gtb"; "geb"; "neqb"; "neg";
     "div"; "mod"; "and"; "or"; "xor"; "andnot"; "not"; "shl"; "shr"]

(* Full-width uint64 ops ([u64_add]/[u64_lit]/…).  [GoU64]/[MkU64]/[u64raw]
   ride the numint machinery (erased type/ctor/proj, rendered uint64), but the
   OPS are NOT masked (a [GoU64] is a real Go uint64, wrapping unsigned-natively
   at 2^64): arithmetic/comparison ops lower to BARE Go operators via [binop_of],
   and [u64_lit] folds its [Z] literal.  The width-64 names fall through
   [parse_fixed_width] (capped at 32), so they never get the narrow masked treatment.
   For literals, [Printf.sprintf "%Lu"] gives unsigned decimal for all [Int64.t]
   values, including those that are "negative" when interpreted as signed
   (i.e. u64 values in [2^63, 2^64)). *)
let is_u64_op r name = String.equal (global_basename r) ("u64_" ^ name)
let is_u64_lit r = is_u64_op r "lit"
let is_any_u64_op r =
  List.exists (is_u64_op r)
    ["lit"; "add"; "sub"; "mul"; "eqb"; "ltb"; "leb";
     "gtb"; "geb"; "neqb"; "neg";
     "div"; "mod"; "and"; "or"; "xor"; "andnot"; "not"; "shl"; "shr"]

(*s Nat arithmetic operation recognition. *)

type nat_op = NatAdd | NatSub | NatMul | NatDiv | NatMod
            | NatEqb | NatLtb | NatLeb

let nat_op_names = [
  "add",     NatAdd;
  "sub",     NatSub;
  "mul",     NatMul;
  (* Integer div / mod are deliberately omitted: Go panics on division by zero,
     but Rocq's Uint63 / nat division is total (x / 0 = 0, x mod 0 = x), so
     emitting a raw `/` or `%` would be silently unsound.  Forbidden until a
     guarded form (proof d <> 0, or a checked form) exists.  Any use now
     extracts to an undefined identifier rather than wrong Go.  (Float `/` is
     handled separately and kept — IEEE, no panic.) *)
  "eqb",     NatEqb;
  "ltb",     NatLtb;
  "leb",     NatLeb;
]

let is_nat_op_ref r name =
  ref_has_suffix r (".Init.Nat." ^ name) ||
  ref_has_suffix r (".Arith.PeanoNat.Nat." ^ name)

let is_int63_op_ref r name =
  ref_has_suffix r (".PrimInt63." ^ name) ||
  ref_has_suffix r (".Cyclic.Int63.PrimInt63." ^ name)

(* Float arithmetic AND comparison.  Both Coq's [PrimFloat] and Go's [float64]
   implement IEEE 754 binary64, so these primitives lower directly to the Go
   operators *with identical semantics, NaN included*: every comparison with a
   NaN operand is false ([ltb]/[leb]/[eqb]) in both, and [eqb (+0) (-0) = true]
   in both.  (Float [/] cannot panic — it is IEEE, yielding ±inf / NaN — so unlike
   integer division it needs no guard.) *)
let float_op_table = [
  "add", " + "; "sub", " - "; "mul", " * "; "div", " / ";
  "ltb", " < "; "leb", " <= "; "eqb", " == ";
]

let is_float_op_ref r name =
  ref_has_suffix r (".PrimFloat." ^ name)

(* Float unary negation: [PrimFloat.opp x] → Go [-x].  IEEE-exact (flips the sign
   bit), so [opp (+0.0) = -0.0] and [opp NaN] stays NaN — matching Go's unary [-]
   on float64.  Distinct from [sub 0 x], which would give [+0.0] for [x = +0.0].
   Needs no package import (unlike [abs]/[sqrt], which want [math]). *)
let is_float_opp_ref r = is_float_op_ref r "opp"

(* Fixed-width unsigned integer ops (builtins.v).  Carrier is int64; each op
   masks back to the width, e.g. [u8_add a b] → [((a + b) & 0xff)], matching Go's
   uint8 wrap.  Comparisons go through [binop_of]; the masked arithmetic and the
   literal/normaliser get explicit cases in [pp_expr]. *)
(* Parse a fixed-width integer op name [u<W>_op] / [i<W>_op] ([u]=unsigned,
   [i]=signed) into (signed, width, op).  One parser serves every width, so adding
   uint16/int16/uint32/… needs only the Rocq definitions — no plugin change.
   Width is capped at 32 (a 64-bit value exceeds the 63-bit carrier — that needs
   the Z-based model and is left to fail loud). *)
let parse_fixed_width n =
  let len = String.length n in
  if len < 4 || not (n.[0] = 'u' || n.[0] = 'i') then None
  else begin
    let i = ref 1 in
    while !i < len && n.[!i] >= '0' && n.[!i] <= '9' do incr i done;
    if !i = 1 || !i >= len || n.[!i] <> '_' then None
    else
      let op = String.sub n (!i + 1) (len - !i - 1) in
      (* only the KNOWN fixed-width ops — else [u8_demo] etc. would falsely match *)
      if not (List.mem op ["lit"; "add"; "sub"; "mul"; "eqb"; "ltb"; "leb"; "norm";
                           "gtb"; "geb"; "neqb";
                           "and"; "or"; "xor"; "andnot"; "not"; "shl"; "shr"; "of_int";
                           "div"; "mod"])
      then None
      else match int_of_string_opt (String.sub n 1 (!i - 1)) with
        | Some width when width >= 1 && width <= 32 -> Some ((n.[0] = 'i'), width, op)
        | _ -> None
  end
let fixed_width_op r = parse_fixed_width (global_basename r)
let fw_is r op = match fixed_width_op r with Some (_, _, o) -> String.equal o op | None -> false

(* Emit the width-[w] wrap of [inner]: mask to [w] bits (Go [& 0x..]); for a
   SIGNED width, additionally sign-extend via [(m ^ 2^(w-1)) - 2^(w-1)].  Matches
   Go's uintW / intW wrap exactly on the int64 carrier. *)
let fw_wrap signed width inner =
  let mask = Printf.sprintf "0x%x" ((1 lsl width) - 1) in
  let masked = str "(" ++ inner ++ str (" & " ^ mask ^ ")") in
  if not signed then masked
  else
    let sbit = Printf.sprintf "0x%x" (1 lsl (width - 1)) in
    str "((" ++ masked ++ str (" ^ " ^ sbit ^ ") - " ^ sbit ^ ")")

let classify_float_op r =
  List.find_map
    (fun (name, op) -> if is_float_op_ref r name then Some op else None)
    float_op_table

(* Coq [nat] is modelled as Go [uint].  Add/mul/comparison are faithful within
   the representable range (a [nat] too large for [uint] is unrepresentable
   either way), but [Nat.sub] is TRUNCATED monus ([3 - 5 = 0]) whereas Go [uint]
   subtraction WRAPS ([3 - 5 = 2^64 - 2]) — they disagree even on small values.
   So [Nat.sub] is excluded here; it must NOT lower to a bare Go [-].  ([pp_expr]
   turns any use into a loud [unsupported] error.)  This is the *Coq nat* path
   only; [PrimInt63.sub] (signed two's-complement, faithful) goes through
   [classify_int63_op] and is unaffected. *)
let is_nat_sub_ref r = is_nat_op_ref r "sub"
let classify_nat_op r =
  List.find_map
    (fun (name, op) ->
       if String.equal name "sub" then None
       else if is_nat_op_ref r name then Some op else None)
    nat_op_names

(* int63 (the [int] = Sint63 model).  Two's-complement [add]/[sub]/[mul] and
   bit-equality [eqb] are identical signed/unsigned, so they lower directly.  But
   the UNSIGNED comparisons [PrimInt63.ltb]/[leb] must NOT map to Go's int64
   [<]/[<=], which are SIGNED — they disagree once the high bit is set (unsigned
   [ltb (-1) 0] is false; signed [-1 < 0] is true).  Signed comparison for [int]
   goes through [ltsb]/[lesb] (classify_sint63_op → Go signed [<]/[<=]); the
   user-facing [Sint63.ltb]/[leb] reduce to those.  So ltb/leb are excluded here;
   raw [PrimInt63.ltb]/[leb] fail loud (no unsigned-int model yet to target). *)
let int63_op_names = [ "add", NatAdd; "sub", NatSub; "mul", NatMul; "eqb", NatEqb ]
let classify_int63_op r =
  List.find_map
    (fun (name, op) -> if is_int63_op_ref r name then Some op else None)
    int63_op_names

(* Signed comparison AND signed division/remainder — where signed and unsigned
   differ ([int] is modelled with signed int64 semantics; the raw +/-/* ops are
   shared).  The signed primitives [ltsb]/[lesb]/[divs]/[mods] live in
   [PrimInt63]; Go's [<]/[<=]/[/]/[%] are type-directed (signed on int64), so
   emitting them is faithful (both truncate division toward zero).  [divs]/[mods]
   are reached only via the proof-carrying [div_nz]/[mod_nz] (which demand the
   divisor be non-zero); raw [PrimInt63.divs] is the escape hatch (Go panics on
   a zero divisor, mirroring raw send/slice_get). *)
let sint63_op_names =
  [ "ltsb", NatLtb; "lesb", NatLeb; "divs", NatDiv; "mods", NatMod ]
let classify_sint63_op r =
  List.find_map
    (fun (name, op) -> if is_int63_op_ref r name then Some op else None)
    sint63_op_names

let go_infix = function
  | NatAdd -> " + " | NatSub -> " - " | NatMul -> " * "
  | NatDiv -> " / " | NatMod -> " % "
  | NatEqb -> " == " | NatLtb -> " < " | NatLeb -> " <= "

(* Go binary-operator precedence (higher binds tighter).  Used by the expression
   printer to parenthesise a sub-operand only when its operator binds looser than
   the surrounding context requires (instead of the old conservative
   "parenthesise every non-atom"). *)
let op_prec = function
  | NatMul | NatDiv | NatMod -> 5
  | NatAdd | NatSub          -> 4
  | NatEqb | NatLtb | NatLeb -> 3
let float_prec s =
  if String.equal s " * " || String.equal s " / " then 5
  else if String.equal s " < " || String.equal s " <= " || String.equal s " == "
  then 3        (* comparisons bind looser than + / -, like the integer ops *)
  else 4

(* Boolean operators.  Coq's [andb]/[orb] take two already-evaluated [bool]
   values; Go's [&&]/[||] short-circuit.  This is observationally identical here:
   the operands are pure, total [bool] values (no effects, no divergence in the
   model), so whether the right operand is "evaluated" is unobservable.  Go
   precedence: [||] = 1 (loosest), [&&] = 2, comparisons = 3. *)
let is_andb_ref r = named "andb" r
let is_orb_ref  r = named "orb"  r
let is_negb_ref r = named "negb" r

(* If [r] is a recognised inlined binary operator, its (precedence, Go operator
   string); covers boolean / nat / int63 / signed-int63 / float arithmetic and
   comparison.  ([negb] is unary — handled separately in the expression printer.) *)
let binop_of r =
  if is_andb_ref r then Some (2, " && ")
  else if is_orb_ref r then Some (1, " || ")
  (* string concatenation: Go [+], same precedence as numeric [+] (level 4) *)
  else if is_str_concat_ref r then Some (4, " + ")
  (* string comparison: Go [==] / [<] (lexicographic), comparison precedence (level 3) *)
  else if is_str_eqb_ref r then Some (3, " == ")
  else if is_str_ltb_ref r then Some (3, " < ")
  (* array comparison: Go field-wise [==] (arrays are comparable, slices are not) *)
  else if is_arr_eqb_ref r then Some (3, " == ")
  (* direct >/>=/!= for string and float64 (recognized by basename) *)
  else if String.equal (global_basename r) "str_gtb"  then Some (3, " > ")
  else if String.equal (global_basename r) "str_geb"  then Some (3, " >= ")
  else if String.equal (global_basename r) "str_neqb" then Some (3, " != ")
  else if String.equal (global_basename r) "f64_gtb"  then Some (3, " > ")
  else if String.equal (global_basename r) "f64_geb"  then Some (3, " >= ")
  else if String.equal (global_basename r) "f64_neqb" then Some (3, " != ")
  (* full-width int64 (GoI64): a Go int64 wraps natively at 2^64, so arithmetic /
     bitwise / shift lower to BARE Go operators (no mask) and comparison is signed
     int64 </<=/==.  Go precedence: [* / % << >> & &^] = 5, [+ - | ^] = 4, cmp = 3.
     Go int64 [/] truncates toward zero and [>>] is arithmetic — matching [Z.quot]/
     [Z.shiftr] in the model. *)
  else if is_i64_op r "add"    then Some (4, " + ")
  else if is_i64_op r "sub"    then Some (4, " - ")
  else if is_i64_op r "mul"    then Some (5, " * ")
  (* overflow-safe forms: the no-overflow proof is erased, so [i64_add_nz a b] is the
     same bare Go op as [i64_add a b] (the proof showed it does not wrap). *)
  else if is_i64_op r "add_nz" then Some (4, " + ")
  else if is_i64_op r "sub_nz" then Some (4, " - ")
  else if is_i64_op r "mul_nz" then Some (5, " * ")
  else if is_i64_op r "div"    then Some (5, " / ")
  else if is_i64_op r "mod"    then Some (5, " % ")
  else if is_i64_op r "and"    then Some (5, " & ")
  else if is_i64_op r "andnot" then Some (5, " &^ ")
  else if is_i64_op r "shl"    then Some (5, " << ")
  else if is_i64_op r "shr"    then Some (5, " >> ")
  else if is_i64_op r "or"     then Some (4, " | ")
  else if is_i64_op r "xor"    then Some (4, " ^ ")
  else if is_i64_op r "ltb"    then Some (3, " < ")
  else if is_i64_op r "leb"    then Some (3, " <= ")
  else if is_i64_op r "eqb"    then Some (3, " == ")
  else if is_i64_op r "gtb"    then Some (3, " > ")
  else if is_i64_op r "geb"    then Some (3, " >= ")
  else if is_i64_op r "neqb"   then Some (3, " != ")
  (* full-width uint64 (GoU64): same bare Go operators as i64 — Go uint64 wraps
     unsigned-natively at 2^64, arithmetic / bitwise / shift lower identically.
     Comparison is unsigned uint64 </<=/==, which matches [Z.ltb]/[Z.leb] on the
     non-negative [u64raw] values.  Go uint64 [/] is truncating-non-negative,
     matching [Z.div] on non-negative operands. *)
  else if is_u64_op r "add"    then Some (4, " + ")
  else if is_u64_op r "sub"    then Some (4, " - ")
  else if is_u64_op r "mul"    then Some (5, " * ")
  else if is_u64_op r "div"    then Some (5, " / ")
  else if is_u64_op r "mod"    then Some (5, " % ")
  else if is_u64_op r "and"    then Some (5, " & ")
  else if is_u64_op r "andnot" then Some (5, " &^ ")
  else if is_u64_op r "shl"    then Some (5, " << ")
  else if is_u64_op r "shr"    then Some (5, " >> ")
  else if is_u64_op r "or"     then Some (4, " | ")
  else if is_u64_op r "xor"    then Some (4, " ^ ")
  else if is_u64_op r "ltb"    then Some (3, " < ")
  else if is_u64_op r "leb"    then Some (3, " <= ")
  else if is_u64_op r "eqb"    then Some (3, " == ")
  else if is_u64_op r "gtb"    then Some (3, " > ")
  else if is_u64_op r "geb"    then Some (3, " >= ")
  else if is_u64_op r "neqb"   then Some (3, " != ")
  (* fixed-width uN/iN comparisons: values are in range, so Go's signed int64
     </<=/== agree with both unsigned and signed fixed-width comparison. *)
  else if fw_is r "ltb" then Some (3, " < ")
  else if fw_is r "leb" then Some (3, " <= ")
  else if fw_is r "eqb" then Some (3, " == ")
  else if fw_is r "gtb" then Some (3, " > ")
  else if fw_is r "geb" then Some (3, " >= ")
  else if fw_is r "neqb" then Some (3, " != ")
  (* fixed-width bitwise: no wrap needed (in-range / sign-extended results are
     already correct on int64).  Go precedence: [& &^] = 5, [| ^] = 4. *)
  else if fw_is r "and"    then Some (5, " & ")
  else if fw_is r "andnot" then Some (5, " &^ ")
  else if fw_is r "or"     then Some (4, " | ")
  else if fw_is r "xor"    then Some (4, " ^ ")
  else
  match classify_nat_op r with
  | Some op -> Some (op_prec op, go_infix op)
  | None ->
  match classify_int63_op r with
  | Some op -> Some (op_prec op, go_infix op)
  | None ->
  match classify_sint63_op r with
  | Some op -> Some (op_prec op, go_infix op)
  | None ->
  match classify_float_op r with
  | Some s -> Some (float_prec s, s)
  | None -> None

(*s Type printer. *)

let rec pp_type state = function
  | Tarr (t1, t2) ->
      str "func(" ++ pp_type state t1 ++ str ") " ++ pp_type state t2
  | Tglob (r, _)  when is_sigT_ref r -> str "any"
  (* GoU<N>/GoI<N> erase to their int64 carrier (the wrapper is gone in Go).
     Exception: GoU64 uses the unsigned uint64 carrier so it can represent [2^63, 2^64). *)
  | Tglob (r, _)  when is_numint_type r ->
      if String.equal (global_basename r) "GoU64" then str "uint64" else str "int64"
  (* IO A erases to A — the world token has no runtime representation *)
  | Tglob (r, [arg]) when is_IO_type r -> pp_type state arg
  (* Ref A → T (a mutable local cell is just a Go variable of type T) *)
  | Tglob (r, [arg]) when is_ref_type r -> pp_type state arg
  (* Ptr A → *T (a first-class Go pointer; copies alias the same cell) *)
  | Tglob (r, [arg]) when is_ptr_type r -> str "*" ++ pp_type state arg
  | Tglob (r, [arg]) when is_sptr_type r -> str "*" ++ pp_type state arg
  | Tglob (r, _) when is_arr_type r ->
      unsupported "an array type in a position needing an explicit [N]T (param / field / return / typed var decl); only LOCAL arrays are supported — write `a := arr_lit […]` so Go infers the size from the literal (the size lives in the construction, not the Coq type)"
  (* SliceH A → []T (Go's slice IS the aliasing handle; sub-slices share) *)
  | Tglob (r, [arg]) when is_sliceh_type r -> str "[]" ++ pp_type state arg
  (* GoChan A → chan T *)
  | Tglob (r, [arg]) when is_go_chan_type r ->
      str "chan " ++ pp_type state arg
  (* GoMap K V → map[KT]VT *)
  | Tglob (r, [kt; vt]) when is_go_map_type r ->
      str "map[" ++ pp_type state kt ++ str "]" ++ pp_type state vt
  (* list A = GoSlice A → []T  (a rune slice [list GoRune] is just []int32; the
     byte-sequence string is the distinct Coq [string] type, handled below) *)
  | Tglob (r, [arg]) when is_list_type r ->
      str "[]" ++ pp_type state arg
  (* Coq [string] (Strings.String) → Go [string] (byte sequence) *)
  | Tglob (r, []) when is_string_type r -> str "string"
  | Tglob (r, []) when is_float64_type r  -> str "float64"
  | Tglob (r, []) when is_go_prim_type r -> str (Option.get (classify_go_prim_type r))
  | Tglob (r, []) when is_unit_type r   -> str "struct{}"
  | Tglob (r, []) when is_uint63_type r -> str "int64"
  | Tglob (r, []) when is_nat_type r    -> str "uint"
  | Tglob (r, []) when is_bool_type r   -> str "bool"
  | Tglob (r, args) ->
      (* A user record reaches here only if it was registered (collect_decls
         pass 1) and hence has a `type T struct {…}` decl emitted.  A Tglob that
         is NOT a registered record has no type decl — emitting its bare name
         would dangle.  The canonical offender is a SINGLE-FIELD Record: Coq
         unboxes it (`Inner {iv}` ≡ its field type), so no struct is emitted yet
         a field still references `Inner`.  Fail loud rather than emit wrong Go
         (meta-invariant); give such a record ≥2 fields. *)
      let bn = global_basename r in
      if not (is_record_typename bn) then
        unsupported (Printf.sprintf
          "type '%s' (no struct decl emitted — a single-field Record is unboxed \
           by Coq; give it >= 2 fields)" (go_export bn));
      str (go_export bn) ++
      (if args = [] then mt ()
       else
         str "[" ++
         prlist_with_sep (fun () -> str ", ") (pp_type state) args ++
         str "]")
  | Tvar i | Tvar' i -> str ("T" ^ string_of_int i)
  | Tdummy _ | Tunknown | Taxiom | Tmeta _ -> str "any"

let rec collect_tarrs = function
  | Tarr (t, rest) -> let ts, ret = collect_tarrs rest in t :: ts, ret
  | t              -> [], t

let is_erased = function MLdummy _ -> true | _ -> false

(*s De Bruijn environment.

    [env] is the list of bound [ml_ident]s, innermost binder first.
    [MLrel 1] is the innermost (most-recently-bound) variable.

    In Rocq 9.x, [ml_ident] is a variant: [Dummy | Id of variable | Tmp of variable]. *)

let is_dummy = function Dummy -> true | _ -> false

let pp_mlident = function
  | Dummy   -> str "_"
  | Id v    -> str (go_safe (Id.to_string v))
  | Tmp v   -> str (go_safe (Id.to_string v))

let pp_rel env i =
  try pp_mlident (List.nth env (i - 1))
  with Not_found -> str ("_db" ^ string_of_int i)

(*s Collect nested lambdas into a flat parameter list. *)

let collect_lam body =
  let rec go acc = function
    | MLlam (id, body) -> go (id :: acc) body
    | body             -> List.rev acc, body
  in
  go [] body

(*s Flatten nested applications. *)

let collect_app head args =
  let rec go acc = function
    | MLapp (h, more) -> go (more @ acc) h
    | h               -> h, acc
  in
  go args head

(*s Peel [MLmagic] coercions (inserted by extraction around casts) to reach
    the underlying node — needed to recognise [MLcase] under a coercion. *)
let rec strip_magic = function
  | MLmagic e -> strip_magic e
  | e         -> e

(*s Unfold a Rocq list literal (MLcons-based) to an OCaml list, or None. *)

let rec unfold_list acc = function
  | MLcons (_, r, [x; rest]) when is_list_cons r -> unfold_list (x :: acc) rest
  | MLcons (_, r, [])        when is_list_nil r  -> Some (List.rev acc)
  | _ -> None

(*s Decode a Coq [string] literal to its bytes, or None if it is not a literal
    (e.g. a runtime [String c rest] with a non-constant head — which we do NOT
    build at runtime; concat lowers to Go [+]).  An [Ascii a0 .. a7] is a byte
    with [a0] the LEAST-significant bit (matching [Ascii.N_of_ascii]). *)
let decode_ascii e =
  match strip_magic e with
  | MLcons (_, r, bits) when is_ascii_ctor r && List.length bits = 8 ->
      let rec go i acc = function
        | [] -> Some acc
        | b :: rest ->
            (match strip_magic b with
             | MLcons (_, rb, []) when is_bool_true rb  -> go (i + 1) (acc lor (1 lsl i)) rest
             | MLcons (_, rb, []) when is_bool_false rb -> go (i + 1) acc rest
             | _ -> None)
      in go 0 0 bits
  | _ -> None

let rec decode_go_string e =
  match strip_magic e with
  | MLcons (_, r, []) when is_string_empty r -> Some []
  | MLcons (_, r, [a; rest]) when is_string_cons r ->
      (match decode_ascii a, decode_go_string rest with
       | Some b, Some bs -> Some (b :: bs)
       | _ -> None)
  | _ -> None

(* Render a byte list as a Go double-quoted string literal.  Bytes outside
   printable ASCII (and backslash / double-quote) use Go's hex byte escape
   [backslash-x-NN], so the literal denotes EXACTLY those bytes (byte-faithful,
   like the Coq model). *)
let go_string_lit bytes =
  let buf = Buffer.create (List.length bytes + 2) in
  Buffer.add_char buf '"';
  List.iter (fun b ->
    if b = Char.code '"' then Buffer.add_string buf "\\\""
    else if b = Char.code '\\' then Buffer.add_string buf "\\\\"
    else if b = Char.code '\n' then Buffer.add_string buf "\\n"
    else if b = Char.code '\t' then Buffer.add_string buf "\\t"
    else if b = Char.code '\r' then Buffer.add_string buf "\\r"
    else if b >= 0x20 && b < 0x7f then Buffer.add_char buf (Char.chr b)
    else Buffer.add_string buf (Printf.sprintf "\\x%02x" b)) bytes;
  Buffer.add_char buf '"';
  Buffer.contents buf


(*s Fold a Peano nat literal to an integer, or return None. *)

let rec nat_value = function
  | MLcons (_, r, [])  when is_nat_zero r -> Some 0
  | MLcons (_, r, [n]) when is_nat_succ r ->
      (match nat_value n with Some v -> Some (v + 1) | None -> None)
  | _ -> None

(*s Fold a Coq [Z] literal to its value as an [Int64], or None if not a literal.
    [Z] = Z0 | Zpos positive | Zneg positive; [positive] = xH | xO p | xI p (LSB-
    recursive: [xO p = 2p], [xI p = 2p+1], [xH = 1]).  [Int64] is exactly the int64
    range, so a constant the smart [i64_lit] admits (proved [in_i64], i.e. in
    [[-2^63, 2^63)]) is represented faithfully — the lone [-2^63] case folds its
    magnitude [2^63] to [Int64.min_int] by two's-complement wrap, and negating
    [min_int] is [min_int], the correct value.  Non-literals fail loud upstream. *)
let rec pos_value = function
  | MLcons (_, r, [])  when String.equal (global_basename r) "xH" -> Some 1L
  | MLcons (_, r, [p]) when String.equal (global_basename r) "xO" ->
      (match pos_value p with Some v -> Some (Int64.mul 2L v) | None -> None)
  | MLcons (_, r, [p]) when String.equal (global_basename r) "xI" ->
      (match pos_value p with Some v -> Some (Int64.add (Int64.mul 2L v) 1L) | None -> None)
  | _ -> None
let z_value = function
  | MLcons (_, r, [])  when String.equal (global_basename r) "Z0"   -> Some 0L
  | MLcons (_, r, [p]) when String.equal (global_basename r) "Zpos" -> pos_value p
  | MLcons (_, r, [p]) when String.equal (global_basename r) "Zneg" ->
      (match pos_value p with Some v -> Some (Int64.neg v) | None -> None)
  | _ -> None
(* Source-[Z] sign: a [Z0]/[Zpos] is non-negative.  Used to tell a [uint64] literal
   in [2^63, 2^64) (a [Zpos] whose 64-bit pattern looks negative as a signed [Int64])
   apart from a genuine [int64] negative (a [Zneg]). *)
let z_is_nonneg = function
  | MLcons (_, r, [])  when String.equal (global_basename r) "Z0"   -> true
  | MLcons (_, r, [_]) when String.equal (global_basename r) "Zpos" -> true
  | _ -> false

(*s Expression printer. *)

(*s Check whether a given de Bruijn index appears free in an expression.
    [dbn_free n e] is "index [n] does NOT occur free in [e]"; the index SHIFTS at
    every binder (lambda / let / match arm / fix), so an inner binder's own
    index-1 variable (e.g. the [ok] of a [recv_ok]/[type_assert_safe] CPS
    continuation [fun v ok => …]) is not mistaken for the outer index.  The old
    version went under [MLlam] WITHOUT shifting, a false-negative that defeated
    discard-detection on left-nested [>>'] chains (the action was wrongly kept as
    a value [x := Bind(…)]).  [db1_free] = "the bind result (index 1) is unused". *)

let rec dbn_free n = function
  | MLrel k             -> k <> n
  | MLglob _ | MLuint _ | MLfloat _ | MLdummy _ | MLaxiom _ | MLexn _ -> true
  | MLapp (h, args)     -> dbn_free n h && List.for_all (dbn_free n) args
  | MLlam (_, b)        -> dbn_free (n + 1) b
  | MLletin (_, e1, e2) -> dbn_free n e1 && dbn_free (n + 1) e2
  | MLcons (_, _, args) -> List.for_all (dbn_free n) args
  | MLcase (_, scrut, branches) ->
      dbn_free n scrut
      && Array.for_all (fun (ids, _, b) -> dbn_free (n + List.length ids) b) branches
  | MLfix (_, names, bodies) ->
      Array.for_all (dbn_free (n + Array.length names)) bodies
  | MLmagic e           -> dbn_free n e
  | _                   -> true

let db1_free e = dbn_free 1 e

(*s CFG block terminators ([Next] values).  A block emitter delegates its
    terminator to one of these, so the same emitter prints raw labels+goto or
    structured continue/break depending on context. *)

let raw_term tab next =
  match next with
  | MLcons (_, c, [n]) when is_jump_ctor c ->
      let lbl = match nat_value n with Some v -> v | None -> 0 in
      str tab ++ str (Printf.sprintf "goto block%d" lbl) ++ fnl ()
  | MLcons (_, c, []) when is_done_ctor c -> str tab ++ str "return" ++ fnl ()
  | _ -> str tab ++ str "return" ++ fnl ()

let rec pp_expr state env = function
  | MLrel i ->
      pp_rel env i

  | MLapp (head, args) ->
      let head, all_args = collect_app head args in
      let vis = List.filter (fun a -> not (is_erased a)) all_args in
      (match head, vis with
       (* ret x → x (identity: IO wrapper erases) *)
       | MLglob r, [x] when is_ret_ref r -> pp_expr state env x

       (* type_assert tag v → v.(T) where T is read from the tag constructor *)
       | MLglob r, [tag; v] when is_type_assert_ref r ->
           let go_type = go_type_of_tag tag in
           pp_expr state env (any_payload v) ++ str ".(" ++ str go_type ++ str ")"

       (* panic(val) *)
       | MLglob r, [x] when is_panic_ref r ->
           str "panic(" ++ pp_expr state env (any_payload x) ++ str ")"

       (* slice_of_list tag list → []T{v1, v2, ...} *)
       | MLglob r, [tag; lst] when is_slice_of_list_ref r ->
           let go_elem = go_type_of_tag tag in
           (match unfold_list [] lst with
            | Some elems ->
                str ("[]" ^ go_elem ^ "{") ++
                prlist_with_sep (fun () -> str ", ") (pp_expr state env) elems ++
                str "}"
            | None -> str ("[]" ^ go_elem ^ "(nil)"))
       (* arr_lit tag list → [N]T{v1, …, vN} — a FIXED-SIZE array literal (B4); the
          size N is the list length (read off the construction, NOT the type), so a
          local [a := arr_lit …] has its Go type inferred from this literal. *)
       | MLglob r, [tag; lst] when is_arr_lit_ref r ->
           let go_elem = go_type_of_tag tag in
           (match unfold_list [] lst with
            | Some elems ->
                str ("[" ^ string_of_int (List.length elems) ^ "]" ^ go_elem ^ "{") ++
                prlist_with_sep (fun () -> str ", ") (pp_expr state env) elems ++
                str "}"
            | None -> unsupported "arr_lit of a non-literal list (an array's size must be statically known)")
       (* arr_set n tag a i v → the copy-mutate-return IIFE (value-copy: a is unchanged)
          func(_a [n]T) [n]T { _a[i] = v; return _a }(a) *)
       | MLglob r, [size; tag; a; i; v] when is_arr_set_ref r ->
           let n = (match nat_value (strip_magic size) with
             | Some n -> n
             | None -> unsupported "arr_set with a non-literal size (the array size must be statically known)") in
           let t = go_type_of_tag (strip_magic tag) in
           let arrty = Printf.sprintf "[%d]%s" n t in
           str ("func(_a " ^ arrty ^ ") " ^ arrty ^ " { _a[") ++ pp_expr state env i ++
           str "] = " ++ pp_typed_lit state env v ++ str "; return _a }(" ++ pp_atom state env a ++ str ")"

       (* slice_get tag xs i → xs[i] — panics if out of bounds *)
       | MLglob r, [_tag; xs; i] when is_slice_get_ref r ->
           pp_expr state env xs ++ str "[" ++ pp_expr state env i ++ str "]"

       (* mutable cells: ref_new tag v → init value (bound as the Go var by the
          enclosing bind); ref_get r → read the var; ref_set r v → r = v.
          int/float literals are typed so the inferred Go var is int64/float64.
          [ref_new] now carries a leading [GoTypeTag] (the cell's element type, for
          the typed-heap model); it is proof-only, dropped at the call site. *)
       | MLglob r, [_tag; v] when is_ref_new_ref r -> pp_typed_lit state env v
       | MLglob r, [_tag; rf] when is_ref_get_ref r -> pp_expr state env rf
       | MLglob r, [rf; v] when is_ref_set_ref r ->
           pp_expr state env rf ++ str " = " ++ pp_typed_lit state env v

       (* Pointers (Phase B1).  [ptr_new tag v] → a fresh [*T] initialised to [v],
          emitted as the single-expression IIFE [func(_v T) *T { return &_v }(v)] (Go
          forbids [&expr]); the enclosing bind binds it as the Go pointer var.
          [ptr_get tag p] → [*p] (deref read); [ptr_set p v] → [*p = v] (deref write);
          [ptr_nil tag] → [nil]. *)
       | MLglob r, [tag; v] when is_ptr_new_ref r ->
           let t = go_type_of_tag (strip_magic tag) in
           str "func(_v " ++ str t ++ str ") *" ++ str t ++ str " { return &_v }(" ++
           pp_typed_lit state env v ++ str ")"
       (* go_new tag → new(T): a fresh *T pointing to the zero value *)
       | MLglob r, [tag] when is_go_new_ref r ->
           str "new(" ++ str (go_type_of_tag (strip_magic tag)) ++ str ")"
       | MLglob r, [_tag; p] when is_ptr_get_ref r ->
           str "*" ++ pp_atom state env p
       | MLglob r, [p; v] when is_ptr_set_ref r ->
           str "*" ++ pp_atom state env p ++ str " = " ++ pp_typed_lit state env v
       (* Struct pointers (Phase Bs.2).  The rep arg is proof-only (ignored here):
          [sptr_new rep v] → [&v] (composite literals are addressable in Go);
          [sptr_deref p] → [*p]; [sptr_get_field p idx proj _] → [p.Field] and
          [sptr_set_field p idx proj _ v] → [p.Field = v], the field NAME read off the
          passed projection (the same [record_proj_field] map [x.Field] uses). *)
       | MLglob r, [_rep; v] when is_sptr_new_ref r ->
           str "&" ++ pp_atom state env v
       | MLglob r, [p] when is_sptr_deref_ref r ->
           str "*" ++ pp_atom state env p
       | MLglob r, [p; _idx; proj; _ftag] when is_sptr_get_field_ref r ->
           let fld = (match strip_magic proj with
             | MLglob rp when is_record_proj rp -> proj_field_name rp
             | _ -> unsupported "a struct-pointer field read whose field arg is not a projection") in
           pp_atom state env p ++ str "." ++ str fld
       | MLglob r, [p; _idx; proj; _ftag; v] when is_sptr_set_field_ref r ->
           let fld = (match strip_magic proj with
             | MLglob rp when is_record_proj rp -> proj_field_name rp
             | _ -> unsupported "a struct-pointer field write whose field arg is not a projection") in
           pp_atom state env p ++ str "." ++ str fld ++ str " = " ++ pp_typed_lit state env v
       (* a TYPED nil pointer conversion to *T — so a [p != nil] comparison (in the
          safe deref) type-checks (a bare untyped [nil != nil] is a Go error). *)
       | MLglob r, [tag] when is_ptr_nil_ref r ->
           str "(*" ++ str (go_type_of_tag (strip_magic tag)) ++ str ")(nil)"

       (* Slices as aliasing handles (Phase B3).  SliceH lowers to Go []T (which IS
          the aliasing handle), so the ops are native: [make([]T,n)], [s[i]],
          [s[i] = v], [s[a:b]] (a sub-slice that SHARES the backing array). *)
       | MLglob r, [tag; n] when is_slice_make_h_ref r ->
           str "make([]" ++ str (go_type_of_tag (strip_magic tag)) ++ str ", "
           ++ pp_expr state env n ++ str ")"
       | MLglob r, [tag; len; cap] when is_slice_make_lc_ref r ->
           str "make([]" ++ str (go_type_of_tag (strip_magic tag)) ++ str ", "
           ++ pp_expr state env len ++ str ", " ++ pp_expr state env cap ++ str ")"
       | MLglob r, [_tag; s; i] when is_slice_idx_get_ref r ->
           pp_atom state env s ++ str "[" ++ pp_expr state env i ++ str "]"
       | MLglob r, [s; i; v] when is_slice_idx_set_ref r ->
           pp_atom state env s ++ str "[" ++ pp_expr state env i ++ str "] = "
           ++ pp_typed_lit state env v
       | MLglob r, [s; a; b] when is_subslice_ref r ->
           pp_atom state env s ++ str "[" ++ pp_expr state env a ++ str ":"
           ++ pp_expr state env b ++ str "]"
       (* slice_append tag s v → append(s, v) — Go's native append makes the
          within-cap (in-place, aliases) vs past-cap (reallocate) choice itself. *)
       | MLglob r, [_tag; s; v] when is_slice_append_h_ref r ->
           str "append(" ++ pp_expr state env s ++ str ", "
           ++ pp_typed_lit state env v ++ str ")"
       | MLglob r, [_tag; s] when is_slice_clear_h_ref r ->
           str "clear(" ++ pp_expr state env s ++ str ")"
       | MLglob r, [_tag; dst; src] when is_slice_copy_ref r ->
           str "copy(" ++ pp_expr state env dst ++ str ", " ++ pp_expr state env src ++ str ")"

       (* make_chan tag → make(chan T) *)
       | MLglob r, [tag] when is_make_chan_ref r ->
           str ("make(chan " ^ go_type_of_tag tag ^ ")")

       (* make_chan_buf tag n → make(chan T, n) *)
       | MLglob r, [tag; n] when is_make_chan_buf_ref r ->
           str ("make(chan " ^ go_type_of_tag tag ^ ", ") ++ pp_expr state env n ++ str ")"

       (* send tag ch v → ch <- v  (tag is the proof-only element type, dropped) *)
       | MLglob r, [_tag; ch; v] when is_send_ref r ->
           pp_expr state env ch ++ str " <- " ++ pp_expr state env v

       (* recv tag ch → <-ch *)
       | MLglob r, [_tag; ch] when is_recv_ref r ->
           str "<-" ++ pp_expr state env ch

       (* close_chan tag ch → close(ch)  (tag dropped) *)
       | MLglob r, [_tag; ch] when is_close_chan_ref r ->
           str "close(" ++ pp_expr state env ch ++ str ")"

       (* recv_ok in expression context — handled properly by pp_stmts;
          this fallback covers the rare case of expression-position use. *)
       | MLglob r, [_tag; ch; _kont] when is_recv_ok_ref r ->
           str "<-" ++ pp_expr state env ch

       (* zero_val tag → false / "" / nil / T(0) *)
       | MLglob r, [tag] when is_zero_val_ref r ->
           str (zero_of_tag tag)

       (* map_make_typed kt vt → make(map[K]V) with concrete types from tags *)
       | MLglob r, [kt; vt] when is_map_make_typed_ref r ->
           str ("make(map[" ^ go_type_of_tag kt ^ "]" ^ go_type_of_tag vt ^ ")")

       (* map_get_or: handled in pp_stmts/MLletin for clean two-statement emission *)

       (* map operations — the leading key/value [GoTypeTag]s are proof-only, dropped *)
       | MLglob r, [_kt; _vt; k; v; m] when is_map_set_ref r ->
           pp_expr state env m ++ str "[" ++ pp_expr state env k ++ str "] = " ++
           pp_expr state env v
       | MLglob r, [_kt; _vt; k; m]    when is_map_del_ref r ->
           str "delete(" ++ pp_expr state env m ++ str ", " ++ pp_expr state env k ++ str ")"
       (* clear(m) — Go 1.21 builtin, remove all map entries *)
       | MLglob r, [_kt; _vt; m]       when is_map_clear_ref r ->
           str "clear(" ++ pp_expr state env m ++ str ")"
       | MLglob r, [m]       when is_map_len_ref r ->
           str "len(" ++ pp_expr state env m ++ str ")"
       | MLglob r, []        when is_map_make_ref r ->
           str "make(map[any]any)" (* type params erased; real type comes from context *)

       (* min(a, b) / max(a, b) — Go 1.21 builtins (on [int]) *)
       | MLglob r, [a; b] when is_min_ref r ->
           str "min(" ++ pp_expr state env a ++ str ", " ++ pp_expr state env b ++ str ")"
       | MLglob r, [a; b] when is_max_ref r ->
           str "max(" ++ pp_expr state env a ++ str ", " ++ pp_expr state env b ++ str ")"
       (* slice_make tag n → make([]T, n) (fresh zeroed slice) *)
       | MLglob r, [tag; n] when is_slice_make_ref r ->
           str ("make([]" ^ go_type_of_tag tag ^ ", ") ++ pp_expr state env n ++ str ")"

       (* len(slice) / cap(slice) *)
       | MLglob r, [s] when is_len_ref r ->
           str "len(" ++ pp_expr state env s ++ str ")"
       | MLglob r, [s] when is_cap_ref r ->
           str "cap(" ++ pp_expr state env s ++ str ")"
       (* str_len s → int64(len(s)): Go's [len] gives the BYTE count as an [int];
          cast to int64 for the [int] model (Sint63/int64). *)
       | MLglob r, [s] when is_str_len_ref r ->
           str "int64(len(" ++ pp_expr state env s ++ str "))"
       (* append(xs, ys) *)
       | MLglob r, [xs; ys] when is_append_ref r ->
           str "append(" ++ pp_expr state env xs ++ str ", " ++
           pp_expr state env ys ++ str "...)"

       (* print/println(list GoAny) — unfold list, strip existT wrappers *)
       | MLglob r, [lst] when is_print_ref r || is_println_ref r ->
           let fname = global_basename r in
           let pp_pa e = pp_expr state env (any_payload e) in
           (match unfold_list [] lst with
            | Some pargs ->
                str (fname ^ "(") ++
                prlist_with_sep (fun () -> str ", ") pp_pa pargs ++
                str ")"
            | None ->
                unsupported (fname ^ " of a non-literal list (only statically-known argument lists are modeled)"))
       (* negb b → !b.  Unary [!] binds tighter than every binary operator, so
          [pp_atom] parenthesises the operand exactly when needed (e.g. a
          comparison: [!(x < y)]) and never otherwise ([!b]). *)
       | MLglob r, [b] when is_negb_ref r ->
           str "!" ++ pp_atom state env b

       (* fixed-width unary complement [uN_not x] / [iN_not x].  Go's unary [^] on
          the int64 CARRIER is the full 64-bit complement ([^240 = -241]), NOT the
          width-N complement — so it MUST be wrapped back to the width: unsigned
          [(^x) & 0xff] ([^240 → 15]); signed additionally sign-extends.  [fw_wrap]
          builds exactly that (same as the [lit]/[add] forms). *)
       | MLglob r, [x] when fw_is r "not" ->
           let (s, w, _) = Option.get (fixed_width_op r) in
           fw_wrap s w (str "^" ++ pp_atom state env x)
       (* full-width int64 unary complement [i64_not x] → [^x].  Go's [^] on an
          int64 IS the full 64-bit complement (= -x-1), exactly the model — no mask. *)
       | MLglob r, [x] when is_i64_op r "not" ->
           str "^" ++ pp_atom state env x
       (* full-width uint64 unary complement [u64_not x] → [^x].  Go's [^] on a
          uint64 gives the 64-bit bitwise complement, exactly [2^64-1-x] = the model. *)
       | MLglob r, [x] when is_u64_op r "not" ->
           str "^" ++ pp_atom state env x

       (* opp x → -x.  Unary [-] (like [!]) binds tighter than any binary op, so
          [pp_atom] parenthesises a compound operand and leaves an atom bare. *)
       | MLglob r, [x] when is_float_opp_ref r ->
           str "-" ++ pp_atom state env x
       (* i64_neg / u64_neg x → unary [-x] (the direct prefix, not the encoded [0 - x]) *)
       | MLglob r, [x] when is_i64_op r "neg" || is_u64_op r "neg" ->
           str "-" ++ pp_atom state env x

       (* numeric-wrapper projection [u8raw g] → [g] (the wrapper is erased) *)
       | MLglob r, [g] when is_numint_proj r ->
           pp_expr state env g

       (* record projection [field recv …] → field access [recv.Field], and when
          the field is a method dictionary entry (a function), its application
          [field recv arg…] is dynamic dispatch [recv.Field(arg…)]. *)
       | MLglob r, (recv :: rest) when is_record_proj r ->
           let fld = pp_atom state env recv ++ str "." ++ str (proj_field_name r) in
           (match rest with
            | [] -> fld
            | _  -> fld ++ str "(" ++
                    prlist_with_sep (fun () -> str ", ") (pp_expr state env) rest ++
                    str ")")

       (* Fixed-width integer arithmetic (uN/iN, any width via [fixed_width_op]):
          mask the result to the width so the int64 carrier wraps like Go's uintN,
          and (signed) sign-extend.  [fw_wrap] builds the masked/sign-extended form
          with the parens Go's precedence needs.  A W-bit MULTIPLY is exact for any
          [W < 63]: the product may exceed the 63-bit carrier, but the masked LOW W
          bits survive ([2^W | 2^63], so [(a*b mod 2^63) mod 2^W = a*b mod 2^W]).
          Only a [W >= 63]-wide product needs the Z model — it fails loud. *)
       (* [uN_lit x] (a typed constant) and [uN_of_int x] (a narrowing conversion)
          both truncate an [int] to the width — [fw_wrap] (mask, + sign-extend for
          intN), exactly Go's [uint8(x)] / [int8(x)]. *)
       | MLglob r, [x] when fw_is r "lit" || fw_is r "of_int" ->
           let (s, w, _) = Option.get (fixed_width_op r) in
           fw_wrap s w (pp_atom state env x)
       (* [int_of_FW x] — widen to [int]: identity (carrier already int64). *)
       | MLglob r, [x] when is_int_of_fw r ->
           pp_expr state env x
       | MLglob r, [a; b]
         when fw_is r "add" || fw_is r "sub" || fw_is r "mul" ->
           let (s, w, op) = Option.get (fixed_width_op r) in
           if String.equal op "mul" && w >= 63 then
             unsupported (string_of_int w ^ "-bit multiply: a >=63-bit-wide product exceeds the 63-bit carrier; needs the Z-based wide-int model")
           else
             let opstr = (match op with "add" -> " + " | "sub" -> " - " | _ -> " * ") in
             fw_wrap s w
               (str "(" ++ pp_expr state env a ++ str opstr ++ pp_expr state env b ++ str ")")

       (* fixed-width shifts.  [shl]: truncate to the width ([fw_wrap]) — uintN
          wraps mod 2^N, intN sign-extends.  [shr]: bare Go [x >> k] — the int64
          carrier is non-negative (uintN ⇒ logical shift) or sign-extended (intN ⇒
          arithmetic shift), both correct without a mask.  The count [k] is a
          plain [int]; the non-negativity proof was erased.  Over-width counts
          give 0 / sign-fill, matching Go (no upper limit). *)
       | MLglob r, [x; k] when fw_is r "shl" ->
           let (s, w, _) = Option.get (fixed_width_op r) in
           fw_wrap s w
             (str "(" ++ pp_expr state env x ++ str " << " ++ pp_expr state env k ++ str ")")
       | MLglob r, [x; k] when fw_is r "shr" ->
           str "(" ++ pp_expr state env x ++ str " >> " ++ pp_expr state env k ++ str ")"

       (* fixed-width division / remainder (divisor-non-zero proof erased).
          [uintN]: non-negative carrier ⇒ Go int64 [/]/[%] is the unsigned result,
          in range, no mask.  [intN]: signed [/]/[%] wrapped to the width ([fw_wrap])
          — needed for the [-2^(N-1) / -1] two's-complement overflow. *)
       | MLglob r, [a; b] when fw_is r "div" || fw_is r "mod" ->
           let (s, w, op) = Option.get (fixed_width_op r) in
           let opstr = if String.equal op "div" then " / " else " % " in
           let raw = str "(" ++ pp_expr state env a ++ str opstr ++ pp_expr state env b ++ str ")" in
           if s then fw_wrap s w raw else raw

       (* Nat.sub is truncated monus, NOT Go uint's wrapping [-] — fail loud
          rather than emit a value that is wrong on underflow (3 - 5 ≠ 2^64-2). *)
       | MLglob r, [_; _] when is_nat_sub_ref r ->
           unsupported "Nat.sub: Coq's truncated subtraction (3 - 5 = 0) does not match Go uint's wrapping minus (3 - 5 = 2^64-2); use int (Sint63) subtraction, or a b<=a-guarded monus"

       (* PrimInt63.ltb/leb are UNSIGNED comparisons; Go int64 </<= are SIGNED and
          disagree on high-bit values — fail loud rather than emit a wrong [<]. *)
       | MLglob r, [_; _] when is_int63_op_ref r "ltb" || is_int63_op_ref r "leb" ->
           unsupported "PrimInt63.ltb/leb (UNSIGNED comparison): Go int64 </<= are SIGNED and disagree once the high bit is set; use Sint63.ltb/leb (signed) for int"

       (* [i64_lit z] — a full-width int64 constant: fold its [Z] literal to the
          decimal int64 (the proof arg was erased).  The smart constructor demanded
          [in_i64 z], so the value fits int64. *)
       | MLglob r, [z] when is_i64_lit r ->
           (match z_value z with
            | Some v -> str (Int64.to_string v)
            | None   -> unsupported "i64_lit of a non-literal Z (only statically-known int64 constants are modeled)")
       (* [u64_lit z] — a full-width uint64 constant: fold its [Z] literal to the
          UNSIGNED decimal ([Printf.sprintf "%Lu"] handles values in [2^63, 2^64)
          that are "negative" in OCaml's signed [Int64.t] — they print as the
          correct positive decimal). *)
       | MLglob r, [z] when is_u64_lit r ->
           (match z_value z with
            | Some v -> str (Printf.sprintf "%Lu" v)
            | None   -> unsupported "u64_lit of a non-literal Z (only statically-known uint64 constants are modeled)")
       (* Inlined binary operator (bool / float / nat / int63 / signed int63),
          printed with Go operator precedence.  At top level there is no
          surrounding operator, so the operands are printed at this op's level
          (left) and one tighter (right, for left-associativity); [pp_prec] adds
          parens only where genuinely needed. *)
       | MLglob r, [a; b] when Option.has_some (binop_of r) ->
           let (p, opstr) = Option.get (binop_of r) in
           pp_prec state env p a ++ str opstr ++ pp_prec state env (p + 1) b
       (* method call [m recv a1 … an] → [recv.M(a1, …, an)] (value receiver).
          The first visible arg is the receiver, pulled out before the dot. *)
       | MLglob r, (recv :: rest) when is_method r ->
           pp_atom state env recv ++ str "." ++ str (go_export (global_basename r)) ++
           str "(" ++
           prlist_with_sep (fun () -> str ", ") (pp_expr state env) rest ++
           str ")"
       | _ ->
           pp_atom state env head ++ str "(" ++
           prlist_with_sep (fun () -> str ", ")
             (pp_expr state env) vis ++
           str ")")

  | MLlam _ as e ->
      let ids, body = collect_lam e in
      let vis = List.filter (fun id -> not (is_dummy id)) ids in
      let new_env = List.rev ids @ env in
      str "func(" ++
      prlist_with_sep (fun () -> str ", ")
        (fun id -> pp_mlident id ++ str " any") vis ++
      str ") any {" ++ fnl () ++
      str "\treturn " ++ pp_expr state new_env body ++ fnl () ++
      str "}"

  | MLglob r ->
      if is_bool_true r     then str "true"
      else if is_bool_false r then str "false"
      else if is_map_make_ref r || is_map_make_typed_ref r then str "make(map[any]any)"
      else str (go_export (global_basename r))

  | MLfix (i, names, _) ->
      str (go_export (go_safe (Id.to_string names.(i))))

  | MLletin (id, e1, e2) ->
      let new_env = id :: env in
      str "(func() any {" ++ fnl () ++
      str "\t" ++ pp_mlident id ++ str " := " ++ pp_expr state env e1 ++ fnl () ++
      str "\treturn " ++ pp_expr state new_env e2 ++ fnl () ++
      str "})()"

  (* record constructor [MkT a1 … an] → Go struct literal [T{a1, …, an}].  A
     field whose type is a function (an interface METHOD dictionary entry) and
     whose argument is a lambda is emitted as a TYPED Go closure, so the literal
     matches the field type [func(A) R] rather than the generic [func(any) any].
     This is what makes the dictionary's method entries well-typed. *)
  | MLcons (_, r, args) when is_record_ctor r ->
      let ftypes = (try Hashtbl.find record_ctor_ftypes (global_basename r) with Not_found -> []) in
      let rec pp_fields ts args = match ts, args with
        | _, [] -> []
        | (Tarr _ as t) :: ts', (MLlam _ as a) :: args' ->
            pp_typed_closure state env t a :: pp_fields ts' args'
        | _ :: ts', a :: args' -> pp_expr state env a :: pp_fields ts' args'
        | [],       a :: args' -> pp_expr state env a :: pp_fields [] args'
      in
      str (record_ctor_tyname r) ++ str "{" ++
      prlist_with_sep (fun () -> str ", ") (fun x -> x) (pp_fields ftypes args) ++ str "}"

  | MLcons _ as e when Option.has_some (decode_go_string e) ->
      (* Coq [string] literal → Go string literal (byte-faithful). *)
      str (go_string_lit (Option.get (decode_go_string e)))

  | MLcons _ as e when Option.has_some (z_value e) ->
      (* Coq [Z] literal (a full-width [GoI64]/[GoU64] literal erases its single-field
         wrapper to this bare [Z]).  [z_value] preserves the 64-bit pattern but
         [Int64.to_string] reads it SIGNED.  Discriminate by the SOURCE Z's sign: a
         [Zpos] whose 64-bit value has the high bit set (negative as [Int64]) cannot be
         an [int64] (its range stops at 2^63-1), so it is an unsigned [uint64] value in
         [2^63, 2^64) and must print via [%Lu]; everything else (incl. genuine [Zneg]
         int64 negatives) prints signed. *)
      let v = Option.get (z_value e) in
      if z_is_nonneg e && Int64.compare v 0L < 0
      then str (Printf.sprintf "%Lu" v)
      else str (Int64.to_string v)

  | MLcons _ as e ->
      (match nat_value e with
       | Some n -> str (string_of_int n)
       | None   ->
           match e with
           | MLcons (_, r, []) when is_unit_tt r    -> mt ()
           | MLcons (_, r, []) when is_bool_true r  -> str "true"
           | MLcons (_, r, []) when is_bool_false r -> str "false"
           | MLcons (_, r, []) when is_list_nil r   -> str "nil"
           (* [any v] (existT) in value position → its payload [v], which Go boxes to
              [any] from the surrounding context (a func arg / slot of interface type).
              [any_payload] strips the existT (and any nested pair) to the value. *)
           | MLcons (_, r, _) when is_existT_ref r -> pp_expr state env (any_payload e)
           (* numeric-wrapper constructor [MkU8 v] → [v] (erase the wrapper) *)
           | MLcons (_, r, [v]) when is_numint_ctor r -> pp_expr state env v
           | MLcons (_, r, _) as lst ->
               (* Non-empty list literal: emit as append(nil, v1, v2, ...).
                  Go infers the slice element type from the values. *)
               (match unfold_list [] lst with
                | Some elems ->
                    str "append(nil, " ++
                    prlist_with_sep (fun () -> str ", ")
                      (pp_expr state env) elems ++
                    str ")"
                | None when is_list_cons r ->
                    unsupported "a list with a non-literal spine (cons whose tail is not statically known)"
                | None ->
                    unsupported ("constructor " ^ global_basename r ^
                                 " (only nat/bool/list/option constructors are modeled)"))
           | _ -> unsupported "a non-constructor value in constructor position")

  | MLmagic e -> pp_expr state env e
  | MLdummy _ -> str "_"
  | MLfloat f ->
      let s = Float64.to_string f in
      (* Ensure Go sees a float literal: add .0 if no decimal or exponent. *)
      let s = if String.contains s '.' || String.contains s 'e'
                 || String.contains s 'E' || String.contains s 'n'
              then s else s ^ ".0" in
      str s
  | MLexn s   -> str ("panic(" ^ Printf.sprintf "%S" s ^ ")")
  | MLaxiom s -> str ("panic(\"axiom: " ^ String.escaped s ^ "\")")
  | MLuint n  ->
      (* [int] is SIGNED (Sint63): a 63-bit value with its sign bit (bit 62) set
         — i.e. [>= 2^62] — denotes a NEGATIVE number ([value - 2^63]).  The raw
         unsigned [Uint63.to_string] would print e.g. [-1] as 9223372036854775807,
         a different value.  Emit the signed decimal instead.  (Positive literals
         [< 2^62] are unchanged.) *)
      let i64 = Uint63.to_int64 n in
      let s =
        if Int64.compare i64 0x4000000000000000L >= 0   (* sign bit set (>= 2^62) *)
        then Int64.to_string (Int64.add i64 Int64.min_int)  (* i64 - 2^63 (two's comp.) *)
        else Int64.to_string i64 in
      str s
  | _         -> unsupported "this expression (unmodeled MiniML node in value position)"

and pp_atom state env e =
  match e with
  | MLrel _ | MLglob _ | MLcons _ | MLuint _ | MLfloat _ | MLmagic _ ->
      pp_expr state env e
  | _ ->
      str "(" ++ pp_expr state env e ++ str ")"

(* Print [e] as an operand inside a binary operator whose context requires
   precedence [>= ctx].  If [e] is itself a binary operator of lower precedence,
   wrap it in parens; otherwise emit it bare (atoms and calls bind tightest, so
   they never need parens — unlike [pp_atom], which parenthesises every non-atom).
   A binop at the same precedence as [ctx] does NOT need parens on the left (Go's
   operators are left-associative); the caller passes [p+1] for the right
   operand to force parens there. *)
and pp_prec state env ctx e =
  match strip_magic e with
  | MLapp (h, args) ->
      let h2, all = collect_app h args in
      let vis = List.filter (fun a -> not (is_erased a)) all in
      (match h2, vis with
       | MLglob r, [a; b] when Option.has_some (binop_of r) ->
           let (p, opstr) = Option.get (binop_of r) in
           let inner = pp_prec state env p a ++ str opstr ++ pp_prec state env (p + 1) b in
           if p < ctx then str "(" ++ inner ++ str ")" else inner
       | _ -> pp_expr state env e)
  | _ -> pp_expr state env e

(* An integer/float literal carries no Go type, so where the target type must be
   fixed (a value boxed into [any], a typed cell/accumulator, …) wrap a bare
   literal as [int64(..)] / [float64(..)].  Non-literals already have a type. *)
and pp_typed_lit state env e =
  match strip_magic e with
  | MLuint _  -> str "int64("   ++ pp_expr state env e ++ str ")"
  | MLfloat _ -> str "float64(" ++ pp_expr state env e ++ str ")"
  (* A full-width [GoI64]/[GoU64] LITERAL erases (single-field record) to a bare [Z]
     decimal, which Go defaults to [int]; in a typed position it mismatches the
     [int64]/[uint64] context, so pin the type.  Signedness from the source-[Z] sign
     (same discrimination as the bare-[Z] value arm): a [Zpos] >= 2^63 is [uint64].
     (When the tag is known — e.g. a map default — [pp_typed_lit_tagged] is preferred.) *)
  | MLcons _ as z when Option.has_some (z_value z) ->
      let v = Option.get (z_value z) in
      let goty = if z_is_nonneg z && Int64.compare v 0L < 0 then "uint64" else "int64" in
      str goty ++ str "(" ++ pp_expr state env e ++ str ")"
  | _         -> pp_expr state env e

(* Like [pp_typed_lit] but the target Go type is KNOWN (from a [GoTypeTag] in the
   caller, e.g. a map's value tag): pin a bare literal to exactly that type, so a
   [uint64] default reads [uint64(0)], not the [int64(0)] [pp_typed_lit] would guess.
   A non-literal already carries its type, so it is emitted unwrapped. *)
and pp_typed_lit_tagged state env tagdoc e =
  match strip_magic e with
  | MLuint _ | MLfloat _ -> tagdoc ++ str "(" ++ pp_expr state env e ++ str ")"
  | MLcons _ as z when Option.has_some (z_value z) ->
      tagdoc ++ str "(" ++ pp_expr state env e ++ str ")"
  | _ -> pp_expr state env e

(* Emit a lambda as a TYPED Go closure [func(x A) R { return body }] using a known
   function type [A1 -> … -> R] (a method dictionary entry).  Unlike the generic
   [MLlam] arm (which emits [func(x any) any]), this matches the struct field type,
   so the dictionary literal type-checks.  The binders stay in the de Bruijn env. *)
and pp_typed_closure state env ftype lam =
  let rec split_arrows = function
    | Tarr (a, b) -> let (args, ret) = split_arrows b in (a :: args, ret)
    | t -> ([], t)
  in
  let argtypes, rettype = split_arrows ftype in
  let ids, body = collect_lam lam in
  let rec zip ids ts = match ids, ts with
    | [], _ -> []
    | id :: ids', t :: ts' -> (id, t) :: zip ids' ts'
    | id :: ids', []       -> (id, Tunknown) :: zip ids' []
  in
  let pairs = zip ids argtypes in
  let new_env = List.rev ids @ env in
  str "func(" ++
  prlist_with_sep (fun () -> str ", ")
    (fun (id, t) -> pp_mlident id ++ str " " ++ pp_type state t) pairs ++
  str ") " ++ pp_type state rettype ++ str " {" ++ fnl () ++
  str "\treturn " ++ pp_expr state new_env body ++ fnl () ++
  str "}"

(*s CFG blocks (the goto model).  A block is an [IO Next]: it does its effects
    then transfers control via [ret (Jump n)] (goto block n) or [ret Done]
    (return).  [block_targets] collects the Jump targets (so only used labels
    are emitted — Go rejects unused ones); [emit_block] prints one block. *)

and block_targets b acc =
  let h, args = collect_app b [] in
  let vis = List.filter (fun a -> not (is_erased a)) args in
  match h, vis with
  | MLglob r, [_action; k] when is_bind_ref r ->
      let _ids, kbody = collect_lam k in block_targets kbody acc
  | MLglob r, [next] when is_ret_ref r ->
      (match next with
       | MLcons (_, c, [n]) when is_jump_ctor c ->
           (match nat_value n with Some v -> v :: acc | None -> acc)
       | _ -> acc)
  | _ ->
      (match b with
       | MLcase (_, _, branches) ->
           Array.fold_left (fun a (_, _, body) -> block_targets body a) acc branches
       | _ -> acc)

(* Does any tail of this block [ret Done] (return)?  A block can both return on
   one branch and jump on another, so this is independent of [block_targets].
   Used to give every returning block an edge to the virtual exit node when
   computing post-dominators for the structurer. *)
and block_has_done b =
  let h, args = collect_app b [] in
  let vis = List.filter (fun a -> not (is_erased a)) args in
  match h, vis with
  | MLglob r, [_action; k] when is_bind_ref r ->
      let _ids, kbody = collect_lam k in block_has_done kbody
  | MLglob r, [next] when is_ret_ref r ->
      (match next with MLcons (_, c, []) when is_done_ctor c -> true | _ -> false)
  | _ ->
      (match b with
       | MLcase (_, _, branches) ->
           Array.exists (fun (_, _, body) -> block_has_done body) branches
       | _ -> false)

(* Loop-block-local variables (bound by a [bind] inside a block) are hoisted to
   [var x T] before the blocks so their declaration dominates all uses and is
   never re-run on a backward jump.  Only tagged reads ([ref_get tag r]) are
   hoistable here, since that is where the Go type is known. *)
and block_hoists b acc =
  let h, args = collect_app b [] in
  let vis = List.filter (fun a -> not (is_erased a)) args in
  match h, vis with
  | MLglob r, [action; k] when is_bind_ref r ->
      let ids, kbody = collect_lam k in
      let acc' =
        (match List.filter (fun id -> not (is_dummy id)) ids with
         | [x] ->
             let ah, aargs = collect_app action [] in
             let avis = List.filter (fun a -> not (is_erased a)) aargs in
             (match ah, avis with
              | MLglob rr, [tag; _rf] when is_ref_get_ref rr ->
                  (x, go_type_of_tag tag) :: acc
              | _ -> acc)
         | _ -> acc) in
      block_hoists kbody acc'
  | _ ->
      (match b with
       | MLcase (_, _, branches) ->
           Array.fold_left (fun a (_, _, body) -> block_hoists body a) acc branches
       | _ -> acc)

(* Emit one [bind] action (the non-control part of a block) as a Go statement.
   [ids] are the continuation's binders (for the [x =] lhs of a value read).
   - a [defer]/[go] closure captures the hoisted loop-temps BY VALUE
     ([kw func(iv T){ body }(iv)]) so each iteration's value is fixed here;
     capturing the shared hoisted cell by reference would give every closure the
     last value (the goto form of Go's loop-variable capture; the structured
     [for] lowering gets this free on Go 1.22+).  A closure body is not part of
     the CFG, so it terminates with [raw_term] (return).
   - a value read ([ref_get]) goes to its hoisted var with [=]; a void action
     (println / ref_set, whose [fun _] may extract as a named var) is just the
     statement. *)
and emit_action state hoists tab env ids action =
  let emit_closure kw body =
    let sep = prlist_with_sep (fun () -> str ", ") (fun x -> x) in
    let params = List.map (fun (x, t) -> pp_mlident x ++ str " " ++ str t) hoists in
    let args   = List.map (fun (x, _) -> pp_mlident x) hoists in
    str tab ++ str kw ++ str " func(" ++ sep params ++ str ") {" ++ fnl () ++
    emit_block state hoists raw_term (tab ^ "\t") env body ++
    str tab ++ str "}(" ++ sep args ++ str ")" ++ fnl ()
  in
  let ah, aargs = collect_app action [] in
  let avis = List.filter (fun a -> not (is_erased a)) aargs in
  match ah, avis with
  | MLglob rr, [dbody] when is_defer_call_ref rr -> emit_closure "defer" dbody
  | MLglob rr, [gbody] when is_go_spawn_ref rr -> emit_closure "go" gbody
  | _ ->
      let action_is_value =
        (match ah with MLglob rr -> is_ref_get_ref rr | _ -> false) in
      let lhs = match List.filter (fun id -> not (is_dummy id)) ids with
        | [x] when action_is_value -> pp_mlident x ++ str " = "
        | _   -> mt () in
      str tab ++ lhs ++ pp_expr state env action ++ fnl ()

(* [term] handles the block's terminator (a [Next] value) — see [raw_term] /
   [loop_term].  This is what lets one emitter print raw goto or structured
   continue/break. *)
and emit_block state hoists term tab env b =
  let h, args = collect_app b [] in
  let vis = List.filter (fun a -> not (is_erased a)) args in
  match h, vis with
  | MLglob r, [action; k] when is_bind_ref r ->
      let ids, kbody = collect_lam k in
      let new_env = List.rev ids @ env in
      emit_action state hoists tab env ids action
      ++ emit_block state hoists term tab new_env kbody
  | MLglob r, [next] when is_ret_ref r -> term tab next
  | _ ->
      (match b with
       | MLcase (_, scrut, branches) ->
           (match Array.to_list branches with
            | [ (_, p1, body1); (_, p2, body2) ] ->
                let cref p = (match p with Pusual r | Pcons (r, _) -> Some r | _ -> None) in
                (match cref p1, cref p2 with
                 | Some c1, Some c2
                   when (is_bool_true c1 && is_bool_false c2)
                     || (is_bool_false c1 && is_bool_true c2) ->
                     let tb, eb = if is_bool_true c1 then body1, body2 else body2, body1 in
                     str tab ++ str "if " ++ pp_expr state env scrut ++ str " {" ++ fnl () ++
                     emit_block state hoists term (tab ^ "\t") env tb ++
                     str tab ++ str "} else {" ++ fnl () ++
                     emit_block state hoists term (tab ^ "\t") env eb ++
                     str tab ++ str "}" ++ fnl ()
                 | _ -> str tab ++ str "return" ++ fnl ())
            | _ -> str tab ++ str "return" ++ fnl ())
       | _ -> str tab ++ pp_expr state env b ++ fnl ())

(*s IO body emitter — shared by pp_function (for IO-returning fns) and pp_main_body. *)

let pp_io_body state tab env body =
  let is_void_call e =
    match collect_app e [] with
    | MLglob r, _ when is_print_ref r || is_println_ref r || is_panic_ref r
                       || is_map_set_ref r || is_map_del_ref r || is_map_clear_ref r
                       || is_send_ref r || is_close_chan_ref r || is_ref_set_ref r -> true
    | _ -> false
  in
  let is_terminating e =
    match collect_app e [] with
    | MLglob r, _ when is_panic_ref r -> true
    | _ -> false
  in
  let rec pp_stmts tab env = function
    | MLapp _ as e ->
        let head, all_args = collect_app e [] in
        let vis = List.filter (fun a -> not (is_erased a)) all_args in
        (match head, vis with
         | MLglob r, [m; f] when is_bind_ref r ->
             (match strip_magic m with
              (* IO-valued branch: [bind] distributes over the match, turning it
                 into an if/switch statement rather than a value. *)
              | MLcase (typ, scrut, branches) ->
                  (match collect_lam f with
                   (* RESULT DISCARDED ([fun _ => rest], bound var unused): the
                      arms need no continuation — each arm's effects run and both
                      fall through to [rest], emitted ONCE after the if/else.  This
                      avoids exponential blow-up: chaining N inline [if]s in one
                      [bind] otherwise duplicates the tail into 2^(N-1) copies. *)
                   | ([id], fbody) when is_dummy id || db1_free fbody ->
                       emit_case tab env typ scrut branches (fun _nb b -> b) ++
                       pp_stmts tab (id :: env) fbody
                   (* RESULT USED: thread the continuation into every arm (lifting
                      it past each arm's pattern binders so its de Bruijn indices
                      stay correct), so each arm's value flows into it. *)
                   | _ ->
                       emit_case tab env typ scrut branches
                         (fun nb b -> MLapp (head, [b; ast_lift nb f])))
              | _ ->
             let ids, body = collect_lam f in
             let new_env = List.rev ids @ env in
             (* go_spawn: bind (go_spawn goroutine_body) f → go func(){body}(); f.
                Strip [MLmagic] coercions so a wrapped action (e.g. a nested
                [bind]) is still recognised by its head. *)
             let h_m, all_m = collect_app (strip_magic m) [] in
             let vis_m = List.filter (fun a -> not (is_erased a)) all_m in
             (match h_m, vis_m with
              | MLglob r2, [goroutine_body] when is_go_spawn_ref r2 ->
                  str tab ++ str "go func() {" ++ fnl () ++
                  pp_stmts (tab ^ "\t") env goroutine_body ++
                  str tab ++ str "}()" ++ fnl () ++
                  pp_stmts tab new_env body
              | MLglob r2, [defer_body] when is_defer_call_ref r2 ->
                  str tab ++ str "defer func() {" ++ fnl () ++
                  pp_stmts (tab ^ "\t") env defer_body ++
                  str tab ++ str "}()" ++ fnl () ++
                  pp_stmts tab new_env body
              (* the action is itself a compound IO computation (a nested [bind],
                 e.g. a fused map_get_opt/match) whose result is discarded — emit
                 it as statements, then the continuation.  ([fun _ =>] may extract
                 as a named-but-unused binder, so check [db1_free], not [is_dummy]). *)
              | MLglob r2, _ when is_bind_ref r2
                              && (List.filter (fun id -> not (is_dummy id)) ids = []
                                  || db1_free body) ->
                  pp_stmts tab env (strip_magic m) ++ pp_stmts tab new_env body
              | MLglob r2, [xs; bodyfn] when is_for_each_ref r2 ->
                  emit_for_each tab env xs bodyfn ++
                  pp_stmts tab new_env body
              (* bind (map_get_or k def m) (fun hit => body) → the value at k or
                 def, via comma-ok.  [hit] takes the default's (typed) value, so
                 it has the map's value type (not [any]); the if-block overwrites
                 it on a hit. *)
              | MLglob r2, [_kt; _vt; k; def; mm] when is_map_get_or_ref r2 ->
                  let hit = match List.filter (fun id -> not (is_dummy id)) ids with
                    | [id] -> pp_mlident id | _ -> str "_g" in
                  (* pin the default to the map's VALUE type (from [_vt]) — for a
                     bare full-width literal that would otherwise default to Go [int]
                     and mismatch an [int64]/[uint64] value slot. *)
                  str tab ++ hit ++ str " := " ++
                  pp_typed_lit_tagged state env (str (go_type_of_tag (strip_magic _vt))) def ++ fnl () ++
                  str tab ++ str "if _v, _ok := " ++ pp_expr state env mm ++
                  str "[" ++ pp_expr state env k ++ str "]; _ok {" ++ fnl () ++
                  str (tab ^ "\t") ++ hit ++ str " = _v" ++ fnl () ++
                  str tab ++ str "}" ++ fnl () ++
                  pp_stmts tab new_env body
              (* bind (map_get_opt k m) (fun o => match o with Some v => A | None => B)
                 → comma-ok if/else.  No [option] value is built; the bound [o] is
                 consumed by the match, so [v] is bound directly from m[k]. *)
              | MLglob r2, [_kt; _vt; k; mm] when is_map_get_opt_ref r2 ->
                  let fallback () =
                    unsupported "a map_get_opt result that is not immediately matched (the option has no Go representation; bind it directly to a Some v / None match)" in
                  (match ids, strip_magic body with
                   | [o_id], MLcase (_, scrut, branches)
                     when (match strip_magic scrut with MLrel 1 -> true | _ -> false) ->
                       (match Array.to_list branches with
                        | [ (bids1, p1, b1); (bids2, p2, b2) ] ->
                            let cref p = (match p with Pusual r | Pcons (r, _) -> Some r | _ -> None) in
                            (match cref p1, cref p2 with
                             | Some c1, Some c2
                               when (is_some_ctor c1 && is_none_ctor c2)
                                 || (is_none_ctor c1 && is_some_ctor c2) ->
                                 let some_ids, some_body, none_body =
                                   if is_some_ctor c1 then bids1, b1, b2
                                   else bids2, b2, b1 in
                                 let v_doc = match some_ids with
                                   | [vid] when not (is_dummy vid) -> pp_mlident vid
                                   | _ -> str "_" in
                                 str tab ++ str "if " ++ v_doc ++ str ", _ok := "
                                   ++ pp_expr state env mm ++ str "["
                                   ++ pp_expr state env k ++ str "]; _ok {" ++ fnl () ++
                                 pp_stmts (tab ^ "\t") (List.rev some_ids @ (o_id :: env)) some_body ++
                                 str tab ++ str "} else {" ++ fnl () ++
                                 pp_stmts (tab ^ "\t") (o_id :: env) none_body ++
                                 str tab ++ str "}" ++ fnl ()
                             | _ -> fallback ())
                        | _ -> fallback ())
                   | _ -> fallback ())
              | _ ->
             let vis_ids = List.filter (fun id -> not (is_dummy id)) ids in
             let lhs = match vis_ids with
               | _ when is_void_call m   -> mt ()
               | _ when db1_free body    ->
                   (* If the head is a known value-returning builtin (slice_get,
                      type_assert, etc.), discard with _ =.
                      User-defined functions have unknown return — assume void. *)
                   let head_m, _ = collect_app m [] in
                   (match head_m with
                    | MLglob r when is_slice_get_ref r || is_type_assert_ref r
                                 || is_map_len_ref r || is_map_get_or_ref r
                                 || is_len_ref r || is_cap_ref r
                                 || is_recv_ref r || is_make_chan_ref r || is_make_chan_buf_ref r
                                 || Option.has_some (classify_nat_op r)
                                 || Option.has_some (classify_int63_op r)
                                 || Option.has_some (classify_float_op r) -> str "_ = "
                    | _                              -> mt ())
               | [id]                    -> pp_mlident id ++ str " := "
               | _                       -> mt ()
             in
             (* Bare MLglob in statement position is a zero-arg IO function
                call — needs () in Go.  Builtins handle their own emission. *)
             let emit_m =
               match m with
               | MLglob r when not (is_bool_true r || is_bool_false r
                                    || is_unit_tt r || is_map_make_ref r) ->
                   pp_expr state env m ++ str "()"
               | _ -> pp_expr state env m
             in
             str tab ++ lhs ++ emit_m ++ fnl () ++
             (if is_terminating m then mt ()
              else pp_stmts tab new_env body)))
         | MLglob r, [_] when is_ret_ref r -> mt ()
         (* for_each in tail position (not inside a bind) *)
         | MLglob r, [xs; bodyfn] when is_for_each_ref r ->
             emit_for_each tab env xs bodyfn
         (* run_blocks start [b0; b1; …] → Go labels + goto.  Only Jump-target
            labels are emitted (Go rejects unused labels); the label sits one
            indent left of its block, as gofmt wants. *)
         | MLglob r, [start; blocks] when is_run_blocks_ref r ->
             (match unfold_list [] blocks with
              | Some bs ->
                  (* Distinct goto-blocks can reuse the same Rocq binder name
                     (each is a closed term, so Rocq does not alpha-rename across
                     them); they become one reused Go variable, assigned before
                     each read, so collapse same-named hoists to a single [var]. *)
                  let hoists =
                    let raw = List.fold_left (fun a b -> block_hoists b a) [] bs in
                    let key = function Id v | Tmp v -> Id.to_string v | Dummy -> "_" in
                    let seen = ref [] in
                    List.filter (fun (x, _) ->
                      let k = key x in
                      if List.mem k !seen then false else (seen := k :: !seen; true)) raw in
                  let start_v = match nat_value start with Some v -> v | None -> 0 in
                  (* hoisted declarations: var x T, dominating every block *)
                  let hoist_doc =
                    prlist
                      (fun (x, t) ->
                         str tab ++ str "var " ++ pp_mlident x ++ str " "
                         ++ str t ++ fnl ())
                      (List.rev hoists) in
                  let blocks = Array.of_list bs in
                  let nblk = Array.length blocks in
                  let exit_node = nblk in
                  let inter a b = List.filter (fun x -> List.mem x b) a in
                  let targets_of i =
                    List.sort_uniq compare (block_targets blocks.(i) []) in
                  (* raw labels + goto: the always-correct fallback.  Only Jump-
                     target labels are emitted (Go rejects unused ones); a label
                     sits one indent left of its block, as gofmt wants. *)
                  let emit_raw () =
                    let targets =
                      List.fold_left (fun a b -> block_targets b a) [] bs in
                    let is_target i = List.mem i targets in
                    let lbl_indent =
                      if String.length tab >= 1
                      then String.sub tab 0 (String.length tab - 1) else "" in
                    let initial =
                      if start_v = 0 then mt ()
                      else str tab ++ str (Printf.sprintf "goto block%d" start_v) ++ fnl () in
                    let rec emit_all i = function
                      | [] -> mt ()
                      | bi :: rest ->
                          (if is_target i
                           then str lbl_indent ++ str (Printf.sprintf "block%d:" i) ++ fnl ()
                           else mt ()) ++
                          emit_block state hoists raw_term tab env bi ++
                          emit_all (i + 1) rest
                    in
                    hoist_doc ++ initial ++ emit_all 0 bs in
                  if nblk = 0 then emit_raw () else begin
                  (* Unified structurer (a relooper) — lifts the goto-CFG back to
                     Go spec "If statements" / "For statements" / "Break/Continue" /
                     "Goto statements" / "Return statements".  Build dominators and
                     post-dominators, identify natural loops, then recursively emit:
                     - a loop header → [for { <body> }] with back-edges becoming
                       loop-around (fall-through) or [continue], the single exit
                       becoming [break];
                     - a conditional → [if]/[else] whose branches run up to their
                       merge (the post-dominator), the merge emitted once after.
                     Anything that breaks the structured assumptions (non-zero
                     entry, a multi-exit loop, improperly nested loops) falls back
                     to raw labels+goto, which is always correct. *)
                  let succ_exit i =
                    let ts = targets_of i in
                    if block_has_done blocks.(i) then exit_node :: ts else ts in
                  (* post-dominators (iterative fixpoint; valid with cycles) *)
                  let full = Array.to_list (Array.init (nblk + 1) (fun i -> i)) in
                  let pdom = Array.make (nblk + 1) full in
                  pdom.(exit_node) <- [exit_node];
                  let pchanged = ref true in
                  while !pchanged do
                    pchanged := false;
                    for i = nblk - 1 downto 0 do
                      let common = match succ_exit i with
                        | [] -> [exit_node]
                        | s0 :: rest ->
                            List.fold_left (fun acc x -> inter acc pdom.(x)) pdom.(s0) rest in
                      let d = List.sort_uniq compare (i :: common) in
                      if d <> pdom.(i) then (pdom.(i) <- d; pchanged := true)
                    done
                  done;
                  (* dominators (iterative fixpoint) *)
                  let preds = Array.make nblk [] in
                  for i = 0 to nblk - 1 do
                    List.iter (fun t -> if t >= 0 && t < nblk then preds.(t) <- i :: preds.(t))
                      (targets_of i)
                  done;
                  let alln = Array.to_list (Array.init nblk (fun i -> i)) in
                  let domA = Array.make nblk alln in
                  if start_v >= 0 && start_v < nblk then domA.(start_v) <- [start_v];
                  let dchanged = ref true in
                  while !dchanged do
                    dchanged := false;
                    for i = 0 to nblk - 1 do
                      if i <> start_v then begin
                        let d = match preds.(i) with
                          | [] -> domA.(i)
                          | p0 :: rest ->
                              List.fold_left (fun acc p -> inter acc domA.(p)) domA.(p0) rest in
                        let d = List.sort_uniq compare (i :: d) in
                        if d <> domA.(i) then (domA.(i) <- d; dchanged := true)
                      end
                    done
                  done;
                  let dominates a b =
                    a >= 0 && a < nblk && b >= 0 && b < nblk && List.mem a domA.(b) in
                  (* back-edges (n → h with h dominating n) and natural loops *)
                  let back_edges = ref [] in
                  for n = 0 to nblk - 1 do
                    List.iter (fun h -> if dominates h n then back_edges := (n, h) :: !back_edges)
                      (targets_of n)
                  done;
                  let headers = List.sort_uniq compare (List.map snd !back_edges) in
                  let loop_body h =
                    let body = ref [h] in
                    let stack = ref [] in
                    List.iter (fun (n, hh) ->
                      if hh = h && not (List.mem n !body) then (body := n :: !body; stack := n :: !stack))
                      !back_edges;
                    while !stack <> [] do
                      let n = List.hd !stack in stack := List.tl !stack;
                      List.iter (fun p ->
                        if not (List.mem p !body) then (body := p :: !body; stack := p :: !stack))
                        preds.(n)
                    done;
                    List.sort_uniq compare !body in
                  let bodies = List.map (fun h -> (h, loop_body h)) headers in
                  let body_of h = try List.assoc h bodies with Not_found -> [h] in
                  let loop_exit_of h =
                    let b = body_of h in
                    let ex = ref [] in
                    List.iter (fun n ->
                      List.iter (fun t ->
                        if not (List.mem t b) && not (List.mem t !ex) then ex := t :: !ex)
                        (targets_of n)) b;
                    !ex in
                  (* Loop nesting.  [a] is an ancestor of [h] when its body
                     strictly contains h's.  An exit of [h] that targets an
                     ancestor's header (a labeled [continue]) or an ancestor's
                     exit (a labeled [break]) escapes the enclosing loop; the one
                     remaining exit, if any, is h's *primary* exit, emitted after
                     the [for].  (A jump that escapes more than the innermost loop
                     is exactly Go's labeled break/continue.) *)
                  let subset a b = List.for_all (fun x -> List.mem x b) a in
                  let ancestors_of h =
                    List.filter (fun a ->
                      a <> h
                      && subset (body_of h) (body_of a)
                      && List.length (body_of a) > List.length (body_of h)) headers in
                  let ancestor_targets h =
                    List.concat (List.map (fun a -> a :: loop_exit_of a) (ancestors_of h)) in
                  let primary_exits h =
                    List.filter (fun e -> not (List.mem e (ancestor_targets h)))
                      (loop_exit_of h) in
                  let deeper_blocks h =
                    List.concat (List.map (fun h2 ->
                      if h2 <> h && subset (body_of h2) (body_of h)
                         && List.length (body_of h2) < List.length (body_of h)
                      then body_of h2 else []) headers) in
                  (* a header needs a Go label iff a break/continue from a *nested*
                     loop targets it (its header, for continue; its primary exit,
                     for break) *)
                  let label_of h = "L" ^ string_of_int h in
                  let needs_label h =
                    let pe = primary_exits h in
                    let deeper = deeper_blocks h in
                    List.exists (fun n ->
                      List.mem n deeper &&
                      (List.mem h (targets_of n)
                       || List.exists (fun e -> List.mem e (targets_of n)) pe))
                      alln in
                  (* Reducibility: removing back-edges (target dominates source)
                     must leave a DAG.  An irreducible CFG — a cycle with no
                     dominating back-edge, so no detected loop header — would make
                     [emit_region] recurse forever (nothing stops the cycle), so
                     it must take the raw-goto fallback. *)
                  let forward_succ n =
                    List.filter (fun t -> t >= 0 && t < nblk && not (dominates t n))
                      (targets_of n) in
                  let reducible =
                    let color = Array.make nblk 0 in
                    let ok = ref true in
                    let rec dfs n =
                      color.(n) <- 1;
                      List.iter (fun t ->
                        if color.(t) = 1 then ok := false
                        else if color.(t) = 0 then dfs t) (forward_succ n);
                      color.(n) <- 2 in
                    for n = 0 to nblk - 1 do if color.(n) = 0 then dfs n done;
                    !ok in
                  (* structurable: single entry at 0, reducible, ≤1 primary exit
                     per loop (extra exits are labeled escapes to enclosing loops),
                     loops properly nested (bodies disjoint or one inside other) *)
                  let structurable =
                    start_v = 0 && reducible &&
                    List.for_all (fun h -> List.length (primary_exits h) <= 1) headers &&
                    List.for_all (fun h1 ->
                      List.for_all (fun h2 ->
                        h1 = h2 ||
                        (let b1 = body_of h1 and b2 = body_of h2 in
                         inter b1 b2 = [] || subset b1 b2 || subset b2 b1))
                        headers) headers in
                  (* merge of a conditional = the *immediate* (closest) block post-
                     dominating both branches' leaf successors (exit for returning
                     leaves).  The common post-dominators form a chain ordered by
                     post-domination; the immediate one is the [m] that every other
                     common post-dominator also post-dominates.  (Min-index is wrong
                     under cycles: a back-edge can make a low-index header appear in
                     the set even though the real merge is a higher-index block.) *)
                  let merge_of tb eb =
                    let leaves bb =
                      let ts = List.sort_uniq compare (block_targets bb []) in
                      if block_has_done bb then exit_node :: ts else ts in
                    match List.sort_uniq compare (leaves tb @ leaves eb) with
                    | [] -> exit_node
                    | s0 :: rest ->
                        let common =
                          List.fold_left (fun acc x -> inter acc pdom.(x)) pdom.(s0) rest in
                        (match List.filter
                                 (fun m -> List.for_all
                                             (fun x -> x = m || List.mem x pdom.(m)) common)
                                 common with
                         | m :: _ -> m
                         | [] -> exit_node) in
                  (* a branch that is a bare unconditional jump to j (no statements) *)
                  let bare_jump bb =
                    match collect_app bb [] with
                    | MLglob r, args when is_ret_ref r ->
                        (match List.filter (fun a -> not (is_erased a)) args with
                         | [MLcons (_, c, [nn])] when is_jump_ctor c -> nat_value nn
                         | _ -> None)
                    | _ -> None in
                  (* Each block is a separate term in the *call-site* de Bruijn
                     context [env]; its free variables (e.g. the enclosing [Ref])
                     are relative to that, not to whatever block we jumped from.
                     So [emit_region] always (re)starts a block with [env]; only
                     [walk] extends the environment, and only for that block's own
                     binders.
                     [loopctx]: enclosing loops, innermost first, each (header,
                     exit option).  [tail]: reaching the loop-around here is the
                     body's natural end (so a back-edge falls through, else it is
                     an explicit [continue]). *)
                  let rec emit_region entry stop loopctx tail tab =
                    if entry = stop || entry = exit_node then mt ()
                    else if List.mem entry headers then begin
                      let ex = match primary_exits entry with [e] -> Some e | _ -> None in
                      let bctx = (entry, ex) :: loopctx in
                      let lbl_indent =
                        if String.length tab >= 1
                        then String.sub tab 0 (String.length tab - 1) else "" in
                      let lbl =
                        if needs_label entry
                        then str lbl_indent ++ str (label_of entry) ++ str ":" ++ fnl ()
                        else mt () in
                      lbl
                      ++ str tab ++ str "for {" ++ fnl ()
                      ++ walk blocks.(entry) entry bctx true (tab ^ "\t") env
                      ++ str tab ++ str "}" ++ fnl ()
                      ++ (match ex with
                          | Some e -> emit_region e stop loopctx tail tab
                          | None -> mt ())
                    end
                    else walk blocks.(entry) stop loopctx tail tab env
                  and handle_edge j stop loopctx tail tab =
                    (* find which enclosing loop this edge targets (innermost
                       first): the header (continue) or the primary exit (break).
                       Depth 0 is the innermost loop → unlabeled; deeper → labeled. *)
                    let rec scan depth = function
                      | (hd, ex) :: rest ->
                          if j = hd then Some (`Continue, depth, hd)
                          else if ex = Some j then Some (`Break, depth, hd)
                          else scan (depth + 1) rest
                      | [] -> None in
                    match scan 0 loopctx with
                    | Some (`Continue, 0, _) ->
                        if tail then mt () else str tab ++ str "continue" ++ fnl ()
                    | Some (`Continue, _, hd) ->
                        str tab ++ str ("continue " ^ label_of hd) ++ fnl ()
                    | Some (`Break, 0, _) -> str tab ++ str "break" ++ fnl ()
                    | Some (`Break, _, hd) ->
                        str tab ++ str ("break " ^ label_of hd) ++ fnl ()
                    | None ->
                        if j = stop then mt ()
                        else emit_region j stop loopctx tail tab
                  and walk b stop loopctx tail tab benv =
                    let h, args = collect_app b [] in
                    let vis = List.filter (fun a -> not (is_erased a)) args in
                    match h, vis with
                    | MLglob r, [action; k] when is_bind_ref r ->
                        let ids, kbody = collect_lam k in
                        emit_action state hoists tab benv ids action
                        ++ walk kbody stop loopctx tail tab (List.rev ids @ benv)
                    | MLglob r, [next] when is_ret_ref r ->
                        (match next with
                         | MLcons (_, c, [nn]) when is_jump_ctor c ->
                             (match nat_value nn with
                              | Some j -> handle_edge j stop loopctx tail tab
                              | None -> str tab ++ str "return" ++ fnl ())
                         | _ -> str tab ++ str "return" ++ fnl ())
                    | _ ->
                        (match b with
                         | MLcase (_, scrut, branches) ->
                             (match Array.to_list branches with
                              | [ (_, p1, body1); (_, p2, body2) ] ->
                                  let cref p =
                                    (match p with Pusual r | Pcons (r, _) -> Some r | _ -> None) in
                                  (match cref p1, cref p2 with
                                   | Some c1, Some c2
                                     when (is_bool_true c1 && is_bool_false c2)
                                       || (is_bool_false c1 && is_bool_true c2) ->
                                       let tb, eb =
                                         if is_bool_true c1 then body1, body2 else body2, body1 in
                                       let m_raw = merge_of tb eb in
                                       (* a merge that is the loop-around (any
                                          enclosing header) or an enclosing loop's
                                          primary exit is reached by [handle_edge]
                                          as a (labeled) continue/break, so there is
                                          no in-region merge to emit after the if *)
                                       let escapes =
                                         List.exists (fun (hh, ee) -> hh = m_raw || ee = Some m_raw)
                                           loopctx in
                                       let m = if m_raw = stop || escapes then stop else m_raw in
                                       let then_empty = bare_jump tb = Some m in
                                       let else_empty = bare_jump eb = Some m in
                                       let branch_tail = tail && (m = stop || m = exit_node) in
                                       let bw bb = walk bb m loopctx branch_tail (tab ^ "\t") benv in
                                       let cond = pp_expr state benv scrut in
                                       let arms =
                                         if then_empty && else_empty then mt ()
                                         else if then_empty then
                                           str tab ++ str "if !" ++ pp_atom state benv scrut ++ str " {" ++ fnl ()
                                           ++ bw eb ++ str tab ++ str "}" ++ fnl ()
                                         else if else_empty then
                                           str tab ++ str "if " ++ cond ++ str " {" ++ fnl ()
                                           ++ bw tb ++ str tab ++ str "}" ++ fnl ()
                                         else
                                           str tab ++ str "if " ++ cond ++ str " {" ++ fnl ()
                                           ++ bw tb ++ str tab ++ str "} else {" ++ fnl ()
                                           ++ bw eb ++ str tab ++ str "}" ++ fnl () in
                                       arms ++ emit_region m stop loopctx tail tab
                                   | _ -> str tab ++ str "return" ++ fnl ())
                              | _ -> str tab ++ str "return" ++ fnl ())
                         | _ -> str tab ++ pp_expr state benv b ++ fnl ())
                  in
                  if structurable
                  then hoist_doc ++ emit_region start_v exit_node [] true tab
                  else emit_raw ()
                  end
              | None ->
                  str tab ++ str "/* run_blocks: non-literal block list */" ++ fnl ())
         | MLglob r, [m; h] when String.equal (global_basename r) "catch" ->
             let ids, h_body = collect_lam h in
             let new_env = List.rev ids @ env in
             let p_id = match List.filter (fun id -> not (is_dummy id)) ids with
               | [id] -> pp_mlident id
               | _    -> str "_p"
             in
             let t = str tab in
             t ++ str "func() {" ++ fnl () ++
             t ++ str "\tdefer func() {" ++ fnl () ++
             t ++ str "\t\tif " ++ p_id ++ str " := recover(); " ++ p_id ++ str " != nil {" ++ fnl () ++
             pp_stmts (tab ^ "\t\t\t") new_env h_body ++
             t ++ str "\t\t}" ++ fnl () ++
             t ++ str "\t}()" ++ fnl () ++
             pp_stmts (tab ^ "\t") env m ++
             t ++ str "}()" ++ fnl ()
         (* go_spawn body (not inside bind — uncommon but valid) *)
         | MLglob r, [goroutine_body] when is_go_spawn_ref r ->
             str tab ++ str "go func() {" ++ fnl () ++
             pp_stmts (tab ^ "\t") env goroutine_body ++
             str tab ++ str "}()" ++ fnl ()
         (* defer_call f → defer func(){ f }() (function-scoped, like Go's defer) *)
         | MLglob r, [defer_body] when is_defer_call_ref r ->
             str tab ++ str "defer func() {" ++ fnl () ++
             pp_stmts (tab ^ "\t") env defer_body ++
             str tab ++ str "}()" ++ fnl ()
         (* recv_ok tag ch (fun x ok => body) → x, ok := <-ch; body.  An UNUSED
            result slot is rendered "_", detected by de Bruijn FREENESS of the body
            (NOT by [is_dummy]/name): the extractor may leave an unused binder named —
            e.g. it reuses the value binder's name for an anonymous [ok], which would
            emit the uncompilable [x, x := <-ch].  [ok] is the innermost binder
            (de Bruijn 1 in the body), [x] the next (de Bruijn 2). *)
         | MLglob r, [_tag; ch; kont] when is_recv_ok_ref r ->
             let ids, k_body = collect_lam kont in
             let new_env = List.rev ids @ env in
             (match ids with
              | [x_id; ok_id] ->
                  let x_unused  = dbn_free 2 k_body in
                  let ok_unused = dbn_free 1 k_body in
                  let x  = if x_unused  then str "_" else pp_mlident x_id in
                  let ok = if ok_unused then str "_" else pp_mlident ok_id in
                  (if x_unused && ok_unused
                   then str tab ++ str "<-" ++ pp_expr state env ch ++ fnl ()
                   else str tab ++ x ++ str ", " ++ ok ++ str " := <-"
                          ++ pp_expr state env ch ++ fnl ())
                  ++ pp_stmts tab new_env k_body
              | _ ->
                  str tab ++ str "_, _ = <-" ++ pp_expr state env ch ++ fnl ())
         (* select_recv2 ta ch1 k1 tb ch2 k2 → Go select with two recv cases.
            Each continuation [fun x => body] becomes [case x := <-ch: body].
            The non-deterministic CHOICE is Go's at runtime (faithful lowering). *)
         | MLglob r, [_ta; ch1; k1; _tb; ch2; k2] when is_select_recv2_ref r ->
             let recv_case ch kont =
               let ids, body = collect_lam kont in
               let new_env = List.rev ids @ env in
               let x = match List.filter (fun id -> not (is_dummy id)) ids with
                 | [id] -> pp_mlident id | _ -> str "_" in
               str (tab ^ "\t") ++ str "case " ++ x ++ str " := <-"
                 ++ pp_expr state env ch ++ str ":" ++ fnl () ++
               pp_stmts (tab ^ "\t\t") new_env body in
             str tab ++ str "select {" ++ fnl () ++
             recv_case ch1 k1 ++ recv_case ch2 k2 ++
             str tab ++ str "}" ++ fnl ()
         (* select_recv_default ta ch1 k1 d → Go select with one recv + default
            (the non-blocking form: recv-and-k1 if ready, else run d). *)
         | MLglob r, [_ta; ch1; k1; d] when is_select_recv_default_ref r ->
             let ids, body = collect_lam k1 in
             let new_env = List.rev ids @ env in
             let x = match List.filter (fun id -> not (is_dummy id)) ids with
               | [id] -> pp_mlident id | _ -> str "_" in
             str tab ++ str "select {" ++ fnl () ++
             str (tab ^ "\t") ++ str "case " ++ x ++ str " := <-"
               ++ pp_expr state env ch1 ++ str ":" ++ fnl () ++
             pp_stmts (tab ^ "\t\t") new_env body ++
             str (tab ^ "\t") ++ str "default:" ++ fnl () ++
             pp_stmts (tab ^ "\t\t") env d ++
             str tab ++ str "}" ++ fnl ()
         (* slice_at_ok tag xs i (fun v ok => body) → bounds-checked index:
              var v T
              ok := i < int64(len(xs))   (* i is uint63 >= 0; only upper bound *)
              if ok { v = xs[i] }
              body
            Each binder is emitted only when used; v defaults to its zero value
            when out of bounds, so no panic is possible. *)
         | MLglob r, [tag; xs; i; kont] when is_slice_at_ok_ref r ->
             let ids, k_body = collect_lam kont in
             let new_env = List.rev ids @ env in
             (match ids with
              | [v_id; ok_id] ->
                  let go_t = go_type_of_tag tag in
                  let xs_d = pp_expr state env xs in
                  let i_d  = pp_expr state env i in
                  (* [int] is SIGNED (Sint63), so a negative index is possible and
                     Go panics on it — the bounds check must cover BOTH ends, else
                     a negative [i] passes [i < len] and [xs[i]] panics, breaking
                     the "cannot panic out of bounds" guarantee. *)
                  let cond = i_d ++ str " >= 0 && " ++ i_d
                             ++ str " < int64(len(" ++ xs_d ++ str "))" in
                  let v_decl =
                    if is_dummy v_id then mt ()
                    else str tab ++ str "var " ++ pp_mlident v_id ++ str " "
                         ++ str go_t ++ fnl () in
                  let ok_bind =
                    if is_dummy ok_id then mt ()
                    else str tab ++ pp_mlident ok_id ++ str " := " ++ cond ++ fnl () in
                  let guard = if is_dummy ok_id then cond else pp_mlident ok_id in
                  let if_block =
                    if is_dummy v_id then mt ()
                    else
                      str tab ++ str "if " ++ guard ++ str " {" ++ fnl () ++
                      str (tab ^ "\t") ++ pp_mlident v_id ++ str " = "
                        ++ xs_d ++ str "[" ++ i_d ++ str "]" ++ fnl () ++
                      str tab ++ str "}" ++ fnl () in
                  v_decl ++ ok_bind ++ if_block ++ pp_stmts tab new_env k_body
              | _ ->
                  pp_stmts tab new_env k_body)
         (* ptr_get_ok tag p (fun v ok => body) → nil-checked deref (Phase B1b):
              var v T
              ok := p != nil
              if ok { v = *p }
              body
            Mirrors slice_at_ok; [v] defaults to the zero value when [p] is nil, and
            the deref [*p] runs only in the [ok] branch — so the nil-deref panic is
            unreachable (the ok=false case is forced on the caller). *)
         | MLglob r, [tag; p; kont] when is_ptr_get_ok_ref r ->
             let ids, k_body = collect_lam kont in
             let new_env = List.rev ids @ env in
             (match ids with
              | [v_id; ok_id] ->
                  let go_t = go_type_of_tag (strip_magic tag) in
                  let p_d = pp_atom state env p in
                  let cond = p_d ++ str " != nil" in
                  let v_decl =
                    if is_dummy v_id then mt ()
                    else str tab ++ str "var " ++ pp_mlident v_id ++ str " "
                         ++ str go_t ++ fnl () in
                  let ok_bind =
                    if is_dummy ok_id then mt ()
                    else str tab ++ pp_mlident ok_id ++ str " := " ++ cond ++ fnl () in
                  let guard = if is_dummy ok_id then cond else pp_mlident ok_id in
                  let if_block =
                    if is_dummy v_id then mt ()
                    else
                      str tab ++ str "if " ++ guard ++ str " {" ++ fnl () ++
                      str (tab ^ "\t") ++ pp_mlident v_id ++ str " = *" ++ p_d ++ fnl () ++
                      str tab ++ str "}" ++ fnl () in
                  v_decl ++ ok_bind ++ if_block ++ pp_stmts tab new_env k_body
              | _ ->
                  pp_stmts tab new_env k_body)
         (* str_at_ok s i (fun b ok => body) → bounds-checked byte index:
              var b int64
              ok := i >= 0 && i < int64(len(s))
              if ok { b = int64(s[i]) }   (* s[i] is byte; widen to the int64 carrier *)
              body
            Mirrors slice_at_ok; the byte is a GoByte (= uint8, GoU8) erased to
            int64.  Cannot panic out of range (the ok-branch is forced). *)
         | MLglob r, [s; i; kont] when is_str_at_ok_ref r ->
             let ids, k_body = collect_lam kont in
             let new_env = List.rev ids @ env in
             (match ids with
              | [b_id; ok_id] ->
                  let s_d = pp_expr state env s in
                  let i_d = pp_expr state env i in
                  let cond = i_d ++ str " >= 0 && " ++ i_d
                             ++ str " < int64(len(" ++ s_d ++ str "))" in
                  let b_decl =
                    if is_dummy b_id then mt ()
                    else str tab ++ str "var " ++ pp_mlident b_id ++ str " int64" ++ fnl () in
                  let ok_bind =
                    if is_dummy ok_id then mt ()
                    else str tab ++ pp_mlident ok_id ++ str " := " ++ cond ++ fnl () in
                  let guard = if is_dummy ok_id then cond else pp_mlident ok_id in
                  let if_block =
                    if is_dummy b_id then mt ()
                    else
                      str tab ++ str "if " ++ guard ++ str " {" ++ fnl () ++
                      str (tab ^ "\t") ++ pp_mlident b_id ++ str " = int64("
                        ++ s_d ++ str "[" ++ i_d ++ str "])" ++ fnl () ++
                      str tab ++ str "}" ++ fnl () in
                  b_decl ++ ok_bind ++ if_block ++ pp_stmts tab new_env k_body
              | _ ->
                  pp_stmts tab new_env k_body)
         (* type_assert_safe tag x (fun v ok => body) → v, ok := x.(T); body
            Go's native two-value assertion: ok=false (no panic) on mismatch. *)
         | MLglob r, [tag; x; kont] when is_type_assert_safe_ref r ->
             let go_t = go_type_of_tag tag in
             let inner = pp_expr state env (any_payload x) in
             let ids, k_body = collect_lam kont in
             let new_env = List.rev ids @ env in
             (match ids with
              | [v_id; ok_id] when not (is_dummy v_id && is_dummy ok_id) ->
                  str tab ++ pp_mlident v_id ++ str ", " ++ pp_mlident ok_id ++
                  str " := " ++ inner ++ str ".(" ++ str go_t ++ str ")" ++ fnl () ++
                  pp_stmts tab new_env k_body
              | _ ->
                  pp_stmts tab new_env k_body)
         (* type_switch2 a t1 k1 t2 k2 d → Go's native type switch:
              switch _tsv := <a>.(type) {
              case T1: v1 := _tsv; <k1 body>
              case T2: v2 := _tsv; <k2 body>
              default: <d> }
            CPS like type_assert_safe but multi-case.  Each kᵢ binds the case-typed
            value, rebound from the shared guard [_tsv] (so distinct binder names need
            no env renaming).  The guard var is omitted entirely if no case uses its
            value (else Go errors "_tsv declared but not used"). *)
         | MLglob r, [a; t1; k1; t2; k2; d] when is_type_switch2_ref r ->
             let payload = pp_expr state env (any_payload a) in
             let case_used kont =
               match collect_lam kont with ([v], _) -> not (is_dummy v) | _ -> false in
             let any_used = case_used k1 || case_used k2 in
             let emit_case tagd kont =
               let go_t = go_type_of_tag tagd in
               let ids, kbody = collect_lam kont in
               (match ids with
                | [v_id] ->
                    str tab ++ str "case " ++ str go_t ++ str ":" ++ fnl () ++
                    (if is_dummy v_id then mt ()
                     else str (tab ^ "\t") ++ pp_mlident v_id ++ str " := _tsv" ++ fnl ()) ++
                    pp_stmts (tab ^ "\t") (v_id :: env) kbody
                | _ -> unsupported "type_switch2 case (expected exactly one value binder)")
             in
             str tab ++ str "switch " ++
             (if any_used then str "_tsv := " else mt ()) ++
             payload ++ str ".(type) {" ++ fnl () ++
             emit_case t1 k1 ++
             emit_case t2 k2 ++
             str tab ++ str "default:" ++ fnl () ++
             pp_stmts (tab ^ "\t") env d ++
             str tab ++ str "}" ++ fnl ()
         (* run_session client server → _sess_ch := make(chan any);
                                        go func(){ <server> }(); <client>
            The indexed-monad roles share one [chan any]; each role's body is
            lowered by [pp_sess_stmts].  ([Proto] index is present, so the two
            roles are the last two visible args.) *)
         | MLglob r, vis when is_run_session_ref r && List.length vis >= 2 ->
             let n = List.length vis in
             let client = List.nth vis (n - 2) in
             let server = List.nth vis (n - 1) in
             str tab ++ str "_sess_ch := make(chan any)" ++ fnl () ++
             str tab ++ str "go func() {" ++ fnl () ++
             pp_sess_stmts (tab ^ "\t") env server ++
             str tab ++ str "}()" ++ fnl () ++
             pp_sess_stmts tab env client
         | _ ->
             str tab ++ pp_expr state env e ++ fnl ())
    | MLletin (id, e1, e2) ->
        let new_env = id :: env in
        (* map_get_or k default m → two-statement pattern: no IIFE *)
        let head1, all_args1 = collect_app e1 [] in
        let vis1 = List.filter (fun a -> not (is_erased a)) all_args1 in
        (match head1, vis1 with
         | MLglob r, [_kt; _vt; k; def; m] when is_map_get_or_ref r && not (is_dummy id) ->
             let lhs = pp_mlident id in
             str tab ++ str "var " ++ lhs ++ str " any = "
               ++ pp_typed_lit state env def ++ fnl () ++
             str tab ++ str "if _v, _ok := " ++ pp_expr state env m ++
             str "[" ++ pp_expr state env k ++ str "]; _ok {" ++ fnl () ++
             str tab ++ str "\t" ++ lhs ++ str " = _v" ++ fnl () ++
             str tab ++ str "}" ++ fnl () ++
             pp_stmts tab new_env e2
         (* let acc := slice_fold xs init step in body →
              acc := init
              for _, x := range xs { acc = step acc x }
              body
            The accumulator reuses the let-bound Go variable [id]; [step]'s two
            binders map to (acc=id, x=loop var). *)
         | MLglob r, [xs; init; step] when is_slice_fold_ref r && not (is_dummy id) ->
             let step_ids, step_body = collect_lam step in
             (match step_ids with
              | [_acc; x_id] ->
                  let acc = pp_mlident id in
                  (* step_body env: MLrel 1 = x, MLrel 2 = acc (= id); any captured
                     outer vars sit past both binders in [env]. *)
                  let loop_env = x_id :: id :: env in
                  str tab ++ acc ++ str " := " ++ pp_typed_lit state env init ++ fnl () ++
                  str tab ++ str "for _, " ++ pp_mlident x_id ++ str " := range "
                    ++ pp_expr state env xs ++ str " {" ++ fnl () ++
                  str (tab ^ "\t") ++ acc ++ str " = "
                    ++ pp_expr state loop_env step_body ++ fnl () ++
                  str tab ++ str "}" ++ fnl () ++
                  pp_stmts tab new_env e2
              | _ ->
                  str tab ++ pp_mlident id ++ str " := " ++ pp_expr state env e1
                    ++ fnl () ++ pp_stmts tab new_env e2)
         | _ ->
             let lhs = if is_dummy id || is_void_call e1 then mt ()
                       else pp_mlident id ++ str " := " in
             str tab ++ lhs ++ pp_expr state env e1 ++ fnl () ++
             pp_stmts tab new_env e2)
    (* Case analysis in statement position (tail of an IO body). *)
    | MLcase (typ, scrut, branches) ->
        emit_case tab env typ scrut branches (fun _nb b -> b)
    | MLcons (_, r, []) when is_unit_tt r -> mt ()
    (* Bare nullary IO function in tail position — needs () to be a call,
       not a function value. *)
    | MLglob r as e
      when not (is_bool_true r || is_bool_false r
                || is_unit_tt r || is_map_make_ref r) ->
        str tab ++ pp_expr state env e ++ str "()" ++ fnl ()
    | e ->
        str tab ++ pp_expr state env e ++ fnl ()

  (* Emit an [MLcase] as Go statements.  [mk_body] transforms each arm's body
     before it is printed: the identity in tail position, or "wrap in [bind …
     k]" when a continuation must be threaded into every arm.  Only [bool]
     matches (i.e. [if]/[else]) are lowered for now; richer inductives and
     type switches follow. *)
  and emit_case tab env typ scrut branches mk_body =
    (* Branch binders are stored in constructor order [arg0; …; argN]; the body
       env is [List.rev ids @ env] (innermost binder = last arg = MLrel 1),
       matching the lambda convention used elsewhere. *)
    let ctor_of (ids, pat, body) =
      match pat with
      | Pusual r | Pcons (r, _)   -> (Some r, ids, body)
      | Pwild | Prel _ | Ptuple _ -> (None, ids, body)
    in
    let unhandled () =
      unsupported "a match of this shape in statement position (only if/bool, option comma-ok, and list nil/cons are modeled)"
    in
    (* Emit a two-arm if/else; [pre] (e.g. an "x, ok :=" init clause) precedes
       the condition, and [tenv]/[fenv] are the branch environments. *)
    let if_else pre cond (tbody, tn, tenv) (fbody, fn, fenv) =
      str tab ++ str "if " ++ pre ++ cond ++ str " {" ++ fnl () ++
      pp_stmts (tab ^ "\t") tenv (mk_body tn tbody) ++
      str tab ++ str "} else {" ++ fnl () ++
      pp_stmts (tab ^ "\t") fenv (mk_body fn fbody) ++
      str tab ++ str "}" ++ fnl ()
    in
    (* [GoString] is a BYTE sequence ([string]), DISTINCT from a [list GoRune]
       ([]int32); the byte-wise slice lowering (xs[0], xs[1:]) is wrong for a rune
       slice, so a string match is excluded from the list case. *)
    let is_string_list =
      match typ with
      | Tglob (r, [Tglob (elem, [])]) ->
          is_list_type r && String.equal (global_basename elem) "GoInt32"
      | _ -> false
    in
    match Array.to_list branches with
    | [ b1; b2 ] ->
        let (r1, ids1, body1) = ctor_of b1 in
        let (r2, ids2, body2) = ctor_of b2 in
        (match r1, r2 with
         (* if/else: match on bool *)
         | Some c1, Some c2
           when (is_bool_true c1 && is_bool_false c2)
             || (is_bool_false c1 && is_bool_true c2) ->
             let then_b, else_b =
               if is_bool_true c1 then body1, body2 else body2, body1 in
             if_else (mt ()) (pp_expr state env scrut)
               (then_b, 0, env) (else_b, 0, env)
         (* option: match on [map_get_opt k m] → comma-ok lookup.  The Some arm
            binds the value; the None arm is the absent case.  Only the
            map-lookup producer is fused; a free [option] value is not yet
            representable. *)
         | Some c1, Some c2
           when (is_some_ctor c1 && is_none_ctor c2)
             || (is_none_ctor c1 && is_some_ctor c2) ->
             let some_ids, some_body, none_body =
               if is_some_ctor c1 then ids1, body1, body2
               else ids2, body2, body1 in
             let sh, sargs = collect_app (strip_magic scrut) [] in
             let svis = List.filter (fun a -> not (is_erased a)) sargs in
             (match sh, svis with
              | MLglob r, [_kt; _vt; k; m] when is_map_get_opt_ref r ->
                  let v_doc = match some_ids with
                    | [id] when not (is_dummy id) -> pp_mlident id
                    | _ -> str "_" in
                  let pre =
                    v_doc ++ str ", _ok := " ++ pp_expr state env m ++
                    str "[" ++ pp_expr state env k ++ str "]; " in
                  if_else pre (str "_ok")
                    (some_body, List.length some_ids, List.rev some_ids @ env)
                    (none_body, 0, env)
              | _ -> unhandled ())
         (* list / slice: [match xs with [] | x :: rest] → len / index / reslice.
            Slices only — a [GoString] (byte sequence) match is excluded above. *)
         | Some c1, Some c2
           when ((is_list_nil c1 && is_list_cons c2)
              || (is_list_cons c1 && is_list_nil c2))
             && not is_string_list ->
             let nil_body, cons_ids, cons_body =
               if is_list_nil c1 then body1, ids2, body2
               else body2, ids1, body1 in
             let head_id, tail_id = match cons_ids with
               | [h; t] -> h, t
               | _      -> Dummy, Dummy in
             let xs = pp_expr state env scrut in
             let prefix =
               (if is_dummy head_id then mt ()
                else str (tab ^ "\t") ++ pp_mlident head_id ++ str " := "
                     ++ xs ++ str "[0]" ++ fnl ()) ++
               (if is_dummy tail_id then mt ()
                else str (tab ^ "\t") ++ pp_mlident tail_id ++ str " := "
                     ++ xs ++ str "[1:]" ++ fnl ())
             in
             str tab ++ str "if len(" ++ xs ++ str ") == 0 {" ++ fnl () ++
             pp_stmts (tab ^ "\t") env (mk_body 0 nil_body) ++
             str tab ++ str "} else {" ++ fnl () ++
             prefix ++
             pp_stmts (tab ^ "\t") (List.rev cons_ids @ env)
               (mk_body (List.length cons_ids) cons_body) ++
             str tab ++ str "}" ++ fnl ()
         | _ -> unhandled ())
    | _ -> unhandled ()

  (* for_each xs (fun x => body) → for _, x := range xs { body } *)
  and emit_for_each tab env xs bodyfn =
    let ids, body = collect_lam bodyfn in
    let new_env = List.rev ids @ env in
    let hdr =
      match List.filter (fun id -> not (is_dummy id)) ids with
      | [x] -> str "for _, " ++ pp_mlident x ++ str " := range "
      | _   -> str "for range " in
    str tab ++ hdr ++ pp_expr state env xs ++ str " {" ++ fnl () ++
    pp_stmts (tab ^ "\t") new_env body ++
    str tab ++ str "}" ++ fnl ()
  (* ---- Indexed-monad (linear) sessions ----
     A [Sess i j A] fragment lowers to sequential Go against the implicit shared
     channel [_sess_ch] (declared by [run_session]).  [sbind] is sequencing;
     [ssend v] → [_sess_ch <- v]; [srecv tag] → typed receive; [slift io] → the
     IO body; [sret] erases.  The protocol [Proto] index is computationally
     present (it is a [Type]-sorted inductive, not erased), so the real payload
     is the LAST visible argument. *)
  and emit_sess_action tab env ids action =
    let h, args = collect_app action [] in
    let vis = List.filter (fun a -> not (is_erased a)) args in
    let last () = List.nth vis (List.length vis - 1) in
    match h with
    | MLglob r when is_ssend_ref r ->
        str tab ++ str "_sess_ch <- " ++ pp_typed_lit state env (last ()) ++ fnl ()
    | MLglob r when is_srecv_ref r ->
        let go_t = go_type_of_tag (last ()) in
        (match List.filter (fun id -> not (is_dummy id)) ids with
         (* receive then type-assert in one expression — no temp, so consecutive
            receives in one scope don't collide on a [:=] re-declaration *)
         | [x] ->
             str tab ++ pp_mlident x ++ str (" := (<-_sess_ch).(" ^ go_t ^ ")") ++ fnl ()
         | _ -> str tab ++ str "<-_sess_ch" ++ fnl ())
    | MLglob r when is_slift_ref r -> pp_stmts tab env (last ())
    | MLglob r when is_sret_ref r ->
        (match List.filter (fun id -> not (is_dummy id)) ids with
         | [b] -> str tab ++ pp_mlident b ++ str " := " ++ pp_expr state env (last ()) ++ fnl ()
         | _   -> mt ())
    | _ -> pp_sess_stmts tab env action
  and pp_sess_stmts tab env e =
    let h, args = collect_app e [] in
    let vis = List.filter (fun a -> not (is_erased a)) args in
    match h with
    | MLglob r when is_sbind_ref r && List.length vis >= 2 ->
        let n = List.length vis in
        let m = List.nth vis (n - 2) and k = List.nth vis (n - 1) in
        let ids, body = collect_lam k in
        let new_env = List.rev ids @ env in
        emit_sess_action tab env ids m ++ pp_sess_stmts tab new_env body
    | MLglob r when is_ssend_ref r || is_srecv_ref r || is_sret_ref r ->
        emit_sess_action tab env [] e
    | MLglob r when is_slift_ref r ->
        pp_stmts tab env (List.nth vis (List.length vis - 1))
    | _ -> str tab ++ pp_expr state env e ++ fnl ()
  in
  let rec peel_catches tab env b =
    let head, all_args = collect_app b [] in
    let vis = List.filter (fun a -> not (is_erased a)) all_args in
    match head, vis with
    | MLglob r, [m; h] when String.equal (global_basename r) "catch" ->
        let ids, h_body = collect_lam h in
        let new_env = List.rev ids @ env in
        let p_id = match List.filter (fun id -> not (is_dummy id)) ids with
          | [id] -> pp_mlident id
          | _    -> str "_p"
        in
        let t = str tab in
        t ++ str "defer func() {" ++ fnl () ++
        t ++ str "\tif " ++ p_id ++ str " := recover(); " ++ p_id ++ str " != nil {" ++ fnl () ++
        pp_stmts (tab ^ "\t\t") new_env h_body ++
        t ++ str "\t}" ++ fnl () ++
        t ++ str "}()" ++ fnl () ++
        peel_catches tab env m
    | _ -> pp_stmts tab env b
  in
  peel_catches tab env body

