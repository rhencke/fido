(* A record (struct) type in value position — the receiver test for methods. *)
let is_record_tglob = function
  | Tglob (r, _) -> is_record_typename (global_basename r)
  | _ -> false

(* A POINTER receiver (Phase B2): first param is [SPtr R] with R a record.  Lowers the
   method to [func (recv *R) M(…)] — [pp_type (SPtr R)] is already [*R] — and a call
   [m p …] to [p.M(…)], exactly the value-receiver path but THROUGH a pointer, so the
   method can MUTATE its receiver (observed by the caller). *)
let is_sptr_record_tglob = function
  | Tglob (r, [arg]) when is_sptr_type r -> is_record_tglob arg
  | _ -> false

(* Pure (non-IO) function body that RETURNS via a tail-position match.  Go has no
   conditional expression, so a [match]/[if] in value position can't be inlined as
   one; in TAIL position (the whole function body, possibly nested) it lowers to an
   [if]/[else] where each arm RETURNS its value (ladder 7b — expressions, tail
   case).  Only a 2-arm BOOL match (i.e. [if]/[else]) is modeled; the arms recurse
   so nested ifs chain, and a leaf arm emits [return <expr>].  A non-bool / non-tail
   value-position match still fails loud via [pp_expr]'s catch-all (the [return]
   fallback below routes it there).  This is what lets a pure function whose body is
   an [if] (e.g. [i64_abs]) extract instead of aborting. *)
let rec pp_pure_tail state tab env e =
  let return_fallback () = str tab ++ str "return " ++ pp_expr state env e ++ fnl () in
  match strip_magic e with
  | MLcase (_typ, scrut, branches) when Array.length branches = 2 ->
      let ctor_of (ids, pat, body) =
        match pat with
        | Pusual r | Pcons (r, _)   -> (Some r, ids, body)
        | Pwild | Prel _ | Ptuple _ -> (None, ids, body)
      in
      let (r1, _, body1) = ctor_of branches.(0) in
      let (r2, _, body2) = ctor_of branches.(1) in
      (match r1, r2 with
       | Some c1, Some c2
         when (is_bool_true c1 && is_bool_false c2)
           || (is_bool_false c1 && is_bool_true c2) ->
           let then_b, else_b =
             if is_bool_true c1 then body1, body2 else body2, body1 in
           str tab ++ str "if " ++ pp_expr state env scrut ++ str " {" ++ fnl () ++
           pp_pure_tail state (tab ^ "\t") env then_b ++
           str tab ++ str "} else {" ++ fnl () ++
           pp_pure_tail state (tab ^ "\t") env else_b ++
           str tab ++ str "}" ++ fnl ()
       | _ -> return_fallback ())
  | _ -> return_fallback ()

(** Emit a top-level function, collecting leading lambdas for the signature
    and using [typ] for parameter/return type annotations.

    A function whose first visible parameter is a record (struct) is emitted as a
    Go value-receiver METHOD [func (recv T) M(rest…)] — faithful (a method call
    [recv.M(a)] denotes the same as [M(recv, a)]) and idiomatic.  Detection here
    mirrors [collect_decls]'s, so declaration and call sites agree. *)
let pp_function state name body typ =
  let ids, inner_body = collect_lam body in
  let param_types, ret_type = collect_tarrs typ in
  (* Pair visible ids with their corresponding types, skipping erased args. *)
  let rec zip_params ids types =
    match ids, types with
    | [], _              -> []
    | id :: rest, _ when is_dummy id ->
        let rest_types = (match types with _ :: t -> t | [] -> []) in
        zip_params rest rest_types
    | id :: rest, t :: rest_types -> (id, t) :: zip_params rest rest_types
    | id :: rest, []              -> (id, Tunknown) :: zip_params rest []
  in
  let param_pairs = zip_params ids param_types in
  (* env includes all ids (with dummies) for de Bruijn; innermost first. *)
  let full_env = List.rev ids in
  let pp_param (id, t) = pp_mlident id ++ str " " ++ pp_type state t in
  let fn_sig =
    match param_pairs with
    | (rid, rt) :: rest when is_record_tglob rt || is_sptr_record_tglob rt ->  (* value- or pointer-receiver method *)
        str "func (" ++ pp_param (rid, rt) ++ str ") " ++
        str (go_export name) ++ str "(" ++
        prlist_with_sep (fun () -> str ", ") pp_param rest
    | _ ->
        str "func " ++ str (go_export name) ++ str "(" ++
        prlist_with_sep (fun () -> str ", ") pp_param param_pairs
  in
  (* IO-returning functions use pp_io_body; pure functions use return+expr *)
  match fullwidth_conv_name name with
  | Some conv ->
      (* Full-width int64<->uint64 reinterpret: emit the canonical one-liner
         [func Name(a int64) uint64 { return uint64(a) }].  The body's Z-arith
         (wrapU64/i64raw) is proof-only and would not lower; the conversion is the
         Go type-cast on the parameter variable. *)
      let arg = (match param_pairs with (id, _) :: _ -> pp_mlident id | [] -> str "_") in
      fn_sig ++ str ") " ++ pp_type state ret_type ++ str " {" ++ fnl () ++
      str "\treturn " ++ str conv ++ str "(" ++ arg ++ str ")" ++ fnl () ++
      str "}" ++ fnl ()
  | None ->
  match ret_type with
  | Tglob (r, [inner]) when is_IO_type r ->
      let go_ret = match inner with
        | Tglob (r2, []) when is_unit_type r2 -> mt ()   (* IO unit → void *)
        | t -> str " " ++ pp_type state t
      in
      fn_sig ++ str ")" ++ go_ret ++ str " {" ++ fnl () ++
      pp_io_body state "\t" full_env inner_body ++
      str "}" ++ fnl ()
  | _ ->
      fn_sig ++ str ") " ++ pp_type state ret_type ++ str " {" ++ fnl () ++
      pp_pure_tail state "\t" full_env inner_body ++
      str "}" ++ fnl ()

