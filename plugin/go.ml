(* A record (struct) type in value position — the receiver test for methods. *)
let is_record_tglob = function
  | Tglob (r, _) -> is_record_typename (global_basename r)
  | _ -> false

(* A POINTER receiver (Phase B2): first param is [SPtr R] with R a record.  Lowers the
   method to [func (recv *R) M(…)] — [pp_type (SPtr R)] is already [*R] — and a call
   [m p …] to [p.M(…)], exactly the value-receiver path but THROUGH a pointer, so the
   method can MUTATE its receiver (observed by the caller). *)
let is_sptr_record_tglob = function
  | Tglob (r, arg :: _) when is_sptr_type r -> is_record_tglob arg  (* receiver record = first type arg *)
  | _ -> false

(* Pure (non-IO) function body that RETURNS via a tail-position match.  Go has no
   conditional expression, so a [match]/[if] in value position can't be inlined as
   one; in TAIL position (the whole function body, possibly nested) it lowers to an
   [if]/[else] where each arm RETURNS its value (ladder 7b — expressions, tail
   case).  Only a 2-arm BOOL match (i.e. [if]/[else]) is modeled; the arms recurse
   so nested ifs chain, and a leaf arm emits [return <expr>].  A non-bool / non-tail
   value-position match still fails loud via [pp_expr]'s catch-all (the [return]
   fallback below routes it there).  This is what lets a pure function whose body is
   an [if] (e.g. [i64_abs]) extract instead of aborting. *)
let rec pp_pure_tail state tab env e =
  let return_fallback () =
    (* P0 #2: a sub-64 narrow return wraps the int-carrier value in its declared Go type. *)
    match !narrow_ret_type with
    | Some gt -> str tab ++ str "return " ++ str (gt ^ "(") ++ pp_expr state env e ++ str ")" ++ fnl ()
    | None    -> str tab ++ str "return " ++ pp_expr state env e ++ fnl () in
  let ctor_of0 (ids, pat, body) =
    match pat with
    | Pusual r | Pcons (r, _)   -> (Some r, ids, body)
    | Pwild | Prel _ | Ptuple _ -> (None, ids, body) in
  match strip_magic e with
  (* VALUE-position multiple-return DESTRUCTURE [let '(x,y[,z…]) := f … in body] (single pair branch,
     2 binders, N-ary via flatten_destructure) → [x, y[, z…] := f(…)] then the body in tail position.
     Mirrors the statement-position destructure; without this, a destructure in a PURE (value-returning)
     function — [func f() int { x, y := g(); return x + y }] — hit the value-position fallthrough. *)
  | MLcase (_typ, scrut, [| (ids, pat, body1) |])
    when (match pat with Pcons (r, _) | Pusual r -> is_pair_ref r | _ -> false)
         && List.length ids = 2 ->
      let flat_ids, fbody, fenv = flatten_destructure ids body1 env in
      str tab ++ prlist_with_sep (fun () -> str ", ") (pp_destr_binder fenv fbody) flat_ids
      ++ str " := " ++ pp_expr state env scrut ++ fnl ()
      ++ pp_pure_tail state tab fenv fbody
  (* VALUE-position ENUM match → a Go [switch] each of whose arms [return]s (e.g. a
     [func (d Direction) String() string]).  Mirrors the statement-position enum switch. *)
  | MLcase (_typ, scrut, branches)
    when Array.length branches >= 2
      && Array.for_all (fun b -> match ctor_of0 b with
           | (Some c, _, _) -> is_enum_ctor c | _ -> false) branches ->
      (* Go requires a value-returning function to return on every path, and does NOT treat
         a [default]-less switch as exhaustive — so the LAST arm (the last constructor, in
         constructor order) is emitted as [default:].  Faithful: the match is total and the
         other constructors are matched above, so [default] catches exactly the last one. *)
      let rec emit = function
        | [] -> mt ()
        | [b] ->
            let (_, _, body) = ctor_of0 b in
            str tab ++ str "default:" ++ fnl () ++
            pp_pure_tail state (tab ^ "\t") env body
        | b :: rest ->
            let (rc, _ids, body) = ctor_of0 b in
            let cname = match rc with Some c -> go_export (global_basename c) | None -> "_" in
            str tab ++ str "case " ++ str cname ++ str ":" ++ fnl () ++
            pp_pure_tail state (tab ^ "\t") env body ++ emit rest in
      str tab ++ str "switch " ++ pp_expr state env scrut ++ str " {" ++ fnl () ++
      emit (Array.to_list branches) ++
      str tab ++ str "}" ++ fnl ()
  | MLcase (_typ, scrut, branches) when Array.length branches = 2 ->
      let ctor_of (ids, pat, body) =
        match pat with
        | Pusual r | Pcons (r, _)   -> (Some r, ids, body)
        | Pwild | Prel _ | Ptuple _ -> (None, ids, body)
      in
      let (r1, ids1, body1) = ctor_of branches.(0) in
      let (r2, ids2, body2) = ctor_of branches.(1) in
      (match r1, r2 with
       | Some c1, Some c2
         when (is_bool_true c1 && is_bool_false c2)
           || (is_bool_false c1 && is_bool_true c2) ->
           let then_b, else_b =
             if is_bool_true c1 then body1, body2 else body2, body1 in
           str tab ++ str "if " ++ pp_expr state env scrut ++ str " {" ++ fnl () ++
           pp_pure_tail state (tab ^ "\t") env then_b ++
           str tab ++ str "} else {" ++ fnl () ++
           pp_pure_tail state (tab ^ "\t") env else_b ++
           str tab ++ str "}" ++ fnl ()
       (* nat in VALUE/tail position → [if n == 0 { return a } else { k := n - 1; return b }],
          enabling PURE (value-returning) structural recursion (e.g. [Fixpoint pow2]).  Mirrors
          the statement-position nat case; [k := n - 1] is reachable only when n != 0. *)
       | Some c1, Some c2
         when (is_nat_zero c1 && is_nat_succ c2)
           || (is_nat_succ c1 && is_nat_zero c2) ->
           let zero_body, succ_ids, succ_body =
             if is_nat_zero c1 then body1, ids2, body2 else body2, ids1, body1 in
           let pred_id = match succ_ids with [k] -> k | _ -> Dummy in
           let n = pp_expr state env scrut in
           let prefix =
             if is_dummy pred_id then mt ()
             else str (tab ^ "\t") ++ pp_mlident pred_id ++ str " := " ++ n ++ str " - 1" ++ fnl () in
           str tab ++ str "if " ++ n ++ str " == 0 {" ++ fnl () ++
           pp_pure_tail state (tab ^ "\t") env zero_body ++
           str tab ++ str "} else {" ++ fnl () ++
           prefix ++
           pp_pure_tail state (tab ^ "\t") (List.rev succ_ids @ env) succ_body ++
           str tab ++ str "}" ++ fnl ()
       | _ -> return_fallback ())
  (* multiple return values: a pair body → [return a, b] (the two components; the pair
     constructor carries 2 args type-erased, or 4 with the type params present). *)
  | MLcons (_, r, args) when is_pair_ref r && (List.length args = 2 || List.length args = 4) ->
      let a, b = (match args with [a; b] -> a, b | [_; _; a; b] -> a, b | _ -> assert false) in
      let vals = flatten_pair_value a @ [b] in   (* N-ary: [pair (pair a b) c] -> [a;b;c] -> [return a, b, c] *)
      (* a NARROW component (int64-carrier) into its narrow return slot needs the cast — e.g. a
         `func() (uint8, uint8)` whose `return (x & 0xff), …` would otherwise be int64-into-uint8 (P1 #4). *)
      let pp_val v =
        match value_narrow_conv env v with
        | Some gt -> str (gt ^ "(") ++ pp_expr state env v ++ str ")"
        | None    -> pp_expr state env v in
      str tab ++ str "return " ++ prlist_with_sep (fun () -> str ", ") pp_val vals ++ fnl ()
  | _ -> return_fallback ()

(** Emit a top-level function, collecting leading lambdas for the signature
    and using [typ] for parameter/return type annotations.

    A function whose first visible parameter is a record (struct) is emitted as a
    Go value-receiver METHOD [func (recv T) M(rest…)] — faithful (a method call
    [recv.M(a)] denotes the same as [M(recv, a)]) and idiomatic.  Detection here
    mirrors [collect_decls]'s, so declaration and call sites agree. *)
let pp_function state name body typ =
  Hashtbl.reset narrow_var_types;   (* P0 #2: narrow-let-var table is per-function (names are fn-local) *)
  let ids, inner_body = collect_lam body in
  let param_types, ret_type = collect_tarrs typ in
  (* P0 #2: a sub-64 narrow return type makes the pure-tail [return]s wrap the int-carrier value
     in its declared Go type ([narrow_ret_type]); [None] for full-width / IO / non-narrow returns. *)
  narrow_ret_type := (match ret_type with Tglob (r, []) -> narrow_prim_type r | _ -> None);
  (* Pair visible ids with their corresponding types, skipping erased args. *)
  let rec zip_params ids types =
    match ids, types with
    | [], _              -> []
    | id :: rest, _ when is_dummy id ->
        let rest_types = (match types with _ :: t -> t | [] -> []) in
        zip_params rest rest_types
    | id :: rest, t :: rest_types -> (id, t) :: zip_params rest rest_types
    | id :: rest, []              -> (id, Tunknown) :: zip_params rest []
  in
  let param_pairs = zip_params ids param_types in
  (* env includes all ids (with dummies) for de Bruijn; innermost first. *)
  let full_env = List.rev ids in
  let pp_param (id, t) = match t with
    (* a [Variadic T] param is Go's variadic [xs ...T] (T is the wrapper's element type) *)
    | Tglob (r, [inner]) when is_variadic_type r ->
        pp_mlident id ++ str " ..." ++ pp_type state inner
    | _ -> pp_mlident id ++ str " " ++ pp_type state t in
  (* Go GENERICS: a parametric-polymorphic function's TYPE VARIABLES become a
     type-parameter list [T1 any, …] after the name (constraint [any] — Coq parametric
     polymorphism imposes no operations on the type).  Call sites rely on Go's type
     inference, so they need no change.  NOT emitted for a method: Go forbids a method
     from introducing its own type parameters (a generic method's params come from a
     generic receiver type — the generic-struct feature, not yet modeled). *)
  let tvars =
    let fp = List.fold_left (fun acc (_, t) -> collect_tvars acc t) [] param_pairs in
    collect_tvars fp ret_type in
  (* comparable-constraint: type vars carried by a [ComparableW (Tvar)] witness param get the
     [comparable] constraint (not [any]); the witness params themselves are DROPPED from the
     rendered signature (kept in [full_env] for de Bruijn, and their only body use — [cw_eqb] —
     lowers to [==], so they never appear in the emitted body either). *)
  let comparable_tvars =
    List.concat_map (fun (_, t) -> match t with
      | Tglob (_, args) when is_comparablew_type t -> List.concat_map (collect_tvars []) args
      | _ -> []) param_pairs in
  let render_pairs = List.filter (fun (_, t) -> not (is_comparablew_type t)) param_pairs in
  let tparams_doc =
    if tvars = [] then mt ()
    else str "[" ++
         prlist_with_sep (fun () -> str ", ")
           (fun i -> str ("T" ^ string_of_int i) ++
                     str (if List.mem i comparable_tvars then " comparable" else " any")) tvars ++
         str "]" in
  let fn_sig =
    match render_pairs with
    | (rid, rt) :: rest when is_record_tglob rt || is_sptr_record_tglob rt ->  (* value- or pointer-receiver method *)
        str "func (" ++ pp_param (rid, rt) ++ str ") " ++
        str (go_export name) ++ str "(" ++
        prlist_with_sep (fun () -> str ", ") pp_param rest
    | _ ->
        str "func " ++ str (go_export name) ++ tparams_doc ++ str "(" ++
        prlist_with_sep (fun () -> str ", ") pp_param render_pairs
  in
  (* IO-returning functions use pp_io_body; pure functions use return+expr *)
  match fullwidth_conv_name name with
  | Some conv ->
      (* Full-width int64<->uint64 reinterpret: emit the canonical one-liner
         [func Name(a int64) uint64 { return uint64(a) }].  The body's Z-arith
         (wrapU64/i64raw) is proof-only and would not lower; the conversion is the
         Go type-cast on the parameter variable. *)
      let arg = (match param_pairs with (id, _) :: _ -> pp_mlident id | [] -> str "_") in
      fn_sig ++ str ") " ++ pp_type state ret_type ++ str " {" ++ fnl () ++
      str "\treturn " ++ str conv ++ str "(" ++ arg ++ str ")" ++ fnl () ++
      str "}" ++ fnl ()
  | None ->
  match ret_type with
  | Tglob (r, [inner]) when is_IO_type r ->
      let ret_val = match inner with
        | Tglob (r2, []) when is_unit_type r2 -> false   (* IO unit → void *)
        | _ -> true in
      let go_ret = if ret_val then str " " ++ pp_type state inner else mt () in
      fn_sig ++ str ")" ++ go_ret ++ str " {" ++ fnl () ++
      pp_io_body ~ret_val state "\t" full_env inner_body ++
