(* ---- Float CONSTANT-FOLDING soundness (code review) ----
   A float op whose operands are all Go CONSTANTS is emitted as a Go *constant* expression,
   where IEEE semantics do NOT hold: Go constants cannot denote -0 / ±Inf / NaN, and a constant
   [/0] or a [float32] overflow are COMPILE ERRORS.  Fido's model is runtime IEEE, so such an op
   must be forced to RUNTIME.  An operand that is a runtime VARIABLE ([MLrel]) already forces
   runtime (Go [:=] vars are not constants); otherwise wrap in a typed IIFE.  Soundness: we force
   UNLESS an operand is [MLrel], so we never leave an all-constant float op unforced (we may
   over-force a non-[MLrel] runtime operand — harmless, the IIFE is value-preserving). *)
let operand_is_runtime = function MLrel _ -> true | _ -> false
(* Go type of a forceable binary float/complex ARITHMETIC op (not comparison), else None.  Complex
   constants hit the SAME Go-constant-vs-runtime hole as float (a constant complex can't denote -0,
   ±Inf, NaN; constant /0 fails to compile), so complex arithmetic is forced too. *)
let float_arith_go_type r =
  if Option.has_some (classify_f32_op r) then Some "float32"
  else if List.exists (is_float_op_ref r) ["add"; "sub"; "mul"; "div"] then Some "float64"
  else if List.mem (global_basename r) ["complex_add"; "complex_sub"; "complex_mul"; "complex_div"] then Some "complex128"
  else None
(* Go float type of a float min/max ([f32_min]/[f64_max]/…), else None (int min/max need no force). *)
let float_minmax_go_type r =
  match global_basename r with
  | "f32_min" | "f32_max" -> Some "float32"
  | "f64_min" | "f64_max" -> Some "float64"
  | _ -> None

(* Go type of a forceable full-width int ARITHMETIC / bitwise / shift op, else None.  Like floats, an
   all-CONSTANT full-width int op constant-folds under Go's ARBITRARY-PRECISION rules, NOT runtime
   wrapping: [u64_sub 0 1] = -1 (not 2^64-1), [i64_add MAX 1] / [1<<70] overflow the constant (compile
   error), [u64_and] on a negative constant is nonsense.  Force to runtime via a typed IIFE so Go applies
   fixed-width wrapping.  COMPARISONS (ltb/leb/eqb/gtb/geb/neqb) are EXCLUDED — they yield [bool] (no wrap,
   and an [int64]-returning IIFE would be ill-typed) and a constant comparison equals its runtime value. *)
let full_width_arith_suffixes =
  ["add"; "sub"; "mul"; "add_nz"; "sub_nz"; "mul_nz"; "div"; "mod";
   "and"; "andnot"; "shl"; "shr"; "or"; "xor"]
let int_arith_go_type r =
  if List.exists (is_i64_op r) full_width_arith_suffixes then Some "int64"
  else if List.exists (is_u64_op r) full_width_arith_suffixes then Some "uint64"
  else None
(* Combined force type: float/complex arith OR full-width int arith. *)
let arith_force_go_type r =
  match float_arith_go_type r with
  | Some _ as t -> t
  | None -> int_arith_go_type r

(* Coq [nat] is modelled as Go [uint].  Add/mul/comparison are faithful within
   the representable range (a [nat] too large for [uint] is unrepresentable
   either way), but [Nat.sub] is TRUNCATED monus ([3 - 5 = 0]) whereas Go [uint]
   subtraction WRAPS ([3 - 5 = 2^64 - 2]) — they disagree even on small values.
   So [Nat.sub] is excluded here; it must NOT lower to a bare Go [-].  ([pp_expr]
   turns any use into a loud [unsupported] error.)  This is the *Coq nat* path
   only; [PrimInt63.sub] (signed two's-complement, faithful) goes through
   [classify_int63_op] and is unaffected. *)
let is_nat_sub_ref r = is_nat_op_ref r "sub"
let classify_nat_op r =
  List.find_map
    (fun (name, op) ->
       if String.equal name "sub" then None
       else if is_nat_op_ref r name then Some op else None)
    nat_op_names

(* int63 (the [int] = Sint63 model).  Two's-complement [add]/[sub]/[mul] and
   bit-equality [eqb] are identical signed/unsigned, so they lower directly.  But
   the UNSIGNED comparisons [PrimInt63.ltb]/[leb] must NOT map to Go's int64
   [<]/[<=], which are SIGNED — they disagree once the high bit is set (unsigned
   [ltb (-1) 0] is false; signed [-1 < 0] is true).  Signed comparison for [int]
   goes through [ltsb]/[lesb] (classify_sint63_op → Go signed [<]/[<=]); the
   user-facing [Sint63.ltb]/[leb] reduce to those.  So ltb/leb are excluded here;
   raw [PrimInt63.ltb]/[leb] fail loud (no unsigned-int model yet to target). *)
let int63_op_names = [ "add", NatAdd; "sub", NatSub; "mul", NatMul; "eqb", NatEqb ]
let classify_int63_op r =
  List.find_map
    (fun (name, op) -> if is_int63_op_ref r name then Some op else None)
    int63_op_names

(* Signed comparison AND signed division/remainder — where signed and unsigned
   differ ([int] is modelled with signed int64 semantics; the raw +/-/* ops are
   shared).  The signed primitives [ltsb]/[lesb]/[divs]/[mods] live in
   [PrimInt63]; Go's [<]/[<=]/[/]/[%] are type-directed (signed on int64), so
   emitting them is faithful (both truncate division toward zero).  [divs]/[mods]
   are reached only via the proof-carrying [div_nz]/[mod_nz] (which demand the
   divisor be non-zero); raw [PrimInt63.divs] is the escape hatch (Go panics on
   a zero divisor, mirroring raw send/slice_get). *)
let sint63_op_names =
  [ "ltsb", NatLtb; "lesb", NatLeb; "divs", NatDiv; "mods", NatMod ]
let classify_sint63_op r =
  List.find_map
    (fun (name, op) -> if is_int63_op_ref r name then Some op else None)
    sint63_op_names

let go_infix = function
  | NatAdd -> " + " | NatSub -> " - " | NatMul -> " * "
  | NatDiv -> " / " | NatMod -> " % "
  | NatEqb -> " == " | NatLtb -> " < " | NatLeb -> " <= "

(* Go binary-operator precedence (higher binds tighter).  Used by the expression
   printer to parenthesise a sub-operand only when its operator binds looser than
   the surrounding context requires (instead of the old conservative
   "parenthesise every non-atom"). *)
let op_prec = function
  | NatMul | NatDiv | NatMod -> 5
  | NatAdd | NatSub          -> 4
  | NatEqb | NatLtb | NatLeb -> 3
let float_prec s =
  if String.equal s " * " || String.equal s " / " then 5
  else if String.equal s " < " || String.equal s " <= " || String.equal s " == "
  then 3        (* comparisons bind looser than + / -, like the integer ops *)
  else 4

(* Boolean operators.  Coq's [andb]/[orb] take two already-evaluated [bool]
   values; Go's [&&]/[||] short-circuit.  This is observationally identical here:
   the operands are pure, total [bool] values (no effects, no divergence in the
   model), so whether the right operand is "evaluated" is unobservable.  Go
   precedence: [||] = 1 (loosest), [&&] = 2, comparisons = 3. *)
let is_andb_ref r = named "andb" r
let is_orb_ref  r = named "orb"  r
let is_negb_ref r = named "negb" r

(* If [r] is a recognised inlined binary operator, its (precedence, Go operator
   string); covers boolean / nat / int63 / signed-int63 / float arithmetic and
   comparison.  ([negb] is unary — handled separately in the expression printer.) *)
let binop_of r =
  if is_andb_ref r then Some (2, " && ")
  else if is_orb_ref r then Some (1, " || ")
  (* string concatenation: Go [+], same precedence as numeric [+] (level 4) *)
  else if is_str_concat_ref r then Some (4, " + ")
  (* string comparison: Go [==] / [<] (lexicographic), comparison precedence (level 3) *)
  else if is_str_eqb_ref r then Some (3, " == ")
  else if is_str_ltb_ref r then Some (3, " < ")
  (* array comparison: Go field-wise [==] (arrays are comparable, slices are not) *)
  else if is_arr_eqb_ref r then Some (3, " == ")
  (* direct >/>=/!= for string and float64 (recognized by name, gated on builtins.v) *)
  else if named "str_gtb"  r then Some (3, " > ")
  else if named "str_geb"  r then Some (3, " >= ")
  else if named "str_neqb" r then Some (3, " != ")
  else if named "f64_gtb"  r then Some (3, " > ")
  else if named "f64_geb"  r then Some (3, " >= ")
  else if named "f64_neqb" r then Some (3, " != ")
  (* float32 comparison: same direct operators, on [float32] operands *)
  else if named "f32_ltb"  r then Some (3, " < ")
  else if named "f32_leb"  r then Some (3, " <= ")
  else if named "f32_eqb"  r then Some (3, " == ")
  else if named "f32_gtb"  r then Some (3, " > ")
  else if named "f32_geb"  r then Some (3, " >= ")
  else if named "f32_neqb" r then Some (3, " != ")
  (* complex128 arithmetic: component-wise [+]/[-] → native Go operators (float +/-) *)
  else if named "complex_add"  r then Some (4, " + ")
  else if named "complex_sub"  r then Some (4, " - ")
  else if named "complex_mul"  r then Some (5, " * ")
  else if named "complex_div"  r then Some (5, " / ")
  (* complex128 comparison: component-wise [==]/[!=] → native Go operators *)
  else if named "complex_eqb"  r then Some (3, " == ")
  else if named "complex_neqb" r then Some (3, " != ")
  (* full-width int64 (GoI64): a Go int64 wraps natively at 2^64, so arithmetic /
     bitwise / shift lower to BARE Go operators (no mask) and comparison is signed
     int64 </<=/==.  Go precedence: [* / % << >> & &^] = 5, [+ - | ^] = 4, cmp = 3.
     Go int64 [/] truncates toward zero and [>>] is arithmetic — matching [Z.quot]/
     [Z.shiftr] in the model. *)
  else if is_i64_op r "add"    then Some (4, " + ")
  else if is_i64_op r "sub"    then Some (4, " - ")
  else if is_i64_op r "mul"    then Some (5, " * ")
  (* overflow-safe forms: the no-overflow proof is erased, so [i64_add_nz a b] is the
     same bare Go op as [i64_add a b] (the proof showed it does not wrap). *)
  else if is_i64_op r "add_nz" then Some (4, " + ")
  else if is_i64_op r "sub_nz" then Some (4, " - ")
  else if is_i64_op r "mul_nz" then Some (5, " * ")
  else if is_i64_op r "div"    then Some (5, " / ")
  else if is_i64_op r "mod"    then Some (5, " % ")
  else if is_i64_op r "and"    then Some (5, " & ")
  else if is_i64_op r "andnot" then Some (5, " &^ ")
  else if is_i64_op r "shl"    then Some (5, " << ")
  else if is_i64_op r "shr"    then Some (5, " >> ")
  else if is_i64_op r "or"     then Some (4, " | ")
  else if is_i64_op r "xor"    then Some (4, " ^ ")
  else if is_i64_op r "ltb"    then Some (3, " < ")
  else if is_i64_op r "leb"    then Some (3, " <= ")
  else if is_i64_op r "eqb"    then Some (3, " == ")
  else if is_i64_op r "gtb"    then Some (3, " > ")
  else if is_i64_op r "geb"    then Some (3, " >= ")
  else if is_i64_op r "neqb"   then Some (3, " != ")
  (* platform int (GoInt, review #6 #13): a Go int wraps natively (64-bit), so arithmetic /
     comparison lower to the same BARE Go operators as int64; division truncates toward zero. *)
  else if is_int_op r "add"    then Some (4, " + ")
  else if is_int_op r "sub"    then Some (4, " - ")
  else if is_int_op r "mul"    then Some (5, " * ")
  else if is_int_op r "div"    then Some (5, " / ")
  else if is_int_op r "mod"    then Some (5, " % ")
  else if is_int_op r "ltb"    then Some (3, " < ")
  else if is_int_op r "leb"    then Some (3, " <= ")
  else if is_int_op r "eqb"    then Some (3, " == ")
  (* full-width uint64 (GoU64): same bare Go operators as i64 — Go uint64 wraps
     unsigned-natively at 2^64, arithmetic / bitwise / shift lower identically.
     Comparison is unsigned uint64 </<=/==, which matches [Z.ltb]/[Z.leb] on the
     non-negative [u64raw] values.  Go uint64 [/] is truncating-non-negative,
     matching [Z.div] on non-negative operands. *)
  else if is_u64_op r "add"    then Some (4, " + ")
  else if is_u64_op r "sub"    then Some (4, " - ")
  else if is_u64_op r "mul"    then Some (5, " * ")
  else if is_u64_op r "div"    then Some (5, " / ")
  else if is_u64_op r "mod"    then Some (5, " % ")
  else if is_u64_op r "and"    then Some (5, " & ")
  else if is_u64_op r "andnot" then Some (5, " &^ ")
  else if is_u64_op r "shl"    then Some (5, " << ")
  else if is_u64_op r "shr"    then Some (5, " >> ")
  else if is_u64_op r "or"     then Some (4, " | ")
  else if is_u64_op r "xor"    then Some (4, " ^ ")
  else if is_u64_op r "ltb"    then Some (3, " < ")
  else if is_u64_op r "leb"    then Some (3, " <= ")
  else if is_u64_op r "eqb"    then Some (3, " == ")
  else if is_u64_op r "gtb"    then Some (3, " > ")
  else if is_u64_op r "geb"    then Some (3, " >= ")
  else if is_u64_op r "neqb"   then Some (3, " != ")
  (* fixed-width uN/iN comparisons: values are in range, so Go's signed int64
     </<=/== agree with both unsigned and signed fixed-width comparison. *)
  else if fw_is r "ltb" then Some (3, " < ")
  else if fw_is r "leb" then Some (3, " <= ")
  else if fw_is r "eqb" then Some (3, " == ")
  else if fw_is r "gtb" then Some (3, " > ")
  else if fw_is r "geb" then Some (3, " >= ")
  else if fw_is r "neqb" then Some (3, " != ")
  (* fixed-width bitwise: no wrap needed (in-range / sign-extended results are
     already correct on int64).  Go precedence: [& &^] = 5, [| ^] = 4. *)
  else if fw_is r "and"    then Some (5, " & ")
  else if fw_is r "andnot" then Some (5, " &^ ")
  else if fw_is r "or"     then Some (4, " | ")
  else if fw_is r "xor"    then Some (4, " ^ ")
  else
  match classify_nat_op r with
  | Some op -> Some (op_prec op, go_infix op)
  | None ->
  match classify_int63_op r with
  | Some op -> Some (op_prec op, go_infix op)
  | None ->
  match classify_sint63_op r with
  | Some op -> Some (op_prec op, go_infix op)
  | None ->
  match classify_float_op r with
  | Some s -> Some (float_prec s, s)
  | None ->
  match classify_f32_op r with
  | Some s -> Some (float_prec s, s)
  | None -> None

(*s Type printer. *)

let rec pp_type state = function
  (* [unit -> R] → Go's nullary [func() R] (the unit arg is erased) — a NULLARY method
     in an interface dictionary (e.g. [String() string]). *)
  | Tarr (t1, t2) when (match t1 with Tglob (r, []) -> is_unit_type r | _ -> false) ->
      str "func() " ++ pp_type state t2
  (* a func returning [IO unit] / [unit] is VOID — emit func with NO result type, matching a
     void method's signature, so a pointer-receiver method expression of an IO-unit method
     type-checks against the HOF param. *)
  | Tarr (t1, t2) when (match t2 with
        | Tglob (r, []) -> is_unit_type r
        | Tglob (r, [Tglob (u, [])]) -> is_IO_type r && is_unit_type u
        | _ -> false) ->
      str "func(" ++ pp_type state t1 ++ str ")"
  | Tarr (t1, t2) ->
      str "func(" ++ pp_type state t1 ++ str ") " ++ pp_type state t2
  | Tglob (r, _)  when is_sigT_ref r -> str "any"
  (* Each numeric wrapper renders as its REAL Go type — GoU8→uint8 … GoI32→int32,
     GoI64→int64, GoU64→uint64 (break #7: a value's static / interface type is faithful;
     the arithmetic stays int64+mask, converted to the narrow type at boundaries / the [any]
     box).  Name shape ["Go"(U|I)<width>] → [(u)int<width>]. *)
  | Tglob (r, _)  when is_numint_type r ->
      let n = global_basename r in
      str ((if String.length n > 2 && n.[2] = 'I' then "int" else "uint")
           ^ String.sub n 3 (String.length n - 3))
  (* IO A erases to A — the world token has no runtime representation *)
  | Tglob (r, [arg]) when is_IO_type r -> pp_type state arg
  (* Ref A → T (a mutable local cell is just a Go variable of type T) *)
  | Tglob (r, [arg]) when is_ref_type r -> pp_type state arg
  (* Ptr A → *T (a first-class Go pointer; copies alias the same cell) *)
  | Tglob (r, [arg]) when is_ptr_type r -> str "*" ++ pp_type state arg
  | Tglob (r, arg :: _) when is_sptr_type r -> str "*" ++ pp_type state arg  (* SPtr R / SPtrH R A B → *R *)
  (* GoArr<N> A → [N]T — a fixed-size array in a typed position (size N from the type name) *)
  | Tglob (r, [a]) when is_arrN_type r ->
      str (Printf.sprintf "[%d]" (Option.get (arrN_size_of_type r))) ++ pp_type state a
  | Tglob (r, _) when is_arr_type r ->
      unsupported "an array type in a position needing an explicit [N]T (param / field / return / typed var decl); only LOCAL arrays are supported — write `a := arr_lit […]` so Go infers the size from the literal (the size lives in the construction, not the Coq type)"
  (* SliceH A → []T (Go's slice IS the aliasing handle; sub-slices share) *)
  | Tglob (r, [arg]) when is_sliceh_type r -> str "[]" ++ pp_type state arg
  (* GoFunc A B → func(A) B — a NULLABLE func value (review #8); the [option] wrapper is erased
     because a Go func is already nilable ([None]=nil, [Some f]=f). *)
  | Tglob (r, [a; b]) when is_gofunc_type r ->
      pp_type state (Tarr (a, b))
  (* GoChan A → chan T *)
  | Tglob (r, [arg]) when is_go_chan_type r ->
      str "chan " ++ pp_type state arg
  (* GoMap K V → map[KT]VT *)
  | Tglob (r, [kt; vt]) when is_go_map_type r ->
      str "map[" ++ pp_type state kt ++ str "]" ++ pp_type state vt
  (* list A = GoSlice A → []T  (a rune slice [list GoRune] is just []int32; the
     byte-sequence string is the distinct Coq [string] type, handled below).  [GoSlice]
     is a Fido [Definition := list]; in most positions extraction unfolds it to [list],
     but a RECORD FIELD type keeps it unexpanded — so recognise the [GoSlice] name too
     (parallel to [GoMap]/[GoChan]), e.g. a defined type [type IntList []int64]. *)
  | Tglob (r, [arg]) when is_list_type r || named "GoSlice" r ->
      str "[]" ++ pp_type state arg
  (* Coq [string] (Strings.String) → Go [string] (byte sequence) *)
  | Tglob (r, []) when is_string_type r -> str "string"
  | Tglob (r, []) when is_float64_type r  -> str "float64"
  | Tglob (r, []) when is_complex_type r  -> str "complex128"
  | Tglob (r, [a; b]) when is_prod_type r ->   (* multiple return values: (A, B, …), N-ary *)
      let tys = flatten_prod_type a @ [b] in
      if List.exists (function Tglob (rr, _) -> is_prod_type rr | _ -> false) tys
      then unsupported "a non-left-nested product return type — Go's multiple-return tuple is FLAT, so only the left-nested `A * B * … * Z` lowers (a `A * (B * C)` cannot be a nested Go tuple)"
      else str "(" ++ prlist_with_sep (fun () -> str ", ") (pp_type state) tys ++ str ")"
  | Tglob (r, []) when is_go_prim_type r -> str (Option.get (classify_go_prim_type r))
  | Tglob (r, []) when is_unit_type r   -> str "struct{}"
  | Tglob (r, []) when is_uint63_type r -> str "int"   (* PrimInt63 = Go's platform [int]
       (break #7: DISTINCT from the Z-carried [GoI64]=int64, so a boxed int ≠ a boxed int64
       and [tag_eq TInt64 TI64 = None] now AGREES with Go's [v.(int)] vs [v.(int64)]). *)
  | Tglob (r, []) when is_nat_type r    -> str "uint"
  | Tglob (r, []) when is_bool_type r   -> str "bool"
  | Tglob (r, args) ->
      (* A user record reaches here only if it was registered (collect_decls
         pass 1) and hence has a `type T struct {…}` decl emitted.  A Tglob that
         is NOT a registered record has no type decl — emitting its bare name
         would dangle.  The canonical offender is a SINGLE-FIELD Record: Coq
         unboxes it (`Inner {iv}` ≡ its field type), so no struct is emitted yet
         a field still references `Inner`.  Fail loud rather than emit wrong Go
         (meta-invariant); give such a record ≥2 fields. *)
      let bn = global_basename r in
      if not (is_record_typename bn || is_enum_typename bn) then
        unsupported (Printf.sprintf
          "type '%s' (no struct decl emitted — a single-field Record is unboxed \
           by Coq; give it >= 2 fields)" (go_export bn));
      str (go_export bn) ++
      (if args = [] then mt ()
       else
         str "[" ++
         prlist_with_sep (fun () -> str ", ") (pp_type state) args ++
         str "]")
  | Tvar i | Tvar' i -> str ("T" ^ string_of_int i)
  | Tdummy _ | Tunknown | Taxiom | Tmeta _ -> str "any"

(* A type in CONVERSION position [<T>(x)].  A composite type (func, …) whose
   rendering contains a space must be parenthesised — Go parses [func(int64) int64(x)]
   wrong, needs [(func(int64) int64)(x)] — whereas a bare identifier ([int64], [string])
   must NOT be (Go rejects [(int64)(x)]… actually accepts it, but keep it clean). *)
let pp_cast_type state t =
  match t with
  | Tarr _ -> str "(" ++ pp_type state t ++ str ")"
  | _ -> pp_type state t

let rec collect_tarrs = function
  | Tarr (t, rest) -> let ts, ret = collect_tarrs rest in t :: ts, ret
  | t              -> [], t

(* The distinct TYPE VARIABLES of an ml_type, in first-seen order — for a Go generic
   function's type-parameter list [func F[T1 any, …]].  [pp_type] renders [Tvar i] /
   [Tvar' i] both as [T<i>], so they share the index space here. *)
let rec collect_tvars acc = function
  | Tvar i | Tvar' i  -> if List.mem i acc then acc else acc @ [i]
  | Tarr (a, b)       -> collect_tvars (collect_tvars acc a) b
  | Tglob (_, args)   -> List.fold_left collect_tvars acc args
  | Tmeta _ | Tdummy _ | Tunknown | Taxiom -> acc

let is_erased = function MLdummy _ -> true | _ -> false

(*s De Bruijn environment.

    [env] is the list of bound [ml_ident]s, innermost binder first.
    [MLrel 1] is the innermost (most-recently-bound) variable.

    In Rocq 9.x, [ml_ident] is a variant: [Dummy | Id of variable | Tmp of variable]. *)

let is_dummy = function Dummy -> true | _ -> false

let pp_mlident = function
  | Dummy   -> str "_"
  | Id v    -> str (go_safe (Id.to_string v))
  | Tmp v   -> str (go_safe (Id.to_string v))

let pp_rel env i =
  try pp_mlident (List.nth env (i - 1))
  with Not_found -> str ("_db" ^ string_of_int i)

(*s Collect nested lambdas into a flat parameter list. *)

let collect_lam body =
  let rec go acc = function
    | MLlam (id, body) -> go (id :: acc) body
    | body             -> List.rev acc, body
  in
  go [] body

(*s Flatten nested applications. *)

let collect_app head args =
  let rec go acc = function
    | MLapp (h, more) -> go (more @ acc) h
    | h               -> h, acc
  in
  go args head

(*s Peel [MLmagic] coercions (inserted by extraction around casts) to reach
    the underlying node — needed to recognise [MLcase] under a coercion. *)
let rec strip_magic = function
  | MLmagic e -> strip_magic e
  | e         -> e

(* N-ary multiple return — VALUE side.  Flatten the left spine of a nested pair VALUE
   [pair (pair a b) c] -> [a; b; c] (for [return a, b, c]).  Mirrors [flatten_prod_type]; a
   non-left-spine element stays a [pair] and aborts at its [pp_expr] (fail-closed). *)
let rec flatten_pair_value v =
  match strip_magic v with
  | MLcons (_, r, [a; b])       when is_pair_ref r -> flatten_pair_value a @ [b]
  | MLcons (_, r, [_; _; a; b]) when is_pair_ref r -> flatten_pair_value a @ [b]
  | _ -> [v]

(* N-ary multiple return — DESTRUCTURE side.  [let '((x,y),z) := f in body] extracts as NESTED
   pair-matches: the outer binds [p; z] and its body immediately re-destructures [p] (the FIRST
   binder, always [MLrel 2] in the branch's de-Bruijn context, since a pair binds [first; second]
   -> env [second; first; …]).  Collect the FLAT leaf binders [x; y; z] for a single Go
   [x, y, z := f()], and return the innermost body with the env the nested matches build — which
   KEEPS the eliminated intermediate [p] as an unused placeholder so every de-Bruijn index in the
   body still resolves (no lifting).  A non-nested body is the ordinary flat 2-destructure. *)
let rec flatten_destructure ids body env =
  let body_env = List.rev ids @ env in
  match strip_magic body with
  | MLcase (_, scrut, [| (ids2, pat2, body2) |])
    when (match strip_magic scrut with MLrel 2 -> true | _ -> false)
         && (match pat2 with Pcons (r, _) | Pusual r -> is_pair_ref r | _ -> false)
         && List.length ids2 = 2 ->
      let leaves, fb, fenv = flatten_destructure ids2 body2 body_env in
      (leaves @ [List.nth ids 1], fb, fenv)
  | _ -> ([List.nth ids 0; List.nth ids 1], body, body_env)

(*s Unfold a Rocq list literal (MLcons-based) to an OCaml list, or None. *)

let rec unfold_list acc = function
  | MLcons (_, r, [x; rest]) when is_list_cons r -> unfold_list (x :: acc) rest
  | MLcons (_, r, [])        when is_list_nil r  -> Some (List.rev acc)
  | _ -> None

(*s Decode a Coq [string] literal to its bytes, or None if it is not a literal
    (e.g. a runtime [String c rest] with a non-constant head — which we do NOT
    build at runtime; concat lowers to Go [+]).  An [Ascii a0 .. a7] is a byte
    with [a0] the LEAST-significant bit (matching [Ascii.N_of_ascii]). *)
let decode_ascii e =
  match strip_magic e with
  | MLcons (_, r, bits) when is_ascii_ctor r && List.length bits = 8 ->
      let rec go i acc = function
        | [] -> Some acc
        | b :: rest ->
            (match strip_magic b with
             | MLcons (_, rb, []) when is_bool_true rb  -> go (i + 1) (acc lor (1 lsl i)) rest
             | MLcons (_, rb, []) when is_bool_false rb -> go (i + 1) acc rest
             | _ -> None)
      in go 0 0 bits
  | _ -> None

let rec decode_go_string e =
  match strip_magic e with
  | MLcons (_, r, []) when is_string_empty r -> Some []
  | MLcons (_, r, [a; rest]) when is_string_cons r ->
      (match decode_ascii a, decode_go_string rest with
       | Some b, Some bs -> Some (b :: bs)
       | _ -> None)
  | _ -> None

(* Render a byte list as a Go double-quoted string literal.  Bytes outside
   printable ASCII (and backslash / double-quote) use Go's hex byte escape
   [backslash-x-NN], so the literal denotes EXACTLY those bytes (byte-faithful,
   like the Coq model). *)
let go_string_lit bytes =
  let buf = Buffer.create (List.length bytes + 2) in
  Buffer.add_char buf '"';
  List.iter (fun b ->
    if b = Char.code '"' then Buffer.add_string buf "\\\""
    else if b = Char.code '\\' then Buffer.add_string buf "\\\\"
    else if b = Char.code '\n' then Buffer.add_string buf "\\n"
    else if b = Char.code '\t' then Buffer.add_string buf "\\t"
    else if b = Char.code '\r' then Buffer.add_string buf "\\r"
    else if b >= 0x20 && b < 0x7f then Buffer.add_char buf (Char.chr b)
    else Buffer.add_string buf (Printf.sprintf "\\x%02x" b)) bytes;
  Buffer.add_char buf '"';
  Buffer.contents buf


(*s Fold a Peano nat literal to an integer, or return None. *)

let rec nat_value = function
  | MLcons (_, r, [])  when is_nat_zero r -> Some 0
  | MLcons (_, r, [n]) when is_nat_succ r ->
      (match nat_value n with Some v -> Some (v + 1) | None -> None)
  | _ -> None

(*s Fold a Coq [Z] literal to its value as an [Int64], or None if not a literal.
    [Z] = Z0 | Zpos positive | Zneg positive; [positive] = xH | xO p | xI p (LSB-
    recursive: [xO p = 2p], [xI p = 2p+1], [xH = 1]).  [Int64] is exactly the int64
    range, so a constant the smart [i64_lit] admits (proved [in_i64], i.e. in
    [[-2^63, 2^63)]) is represented faithfully — the lone [-2^63] case folds its
    magnitude [2^63] to [Int64.min_int] by two's-complement wrap, and negating
    [min_int] is [min_int], the correct value.  Non-literals fail loud upstream. *)
let rec pos_value = function
  | MLcons (_, r, [])  when String.equal (global_basename r) "xH" -> Some 1L
  | MLcons (_, r, [p]) when String.equal (global_basename r) "xO" ->
      (match pos_value p with Some v -> Some (Int64.mul 2L v) | None -> None)
  | MLcons (_, r, [p]) when String.equal (global_basename r) "xI" ->
      (match pos_value p with Some v -> Some (Int64.add (Int64.mul 2L v) 1L) | None -> None)
  | _ -> None
let z_value = function
  | MLcons (_, r, [])  when String.equal (global_basename r) "Z0"   -> Some 0L
  | MLcons (_, r, [p]) when String.equal (global_basename r) "Zpos" -> pos_value p
  | MLcons (_, r, [p]) when String.equal (global_basename r) "Zneg" ->
      (match pos_value p with Some v -> Some (Int64.neg v) | None -> None)
  | _ -> None

(* Checked signed-int64 arithmetic for CONSTANT-EXPRESSION folding: [None] on overflow, so a
   constant whose INTERMEDIATE exceeds int64 fails loud rather than silently wrapping — Go folds
   constants at arbitrary precision, so `(1<<62)+(1<<62)-(1<<62)` (= 2^62, fits) must NOT wrap at
   the `+`.  The final value is in-range ([in_i64] proven), but intermediates need not be. *)
let chk_add a b = let s = Int64.add a b in
  if Int64.logxor a b >= 0L && Int64.logxor a s < 0L then None else Some s
let chk_sub a b = let s = Int64.sub a b in
  if Int64.logxor a b < 0L && Int64.logxor a s < 0L then None else Some s
let chk_mul a b =
  if a = 0L || b = 0L then Some 0L
  else if (a = Int64.min_int && b = -1L) || (b = Int64.min_int && a = -1L) then None
  else let p = Int64.mul a b in if Int64.div p a = b then Some p else None
let chk_shl a n =
  if n < 0L || n >= 64L || a < 0L then None
  else let s = Int64.shift_left a (Int64.to_int n) in
       if Int64.shift_right_logical s (Int64.to_int n) = a then Some s else None

(* Fold a CONSTANT [Z] EXPRESSION (a literal, or [Z.add]/[sub]/[mul]/[opp]/[shiftl]/[land]/[lor]/
   [lxor] of constants) to its int64 value — [None] if not a closed constant or an intermediate
   overflows.  Lets a Go untyped CONSTANT EXPRESSION ([i64_lit (1<<40 + 5)]) extract, not just a
   pre-folded literal.  ([Z] ops matched by basename + a [BinInt] module path.) *)
let rec z_eval e =
  match z_value e with
  | Some v -> Some v
  | None ->
    let is_zop name h = match h with
      | MLglob r -> String.equal (global_basename r) name && path_contains r "BinInt"
      | _ -> false in
    let bin f a b = match z_eval a, z_eval b with Some x, Some y -> f x y | _ -> None in
    match e with
    | MLapp (h, [a; b]) when is_zop "add"    h -> bin chk_add a b
    | MLapp (h, [a; b]) when is_zop "sub"    h -> bin chk_sub a b
    | MLapp (h, [a; b]) when is_zop "mul"    h -> bin chk_mul a b
    | MLapp (h, [a; b]) when is_zop "shiftl" h -> bin chk_shl a b
    | MLapp (h, [a; b]) when is_zop "land"   h -> bin (fun x y -> Some (Int64.logand x y)) a b
    | MLapp (h, [a; b]) when is_zop "lor"    h -> bin (fun x y -> Some (Int64.logor  x y)) a b
    | MLapp (h, [a; b]) when is_zop "lxor"   h -> bin (fun x y -> Some (Int64.logxor x y)) a b
    | MLapp (h, [a])    when is_zop "opp"    h ->
        (match z_eval a with Some x when x <> Int64.min_int -> Some (Int64.neg x) | _ -> None)
    | _ -> None

(* UNSIGNED constant folding for [u64_lit] — same as [z_eval] but with uint64 semantics: ops
   compute on the int64 BIT PATTERN (two's-complement add/mul = unsigned mod 2^64), and overflow
   (a result exceeding 2^64, or a subtraction going below 0 — not a valid uint64 constant) FAILS
   LOUD via the unsigned comparisons.  No [opp] (a uint64 constant is never negative). *)
let chk_uadd a b = let s = Int64.add a b in
  if Int64.unsigned_compare s a < 0 then None else Some s
let chk_usub a b = if Int64.unsigned_compare a b < 0 then None else Some (Int64.sub a b)
let chk_umul a b =
  if a = 0L then Some 0L
  else let p = Int64.mul a b in if Int64.unsigned_div p a = b then Some p else None
let chk_ushl a n =
  if n < 0L || n >= 64L then None
  else let s = Int64.shift_left a (Int64.to_int n) in
       if Int64.shift_right_logical s (Int64.to_int n) = a then Some s else None
let rec zu_eval e =
  match z_value e with
  | Some v -> Some v
  | None ->
    let is_zop name h = match h with
      | MLglob r -> String.equal (global_basename r) name && path_contains r "BinInt"
      | _ -> false in
    let bin f a b = match zu_eval a, zu_eval b with Some x, Some y -> f x y | _ -> None in
    match e with
    | MLapp (h, [a; b]) when is_zop "add"    h -> bin chk_uadd a b
    | MLapp (h, [a; b]) when is_zop "sub"    h -> bin chk_usub a b
    | MLapp (h, [a; b]) when is_zop "mul"    h -> bin chk_umul a b
    | MLapp (h, [a; b]) when is_zop "shiftl" h -> bin chk_ushl a b
    | MLapp (h, [a; b]) when is_zop "land"   h -> bin (fun x y -> Some (Int64.logand x y)) a b
    | MLapp (h, [a; b]) when is_zop "lor"    h -> bin (fun x y -> Some (Int64.logor  x y)) a b
    | MLapp (h, [a; b]) when is_zop "lxor"   h -> bin (fun x y -> Some (Int64.logxor x y)) a b
    | _ -> None

(* Fold a CONSTANT [FConst] expression ([mkFC num den] / [fc_add]/[fc_sub]/[fc_mul]) to its exact
   rational as an int64 (num, den) pair — [None] if not a closed constant or an intermediate
   overflows int64 (checked arithmetic).  Backs [f64_of_fconst]'s lowering. *)
let rec fc_eval e =
  match e with
  | MLcons (_, r, [num; den]) when named "mkFC" r ->   (* the record CONSTRUCTOR is an MLcons *)
      (* [fc_den] is a [positive] (Q-style, always >= 1), so the denominator is read with
         [pos_value], not [z_eval]; it can never fold to 0 (review #6 P2 #16). *)
      (match z_eval num, pos_value den with Some n, Some d -> Some (n, d) | _ -> None)
  | MLapp (MLglob r, [a; b]) when named "fc_mul" r ->
      (match fc_eval a, fc_eval b with
       | Some (na, da), Some (nb, db) ->
           (match chk_mul na nb, chk_mul da db with Some n, Some d -> Some (n, d) | _ -> None)
       | _ -> None)
  | MLapp (MLglob r, (a :: b :: _)) when named "fc_div" r ->   (* (na/da)/(nb/db) = (na·db)/(da·nb) *)
      (* [fc_div] carries a nonzero-divisor PROOF arg ([hb : fc_num b <> 0]); Coq erases it, but
         match [a :: b :: _] so a residual erased dummy never defeats recognition.  The folded
         value (na·db)/(da·nb) equals the model's sign-normalised (sgn·na·db)/(da·|nb|). *)
      (match fc_eval a, fc_eval b with
       | Some (na, da), Some (nb, db) ->
           (match chk_mul na db, chk_mul da nb with Some n, Some d -> Some (n, d) | _ -> None)
       | _ -> None)
  | MLapp (MLglob r, [a; b]) when named "fc_add" r || named "fc_sub" r ->
      (match fc_eval a, fc_eval b with
       | Some (na, da), Some (nb, db) ->
           let cross = if named "fc_add" r then chk_add else chk_sub in
           (match chk_mul na db, chk_mul nb da, chk_mul da db with
            | Some x, Some y, Some d -> (match cross x y with Some n -> Some (n, d) | None -> None)
            | _ -> None)
       | _ -> None)
  | _ -> None
(* Source-[Z] sign: a [Z0]/[Zpos] is non-negative.  Used to tell a [uint64] literal
   in [2^63, 2^64) (a [Zpos] whose 64-bit pattern looks negative as a signed [Int64])
   apart from a genuine [int64] negative (a [Zneg]). *)
let z_is_nonneg = function
  | MLcons (_, r, [])  when String.equal (global_basename r) "Z0"   -> true
  | MLcons (_, r, [_]) when String.equal (global_basename r) "Zpos" -> true
  | _ -> false

(*s Expression printer. *)

(*s Check whether a given de Bruijn index appears free in an expression.
    [dbn_free n e] is "index [n] does NOT occur free in [e]"; the index SHIFTS at
    every binder (lambda / let / match arm / fix), so an inner binder's own
    index-1 variable (e.g. the [ok] of a [recv_ok]/[type_assert_safe] CPS
    continuation [fun v ok => …]) is not mistaken for the outer index.  The old
    version went under [MLlam] WITHOUT shifting, a false-negative that defeated
    discard-detection on left-nested [>>'] chains (the action was wrongly kept as
    a value [x := Bind(…)]).  [db1_free] = "the bind result (index 1) is unused". *)

let rec dbn_free n = function
  | MLrel k             -> k <> n
  | MLglob _ | MLuint _ | MLfloat _ | MLdummy _ | MLaxiom _ | MLexn _ -> true
  | MLapp (h, args)     -> dbn_free n h && List.for_all (dbn_free n) args
  | MLlam (_, b)        -> dbn_free (n + 1) b
  | MLletin (_, e1, e2) -> dbn_free n e1 && dbn_free (n + 1) e2
  | MLcons (_, _, args) -> List.for_all (dbn_free n) args
  | MLcase (_, scrut, branches) ->
      dbn_free n scrut
      && Array.for_all (fun (ids, _, b) -> dbn_free (n + List.length ids) b) branches
  | MLfix (_, names, bodies) ->
      Array.for_all (dbn_free (n + Array.length names)) bodies
  | MLmagic e           -> dbn_free n e
  | _                   -> true

(* A multiple-return DESTRUCTURE binder, blanked to Go [_] when UNUSED.  A wildcard pattern
   [let '(_, y) := f …] extracts the [_] as a fresh gensym (e.g. [g]); left as a real binder, the Go
   [g, y := f()] would be `g declared and not used` — invalid Go.  [fenv] is the body's de-Bruijn
   context (the binder is physically in it, prepended by [flatten_destructure]), so [idx] finds its
   index [n] and [dbn_free n body] ⇒ unused ⇒ [_].  A [Dummy] binder is [_] outright. *)
let pp_destr_binder fenv body id =
  if is_dummy id then str "_"
  else
    let rec idx i = function
      | [] -> 0
      | x :: r -> if x == id then i else idx (i + 1) r in
    let n = idx 1 fenv in
    if n > 0 && dbn_free n body then str "_" else pp_mlident id

let db1_free e = dbn_free 1 e

(*s CFG block terminators ([Next] values).  A block emitter delegates its
    terminator to one of these, so the same emitter prints raw labels+goto or
    structured continue/break depending on context. *)

let raw_term tab next =
  match next with
  | MLcons (_, c, [n]) when is_jump_ctor c ->
      let lbl = match nat_value n with
        | Some v -> v
        | None -> unsupported "a run_blocks Jump to a non-literal block target (the goto label must be statically known; defaulting to block0 would jump to the WRONG block and silently run a different program)" in
      str tab ++ str (Printf.sprintf "goto block%d" lbl) ++ fnl ()
  | MLcons (_, c, []) when is_done_ctor c -> str tab ++ str "return" ++ fnl ()
  | _ -> unsupported "a run_blocks block terminator that is neither Jump nor Done — an unrecognized Next value would silently become `return`, truncating the block's control flow"

let rec pp_expr state env = function
  | MLrel i ->
      pp_rel env i

  | MLapp (head, args) ->
      let head, all_args = collect_app head args in
      let vis = List.filter (fun a -> not (is_erased a)) all_args in
      (match head, vis with
       (* ret x → x (identity: IO wrapper erases) *)
       | MLglob r, [x] when is_ret_ref r -> pp_expr state env x

       (* gofunc_of f → f  (the [Some]-wrap of a nullable func value erases; review #8) *)
       | MLglob r, [f] when is_gofunc_of_ref r -> pp_expr state env f
       (* gofunc_call f x → f(x)  (Go's native call; a nil func panics on its own with the
          nil-dereference message, matching the modelled [None] → [rt_nil_deref]) *)
       | MLglob r, [f; x] when is_gofunc_call_ref r ->
           pp_expr state env f ++ str "(" ++ pp_expr state env x ++ str ")"

       (* type_assert tag v → v.(T) where T is read from the tag constructor *)
       | MLglob r, [tag; v] when is_type_assert_ref r ->
           let go_type = go_type_of_tag tag in
           let operand =
             match strip_magic v with
             | MLcons (_, r2, _) when is_existT_ref r2 ->
                 (* a FRESH box [any x]: [any_payload] stripped it to its raw (non-interface) payload, so
                    re-materialize as [any(<narrow-converted payload>)] — else [x.(T)] is "not an interface". *)
                 let pv = pp_expr state env (any_payload v) in
                 let conv = (match narrow_conv_of env v with Some gt -> str (gt ^ "(") ++ pv ++ str ")" | None -> pv) in
                 str "any(" ++ conv ++ str ")"
             | _ -> pp_expr state env (any_payload v)
           in
           operand ++ str ".(" ++ str go_type ++ str ")"

       (* panic(val) *)
       | MLglob r, [x] when is_panic_ref r ->
           str "panic(" ++ pp_expr state env (any_payload x) ++ str ")"

       (* slice_of_list tag list → []T{v1, v2, ...}.  Only a STATICALLY-KNOWN element list is
          modeled; an opaque Coq [list] has no faithful Go [[]T] lowering yet, so a non-literal
          spine FAILS LOUD (rule 2) rather than emitting [[]T(nil)], which would silently DISCARD
          the runtime data — exactly the plausible-but-wrong output the backend must never produce. *)
       | MLglob r, [tag; lst] when is_slice_of_list_ref r ->
           let go_elem = go_type_of_tag tag in
           (* narrow element [[]uint8{…}] needs each element cast to the destination (review #4 P1 #4):
              values are int64-carried, so a bare [[]uint8{x & 0xff}] is invalid Go. *)
           let pp_el e = match narrow_go_name go_elem with
             | Some gt -> str (gt ^ "(") ++ pp_expr state env e ++ str ")"
             | None    -> pp_expr state env e in
           (match unfold_list [] lst with
            | Some elems ->
                str ("[]" ^ go_elem ^ "{") ++
                prlist_with_sep (fun () -> str ", ") pp_el elems ++
                str "}"
            | None -> unsupported "slice_of_list of a non-literal list (only a statically-known element list is modeled; an opaque Coq list has no faithful []T lowering yet — emitting []T(nil) would silently discard the runtime data)")
       (* arr_lit tag list → [N]T{v1, …, vN} — a FIXED-SIZE array literal (B4); the
          size N is the list length (read off the construction, NOT the type), so a
          local [a := arr_lit …] has its Go type inferred from this literal. *)
       | MLglob r, [tag; lst] when is_arr_lit_ref r ->
           let go_elem = go_type_of_tag tag in
           let pp_el e = match narrow_go_name go_elem with   (* narrow element cast — review #4 P1 #4 *)
             | Some gt -> str (gt ^ "(") ++ pp_expr state env e ++ str ")"
             | None    -> pp_expr state env e in
           (match unfold_list [] lst with
            | Some elems ->
                str ("[" ^ string_of_int (List.length elems) ^ "]" ^ go_elem ^ "{") ++
                prlist_with_sep (fun () -> str ", ") pp_el elems ++
                str "}"
            | None -> unsupported "arr_lit of a non-literal list (an array's size must be statically known)")
       (* arr<N>_lit tag e1 … eN → [N]T{e1, …, eN} — a fixed-size array literal in a typed position *)
       | MLglob r, (tag :: elems) when is_arrN_lit_ref r ->
           let n = Option.get (arrN_lit_size r) in
           let go_elem = go_type_of_tag tag in
           let pp_el e = match narrow_go_name go_elem with   (* narrow element cast — review #4 P1 #4 *)
             | Some gt -> str (gt ^ "(") ++ pp_expr state env e ++ str ")"
             | None    -> pp_expr state env e in
           str (Printf.sprintf "[%d]%s{" n go_elem) ++
           prlist_with_sep (fun () -> str ", ") pp_el elems ++ str "}"
       (* arr_set n tag a i v → the copy-mutate-return IIFE (value-copy: a is unchanged)
          func(_a [n]T) [n]T { _a[i] = v; return _a }(a) *)
       | MLglob r, (size :: tag :: a :: i :: v :: _) when is_arr_set_ref r ->   (* trailing _ = erased bounds proof *)
           let n = (match nat_value (strip_magic size) with
             | Some n -> n
             | None -> unsupported "arr_set with a non-literal size (the array size must be statically known)") in
           let t = go_type_of_tag (strip_magic tag) in
           let arrty = Printf.sprintf "[%d]%s" n t in
           str ("func(_a " ^ arrty ^ ") " ^ arrty ^ " { _a[") ++ pp_expr state env i ++
           str "] = " ++ pp_typed_lit state env v ++ str "; return _a }(" ++ pp_atom state env a ++ str ")"

       (* slice_get tag xs i → xs[i] — panics if out of bounds *)
       | MLglob r, [_tag; xs; i] when is_slice_get_ref r ->
           pp_expr state env xs ++ str "[" ++ pp_expr state env i ++ str "]"

       (* mutable cells: ref_new tag v → init value (bound as the Go var by the
          enclosing bind); ref_get r → read the var; ref_set r v → r = v.
          int/float literals are typed so the inferred Go var is int64/float64.
          [ref_new] now carries a leading [GoTypeTag] (the cell's element type, for
          the typed-heap model); it is proof-only, dropped at the call site. *)
       | MLglob r, [tag; v] when is_ref_new_ref r -> pp_payload_at_tag state env tag v  (* narrow cell ← int64 carrier needs uint8(…) so the inferred Go var is the model's type, not int64 (P1 #4 narrow-Ref) *)
       | MLglob r, [_tag; rf] when is_ref_get_ref r -> pp_expr state env rf
       | MLglob r, [rf; v] when is_ref_set_ref r ->
           (* a narrow value (int64-carrier) into a narrow Go ref var needs a cast; the value's OWN
              narrow Go type IS the ref's type (both are the source type [A]), so [value_narrow_conv]
              supplies it — [ref_set] carries no tag.  Non-narrow ⇒ [pp_typed_lit] (byte-identical). *)
           let rhs = (match value_narrow_conv env v with
                      | Some gt -> str (gt ^ "(") ++ pp_expr state env v ++ str ")"
                      | None -> pp_typed_lit state env v) in
           pp_expr state env rf ++ str " = " ++ rhs
       (* [ref_as_ptr r] = Go [&x]: the ADDRESS of a local variable [x].  A [Ref] lowers to an
          addressable Go var, so [&x] is a valid [*T] aliasing its cell.  Emit [&] of the var — but
          ONLY when the operand is a bound variable ([MLrel], the provably-addressable case); a [Ref]
          held in any other form (e.g. a function result) is NOT guaranteed addressable, and Go forbids
          [&] of a non-addressable expression, so emit a fail-LOUD [unsupported] there rather than
          invalid Go (rule 2 — never rely on a later [go build] error). *)
       | MLglob r, [rf] when is_ref_as_ptr_ref r ->
           (match strip_magic rf with
            | MLrel _ -> str "&" ++ pp_atom state env rf
            | _ -> unsupported "address-of (&x) whose operand is not a bound local variable (not provably addressable in Go)")

       (* Pointers (Phase B1).  [ptr_new tag v] → a fresh [*T] initialised to [v],
          emitted as the single-expression IIFE [func(_v T) *T { return &_v }(v)] (Go
          forbids [&expr]); the enclosing bind binds it as the Go pointer var.
          [ptr_get tag p] → [*p] (deref read); [ptr_set p v] → [*p = v] (deref write);
          [ptr_nil tag] → [nil]. *)
       | MLglob r, [tag; v] when is_ptr_new_ref r ->
           let t = go_type_of_tag (strip_magic tag) in
           str "func(_v " ++ str t ++ str ") *" ++ str t ++ str " { return &_v }(" ++
           pp_payload_at_tag state env tag v ++ str ")"   (* narrow pointee ← int64 carrier needs [uint8(…)] (P1 #4) *)
       (* go_new tag → new(T): a fresh *T pointing to the zero value *)
       | MLglob r, [tag] when is_go_new_ref r ->
           str "new(" ++ str (go_type_of_tag (strip_magic tag)) ++ str ")"
       | MLglob r, [_tag; p] when is_ptr_get_ref r ->
           str "*" ++ pp_atom state env p
