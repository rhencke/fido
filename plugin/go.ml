(*s Declaration printer. *)

(* review R7: claim a TERM declaration's Go identifier (method → its `RecvType.Method` namespace, which
   is distinct from a package-level function/type of the same bare name).  Aborts on a colliding claim. *)
let register_term_name r =
  let name = go_export (global_basename r) in
  let key =
    if is_method r then
      (match Hashtbl.find_opt method_recvtype (global_path r) with
       | Some rt -> rt ^ "." ^ name | None -> name)
    else name in
  register_emitted_name key (global_path r)

let pp_decl state decl =
  (match decl with
   | Dterm (r, _, _) -> current_decl := global_basename r
   | Dfix (refs, _, _) when Array.length refs > 0 -> current_decl := global_basename refs.(0)
   | Dind mi when Array.length mi.ind_packets > 0 ->
       current_decl := Id.to_string mi.ind_packets.(0).ip_typename
   | Dtype (r, _, _) -> current_decl := global_basename r
   | _ -> ());
  match decl with
  | Dterm (r, _, _) when is_inlined_ref r ->
      mt ()

  (* Record projections are emitted as field access at use sites, not as functions. *)
  | Dterm (r, _, _) when is_record_proj r ->
      mt ()

  (* Suppress top-level definitions whose result type is Proto — protocol
     descriptors are proof-only witnesses with no runtime representation. *)
  | Dterm (_, _, Tglob (rt, _)) when is_proto_type rt ->
      mt ()

  | Dterm (r, body, typ) ->
      register_term_name r;   (* review R7: claim this decl's Go identifier, aborting on a collision *)
      let name = global_basename r in
      (match body with
       | MLlam _ ->
           pp_function state name body typ ++ fnl ()
       | _ ->
           match typ with
           | Tglob (r, []) when is_unit_type r ->
               str "func " ++ str (go_export name) ++ str "() {" ++ fnl () ++
               str "\t" ++ pp_expr state [] body ++ fnl () ++
               str "}" ++ fnl () ++ fnl ()
           | Tglob (r, [inner]) when is_IO_type r ->
               let go_ret = match inner with
                 | Tglob (r2, []) when is_unit_type r2 -> mt ()
                 | t -> str " " ++ pp_type state t
               in
               str "func " ++ str (go_export name) ++ str "()" ++ go_ret ++ str " {" ++ fnl () ++
               pp_io_body state "\t" [] body ++
               str "}" ++ fnl () ++ fnl ()
           | _ ->
               str "var " ++ str (go_export name) ++ str " " ++
               pp_type state typ ++ str " = " ++
               pp_expr state [] body ++ fnl () ++ fnl ())

  | Dfix (refs, bodies, types) ->
      if Array.exists is_inlined_ref refs then mt ()
      else begin
        Array.iter register_term_name refs;   (* review R7: claim each fixpoint's Go identifier *)
        prvecti
          (fun i _ -> pp_function state (global_basename refs.(i)) bodies.(i) types.(i))
          refs
      end

  | Dind mi ->
      (* A record (single-constructor inductive with projections) → a Go struct;
         other inductives are still suppressed (nat/bool are builtins). *)
      (match mi.ind_kind with
       | Record projs when Array.length mi.ind_packets > 0
           && not (is_erased_record_typename (Id.to_string mi.ind_packets.(0).ip_typename))
           (* a proof-only stdlib RECORD type (SpecFloat's [shr_record], fields are [Z]) is
              suppressed — safe to match by name (no user type shares these names). *)
           && not (List.mem (Id.to_string mi.ind_packets.(0).ip_typename) ["shr_record"]) ->
           let pkt = mi.ind_packets.(0) in
           register_emitted_name (go_export (Id.to_string pkt.ip_typename)) (Id.to_string pkt.ip_typename);
           let ftypes = (try pkt.ip_types.(0) with _ -> []) in
           (match Hashtbl.find_opt defined_prim_under (go_export (Id.to_string pkt.ip_typename)) with
            (* DEFINED TYPE over a primitive → [type Name <under>] (NOT a struct; the GoTypeTag
               phantom field is never rendered) *)
            | Some under ->
                str "type " ++ str (go_export (Id.to_string pkt.ip_typename)) ++ str " " ++
                pp_type state under ++ fnl () ++ fnl ()
            | None ->
           let field proj t =
             let fname = match proj with
               | Some g -> go_export (global_basename g) | None -> "F" in
             (* Go struct EMBEDDING (spec "Struct types"): an ANONYMOUS field — written as
                just the type name — promotes the embedded type's fields and methods.  We
                model it as a record field whose exported name EQUALS its (record) type's
                name; emit it anonymous so the Go struct genuinely embeds (and Go promotes
                the embedded method set). *)
             let embedded = match t with
               | Tglob (r, []) ->
                   let bn = global_basename r in
                   String.equal fname (go_export bn) && is_record_typename bn
               | Tglob (r, (Tglob (br, []) :: _)) when is_sptr_type r ->
                   (* embedded POINTER-to-struct: an [SPtr T] field whose exported name is the
                      BASE record's name → Go anonymous [*T] field (promotes T's method set). *)
                   let bn = global_basename br in
                   String.equal fname (go_export bn) && is_record_typename bn
               | _ -> false in
             if embedded then str "\t" ++ pp_type state t ++ fnl ()
             else str "\t" ++ str fname ++ str " " ++ pp_type state t ++ fnl () in
           (* GENERIC STRUCT: the type variables appearing in the field types become the
              struct's type-parameter list [type Box[T1 any, …] struct {…}] (constraint
              [any]).  pp_type already renders [Tvar i] as [T<i>] in the fields and a
              parameterised use [Box[T1]] (the generic Tglob arm), so the decl only needs to
              DECLARE the params. *)
           let tvars = List.fold_left collect_tvars [] ftypes in
           let tparams =
             if tvars = [] then mt ()
             else str "[" ++
                  prlist_with_sep (fun () -> str ", ")
                    (fun i -> str ("T" ^ string_of_int i) ++ str " any") tvars ++
                  str "]" in
           str "type " ++ str (go_export (Id.to_string pkt.ip_typename)) ++ tparams ++
           str " struct {" ++ fnl () ++
           prlist (fun x -> x) (List.map2 field projs ftypes) ++
           str "}" ++ fnl () ++ fnl ())
       (* ENUM (nullary-ctor inductive) → [type T int] + an iota const block. *)
       | _ when Array.length mi.ind_packets > 0
             && is_enum_typename (Id.to_string mi.ind_packets.(0).ip_typename) ->
           let pkt = mi.ind_packets.(0) in
           let tn = go_export (Id.to_string pkt.ip_typename) in
           let ctors = Array.to_list pkt.ip_consnames in
           register_emitted_name tn (Id.to_string pkt.ip_typename);   (* review R7: enum type name *)
           List.iter (fun c -> register_emitted_name (go_export (Id.to_string c)) (Id.to_string c)) ctors;  (* + iota consts *)
           str "type " ++ str tn ++ str " int" ++ fnl () ++ fnl () ++
           str "const (" ++ fnl () ++
           (match ctors with
            | [] -> mt ()
            | c0 :: rest ->
                str "\t" ++ str (go_export (Id.to_string c0)) ++ str " " ++ str tn ++ str " = iota" ++ fnl () ++
                prlist (fun c -> str "\t" ++ str (go_export (Id.to_string c)) ++ fnl ()) rest) ++
           str ")" ++ fnl () ++ fnl ()
       | _ -> mt ())

  | Dtype (r, _, _) when is_uint63_type r || is_go_prim_type r || is_float64_type r
    || is_IO_type r || is_go_map_type r || is_go_chan_type r
    || is_ref_type r
    || String.equal (global_basename r) "Sess"
    || String.equal (global_basename r) "GoString"
    || String.equal (global_basename r) "GoSlice"
    (* canonical-rep typeclasses (review #6 #10(b)): a single-field class unboxes to a type ALIAS
       [StructRep<n>Of R = StructRep<n> R]; proof-only, suppress (the rep never reaches Go) *)
    || String.equal (global_basename r) "StructRep2Of"
    || String.equal (global_basename r) "StructRep3Of"
    || String.equal (global_basename r) "StructRep2HOf"
    || List.mem (global_basename r) ["RefCell"; "RefHeap"; "ChanCell"; "ChanHeap";
                                      "MapCell"; "MapHeap"; "Tagged"; "GoTypeTag"] -> mt ()
  | Dtype (_, _, Tglob (r, _)) when is_sigT_ref r    -> mt ()

  | Dtype (r, _, typ) ->
      (* review #4 P1 6: a type ALIAS bypassed the collision registry — claim its Go
         identifier so an alias/term or alias/alias clash aborts (vs a silent Go redeclaration). *)
      register_emitted_name (go_export (global_basename r)) (global_path r);
      str "type " ++ str (go_export (global_basename r)) ++
      str " = " ++ pp_type state typ ++ fnl ()

