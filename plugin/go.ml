(*s Identifier utilities. *)

(** Sanitise a Rocq id for Go: replace prime ['] with underscore. *)
let go_safe s = String.map (function '\'' -> '_' | c -> c) s

let pp_goid id = str (go_safe (Id.to_string id))

(** Capitalise the first character (export in Go). *)
let go_export s =
  if String.length s = 0 then s
  else
    let b = Bytes.of_string s in
    Bytes.set b 0 (Char.uppercase_ascii (Bytes.get b 0));
    Bytes.to_string b

(*s Global-reference helpers. *)

let global_path r =
  try
    DirPath.to_string (Nametab.dirpath_of_global r.glob) ^ "." ^
    Id.to_string (Nametab.basename_of_global r.glob)
  with Not_found -> ""

let global_basename r =
  try Id.to_string (Nametab.basename_of_global r.glob)
  with Not_found -> ""

let ref_has_suffix r suffix =
  let p = global_path r in
  let n = String.length p and m = String.length suffix in
  n >= m && String.equal suffix (String.sub p (n - m) m)

