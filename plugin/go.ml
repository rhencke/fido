(* The first visible (non-erased) parameter's [ml_type], if any — mirrors
   [pp_function]'s [zip_params] for the first pair (skip dummies, advance types). *)
let first_param_type body typ =
  let ids, _ = collect_lam body in
  let param_types, _ = collect_tarrs typ in
  let rec go ids types =
    match ids, types with
    | [], _ -> None
    | id :: rest, _ when is_dummy id ->
        go rest (match types with _ :: t -> t | [] -> [])
    | _ :: _, t :: _ -> Some t
    | _ :: _, [] -> None
  in
  go ids param_types

(* Gather, BEFORE emitting any decl, two things every use site depends on:
   (1) records — projections (→ field names), constructors, and type names, so a
       projection lowers to [x.Field] and a constructor to [T{…}];
   (2) methods — any function whose first visible param is a (already-registered)
       record, so calls lower to [recv.M(rest)].  Records must be registered first
       (pass 1) because the method test consults [record_typenames] (pass 2). *)
let collect_decls struc =
  let decls = ref [] in
  List.iter (fun (_, sel) ->
    List.iter (function (_, SEdecl d) -> decls := d :: !decls | _ -> ()) sel) struc;
  let decls = List.rev !decls in
  (* pass 1 — records *)
  List.iter (function
    | Dind mi ->
        (match mi.ind_kind with
         | Record projs when Array.length mi.ind_packets > 0
             && not (is_erased_record_typename (Id.to_string mi.ind_packets.(0).ip_typename)) ->
             let pkt = mi.ind_packets.(0) in
             (* review R7: two records sharing a typename would silently OVERWRITE each other's
                metadata (and emit a duplicate `type Name struct`) — catch it at extraction. *)
             if Hashtbl.mem record_typenames (Id.to_string pkt.ip_typename) then
               unsupported (Printf.sprintf "two record types both named `%s` — their `type %s struct` decls and field/ctor metadata would collide; rename one in the source"
                              (Id.to_string pkt.ip_typename) (go_export (Id.to_string pkt.ip_typename)));
             Hashtbl.replace record_typenames (Id.to_string pkt.ip_typename) ();
             List.iter (function
               | Some g -> Hashtbl.replace record_proj_field (global_path g) (global_basename g)
               | None -> ()) projs;
             if Array.length pkt.ip_consnames > 0 then begin
               if Hashtbl.mem record_ctor_names (Id.to_string pkt.ip_consnames.(0)) then
                 unsupported (Printf.sprintf "two record constructors both named `%s` — their ctor/field metadata would collide; rename one in the source"
                                (Id.to_string pkt.ip_consnames.(0)));
               Hashtbl.replace record_ctor_names (Id.to_string pkt.ip_consnames.(0)) ();
               let ftypes = (try pkt.ip_types.(0) with _ -> []) in
               Hashtbl.replace record_ctor_ftypes (Id.to_string pkt.ip_consnames.(0)) ftypes ;
               (* ordered Go field names, for keyed struct literals — from the projections *)
               let fields = List.filter_map
                 (function Some g -> Some (go_export (global_basename g)) | None -> None) projs in
               (* review #4 P1 6: two projections of THIS struct exporting to the SAME Go field name
                  (e.g. Coq `x'` and `x_` both mangle to `X_`) would emit a struct with a duplicate
                  field — invalid Go.  Each struct is its own field namespace; abort on a clash. *)
               let rec find_field_dup seen = function
                 | [] -> None
                 | f :: tl -> if List.mem f seen then Some f else find_field_dup (f :: seen) tl in
               (match find_field_dup [] fields with
                | Some f -> unsupported (Printf.sprintf "two fields of record `%s` export to the SAME Go field name `%s` (distinct Coq projections differing only by mangling, e.g. `'` vs `_`) — the emitted struct would have a duplicate field; rename one in the source"
                                           (Id.to_string pkt.ip_typename) f)
                | None -> ());
               Hashtbl.replace record_ctor_fields (Id.to_string pkt.ip_consnames.(0)) fields ;
               (* defined-primitive-type? (2 fields, 2nd a GoTypeTag phantom) — record its
                  underlying type for the special [type T <under>] emission + the value cast *)
               (match deftype_under_of_ftypes ftypes with
                | Some vt ->
                    Hashtbl.replace defined_prim_under (go_export (Id.to_string pkt.ip_typename)) vt ;
                    (match projs with
                     | Some g :: _ -> Hashtbl.replace defined_prim_proj (global_path g) vt
                     | _ -> ())
                | None -> ())
             end
         (* ENUM: a non-record inductive whose constructors are ALL NULLARY (≥2 of them) →
            Go [type T int] + iota consts.  Excludes [bool] (a builtin, handled specially);
            nat/list/option are excluded automatically (they have non-nullary constructors). *)
         | _ when Array.length mi.ind_packets = 1 ->
             let pkt = mi.ind_packets.(0) in
             let ncons = Array.length pkt.ip_consnames in
             let all_nullary =
               ncons >= 2 &&
               Array.for_all (fun ts -> ts = []) pkt.ip_types in
             let is_bool =
               ncons >= 1 &&
               (let c0 = pkt.ip_consnames.(0) in
                let s = Id.to_string c0 in String.equal s "true" || String.equal s "false") in
             if all_nullary && not is_bool then begin
               if Hashtbl.mem enum_typenames (Id.to_string pkt.ip_typename) then
                 unsupported (Printf.sprintf "two enum types both named `%s` — their `type %s int` + iota const blocks would collide; rename one in the source"
                                (Id.to_string pkt.ip_typename) (go_export (Id.to_string pkt.ip_typename)));
               Hashtbl.replace enum_typenames (Id.to_string pkt.ip_typename) () ;
               Array.iter (fun c -> Hashtbl.replace enum_ctors (Id.to_string c) ())
                 pkt.ip_consnames
             end
         | _ -> ())
    | _ -> ()) decls;
  (* pass 1b — embedded fields: a projection whose exported name equals its (record) field
     type's name is a Go EMBEDDED (anonymous) field; record it so member access through it
     PROMOTES.  Runs after pass 1 so the field type is already a registered record. *)
  List.iter (function
    | Dind mi ->
        (match mi.ind_kind with
         | Record projs when Array.length mi.ind_packets > 0
             && not (is_erased_record_typename (Id.to_string mi.ind_packets.(0).ip_typename)) ->
             let pkt = mi.ind_packets.(0) in
             let ftypes = (try pkt.ip_types.(0) with _ -> []) in
             (try
                List.iter2 (fun proj t ->
                  match proj, t with
                  | Some g, Tglob (r, []) ->
                      let bn = global_basename r in
                      if String.equal (go_export (global_basename g)) (go_export bn)
                         && is_record_typename bn then
                        Hashtbl.replace embedded_proj (global_path g) ()
                  | Some g, Tglob (r, (Tglob (br, []) :: _)) when is_sptr_type r ->
                      (* embedded [*T] (SPtr base): promote through the anonymous pointer field *)
                      let bn = global_basename br in
                      if String.equal (go_export (global_basename g)) (go_export bn)
                         && is_record_typename bn then
                        Hashtbl.replace embedded_proj (global_path g) ()
                  | _ -> ()) projs ftypes
              with Invalid_argument _ -> ())
         | _ -> ())
    | _ -> ()) decls;
  (* pass 2 — methods (first visible param is a record; projections excluded) *)
  let register_method r body typ =
    match first_param_type body typ with
    | Some t when (is_record_tglob t || is_sptr_record_tglob t)
                  && not (is_record_proj r) && not (is_inlined_ref r) ->
        Hashtbl.replace method_paths (global_path r) () ;
        (* visible param count (incl. receiver) — non-dummy lambda binders — so a call
           site applying fewer than this many args is a method VALUE, not a full call. *)
        let ids, _ = collect_lam body in
        let arity = List.length (List.filter (fun i -> not (is_dummy i)) ids) in
        Hashtbl.replace method_arity (global_path r) arity ;
        (* receiver Go type name, for a method EXPRESSION used as a bare value: value-receiver
           (record) -> T.M (Point.Sum_coords); pointer-receiver (SPtr R = pointer to R) -> the
           PARENTHESIZED pointer form, Go's syntax for a pointer-receiver method expression
           (e.g. Cell_incx -> the parenthesized-star-Cell dot form).  The receiver type is the
           FIRST type arg of the SPtr R / SPtr3 R / SPtrH R A B glob. *)
        (match t with
         | Tglob (rt, _) when is_record_tglob t ->
             Hashtbl.replace method_recvtype (global_path r) (go_export (global_basename rt))
         | Tglob (_, (Tglob (rt, _) :: _)) when is_sptr_record_tglob t ->
             Hashtbl.replace method_recvtype (global_path r) ("(*" ^ go_export (global_basename rt) ^ ")")
         | _ -> ())
    | _ -> ()
  in
  List.iter (function
    | Dterm (r, body, typ) -> register_method r body typ ;
        Hashtbl.replace func_param_types (global_path r) (fst (collect_tarrs typ))
    | Dfix (refs, bodies, types) ->
        Array.iteri (fun i r -> register_method r bodies.(i) types.(i) ;
          Hashtbl.replace func_param_types (global_path r) (fst (collect_tarrs types.(i)))) refs
    | _ -> ()) decls ;
  (* pass 3 — comparable-constraint functions: record which visible param indices are
     [ComparableW] equality witnesses, so they are DROPPED at the decl and at every call site. *)
  let register_comparable r typ =
    let param_types, ret = collect_tarrs typ in
    (* a value (no params) of type [ComparableW K] is a witness INSTANCE → suppress its decl *)
    if param_types = [] && is_comparablew_type ret then
      Hashtbl.replace comparable_inst (global_path r) () ;
    let idxs =
      List.mapi (fun i t -> (i, t)) param_types
      |> List.filter (fun (_, t) -> is_comparablew_type t)
      |> List.map fst in
    if idxs <> [] then Hashtbl.replace comparable_witness (global_path r) idxs
  in
  List.iter (function
    | Dterm (r, _, typ) -> register_comparable r typ
    | Dfix (refs, _, types) -> Array.iteri (fun i r -> register_comparable r types.(i)) refs
    | _ -> ()) decls

let pp_struct state struc =
  Hashtbl.reset emitted_names;   (* review R7: per-extraction identifier-collision registry *)
  collect_decls struc;
  let pp_mod (mp, sel) =
    State.with_visibility state mp [] (fun state ->
      prlist
        (fun (_, elem) ->
           match elem with
           | SEdecl d      -> pp_decl state d
           | SEmodule _
           | SEmodtype _   -> mt ())
        sel)
  in
  prlist pp_mod struc

