          [int] destination is invalid Go; [int(x)] widens it and is idempotent on an int-carried
          operand. *)
       | MLglob r, [x] when is_int_of_fw r ->
           str "int(" ++ pp_expr state env x ++ str ")"
       (* [f64_of_int i] / [f64_of_i64 a] — int / int64 → float64: Go's native cast. *)
       | MLglob r, [x] when is_int_to_f64_ref r ->
           str "float64(" ++ pp_expr state env x ++ str ")"
       (* [f32_of_int]/[f32_of_i64]/[f32_of_u64] — DIRECT int → float32: Go's [float32(x)] cast
          (rounds the integer once, unlike the double-rounding [float32(float64(x))]). *)
       | MLglob r, [x] when is_int_to_f32_ref r ->
           str "float32(" ++ pp_expr state env x ++ str ")"
       (* [f64_of_fconst c] — an untyped FLOAT CONSTANT: fold the exact rational [num/den] and emit
          [float64(num.0 / den.0)].  Go computes [num.0 / den.0] as an UNTYPED constant (arbitrary
          precision), then [float64(…)] rounds ONCE — the correctly-rounded rational→binary64, for ALL
          num/den (no 2^53 guard: the untyped intermediate is exact, vs the old [float64(num)/float64
          (den)] which double-rounds when both endpoints exceed 2^53). *)
       | MLglob r, [fc] when named "f64_of_fconst" r ->
           (match fc_eval fc with
            | Some (num, den) when den <> 0L ->
                str (Printf.sprintf "float64(%Ld.0 / %Ld.0)" num den)
            (* den=0 is now UNREACHABLE: [fc_den] is a [positive] and [fc_div] is nonzero-divisor
               gated (review #6 P2 #16), so a 0 denominator cannot be constructed — this stays as
               a defensive boundary guard (closed-world tenet), not a reachable fail-loud path. *)
            | Some _ -> unsupported "f64_of_fconst: den = 0 (a float64 constant cannot be ±Inf)"
            | None -> unsupported "f64_of_fconst of a non-constant FConst (only statically-known float constants are modeled)")
       (* [f32_of_fconst c] — exact FConst → float32: fold the rational and emit [float32(num.0 / den.0)].
          Go computes [num.0 / den.0] as an UNTYPED constant (arbitrary precision), then [float32(…)]
          rounds ONCE — the correctly-rounded rational→binary32, for ALL num/den (no 2^53 guard, unlike
          [f64_of_fconst]: the binary64 intermediate is what forces that guard; the untyped one is exact). *)
       | MLglob r, [fc] when named "f32_of_fconst" r ->
           (match fc_eval fc with
            | Some (num, den) when den <> 0L ->
                str (Printf.sprintf "float32(%Ld.0 / %Ld.0)" num den)
            | Some _ -> unsupported "f32_of_fconst: den = 0 (a float32 constant cannot be ±Inf)"
            | None -> unsupported "f32_of_fconst of a non-constant FConst (only statically-known float constants are modeled)")
       (* [i64_of_f64 f] → [int64(f)] (float64 → int64 truncation toward zero) *)
       | MLglob r, [x] when is_f64_to_i64_ref r ->
           str "int64(" ++ pp_expr state env x ++ str ")"
       (* [u64_of_f64 f] → [uint64(f)] (float64 → uint64 truncation toward zero) *)
       | MLglob r, [x] when is_f64_to_u64_ref r ->
           str "uint64(" ++ pp_expr state env x ++ str ")"
       (* narrow → int64 widening → [int64(x)] (Go's widening conversion).  NOT identity: review
          #4 P1 #4 — a narrow operand at a typed boundary (a [uint8]/[int8]/… PARAM, or a value cast
          to its narrow Go type) is a REAL narrow Go type, so a bare [x] against an [int64]
          destination is invalid Go (e.g. [func Widen(x uint8) int64 { return x }]).  [int64(x)]
          widens it (sign/zero-extending per the narrow type) and is harmlessly idempotent when [x]
          is already an int64-carried expression — so it is correct in EVERY position. *)
       | MLglob r, [x] when is_i64_of_narrow_ref r ->
           str "int64(" ++ pp_expr state env x ++ str ")"
       (* float64 → float32 narrowing (round-nearest-even) → [float32(x)].  A CONSTANT that
          overflows float32 is a Go compile error, so force runtime (the float64 operand fits). *)
       | MLglob r, [x] when is_f64_to_f32_ref r ->
           if operand_is_runtime x then str "float32(" ++ pp_expr state env x ++ str ")"
           else str "func(x float64) float32 { return float32(x) }(" ++ pp_expr state env x ++ str ")"
       | MLglob r, [a; b]
         when fw_is r "add" || fw_is r "sub" || fw_is r "mul" ->
           let (s, w, op) = Option.get (fixed_width_op r) in
           if String.equal op "mul" && w >= 63 then
             unsupported (string_of_int w ^ "-bit multiply: a >=63-bit-wide product exceeds the 63-bit carrier; needs the Z-based wide-int model")
           else
             let opstr = (match op with "add" -> " + " | "sub" -> " - " | _ -> " * ") in
             (* P0 #2: widen each operand to the int CARRIER before the masked op.  An operand may be a
                NARROW-typed value (a uint8/int8 param, field, or call-result), and `int8 & 0xff` overflows
                int8 — computing in int (then masking) is correct for any operand (a constant/int carrier is
                unchanged by [int(…)], a narrow-typed one is widened).  This is what lets a narrow value flow
                through arithmetic from ANY position, not just a literal. *)
             fw_wrap s w
               (str "(int(" ++ pp_expr state env a ++ str ")" ++ str opstr ++ str "int(" ++ pp_expr state env b ++ str "))")

       (* fixed-width shifts.  [shl]: truncate to the width ([fw_wrap]) — uintN
          wraps mod 2^N, intN sign-extends.  [shr]: bare Go [x >> k] — the int64
          carrier is non-negative (uintN ⇒ logical shift) or sign-extended (intN ⇒
          arithmetic shift), both correct without a mask.  The count [k] is a
          plain [int]; the non-negativity proof was erased.  Over-width counts
          give 0 / sign-fill, matching Go (no upper limit). *)
       | MLglob r, [x; k] when fw_is r "shl" ->
           let (s, w, _) = Option.get (fixed_width_op r) in
           (* P0 #2: widen the shifted value to the int carrier (a narrow-typed operand would overflow the
              mask); the shift COUNT [k] is a plain int already. *)
           fw_wrap s w
             (str "(int(" ++ pp_expr state env x ++ str ") << " ++ pp_expr state env k ++ str ")")
       | MLglob r, [x; k] when fw_is r "shr" ->
           str "(" ++ pp_expr state env x ++ str " >> " ++ pp_expr state env k ++ str ")"

       (* fixed-width division / remainder (divisor-non-zero proof erased).
          [uintN]: non-negative carrier ⇒ Go int64 [/]/[%] is the unsigned result,
          in range, no mask.  [intN]: signed [/]/[%] wrapped to the width ([fw_wrap])
          — needed for the [-2^(N-1) / -1] two's-complement overflow. *)
       | MLglob r, [a; b] when fw_is r "div" || fw_is r "mod" ->
           let (s, w, op) = Option.get (fixed_width_op r) in
           let opstr = if String.equal op "div" then " / " else " % " in
           if s then
             (* signed [intN]: wrap to the width (the -2^(N-1)/-1 two's-complement overflow), widening each
                operand to the int carrier first (a narrow-typed int8 would overflow the mask). *)
             fw_wrap s w (str "(int(" ++ pp_expr state env a ++ str ")" ++ str opstr ++ str "int(" ++ pp_expr state env b ++ str "))")
           else
             (* unsigned [uintN]: non-negative carrier ⇒ Go's /,% is the in-range unsigned result, no mask. *)
             str "(" ++ pp_expr state env a ++ str opstr ++ pp_expr state env b ++ str ")"

       (* Nat.sub is truncated monus, NOT Go uint's wrapping [-] — fail loud
          rather than emit a value that is wrong on underflow (3 - 5 ≠ 2^64-2). *)
       | MLglob r, [_; _] when is_nat_sub_ref r ->
           unsupported "Nat.sub: Coq's truncated subtraction (3 - 5 = 0) does not match Go uint's wrapping minus (3 - 5 = 2^64-2); use int (Sint63) subtraction, or a b<=a-guarded monus"

       (* PrimInt63.ltb/leb are UNSIGNED comparisons; Go int64 </<= are SIGNED and
          disagree on high-bit values — fail loud rather than emit a wrong [<]. *)
       | MLglob r, [_; _] when is_int63_op_ref r "ltb" || is_int63_op_ref r "leb" ->
           unsupported "PrimInt63.ltb/leb (UNSIGNED comparison): Go int64 </<= are SIGNED and disagree once the high bit is set; use Sint63.ltb/leb (signed) for int"

       (* [i64_lit z] — a full-width int64 constant: fold its [Z] literal to the
          decimal int64 (the proof arg was erased).  The smart constructor demanded
          [in_i64 z], so the value fits int64. *)
       | MLglob r, [z] when is_i64_lit r ->
           (match z_eval z with
            (* TYPE the literal [int64(N)] not bare [N]: a bare decimal makes Go infer
               [int] for a binding ([x := 9]) or box ([any 9]) — diverging from the
               [TI64] tag (int64) and breaking [.(int64)] (review R10 differential). *)
            | Some v -> str ("int64(" ^ Int64.to_string v ^ ")")
            | None   -> unsupported "i64_lit of a non-constant Z (only statically-known int64 constant expressions are modeled)")
       (* [u64_lit z] — a full-width uint64 constant: fold its [Z] literal to the
          UNSIGNED decimal ([Printf.sprintf "%Lu"] handles values in [2^63, 2^64)
          that are "negative" in OCaml's signed [Int64.t] — they print as the
          correct positive decimal). *)
       | MLglob r, [z] when is_u64_lit r ->
           (match zu_eval z with
            (* TYPE the literal [uint64(N)] (same int-vs-uint64 boxing fix as i64_lit). *)
            | Some v -> str ("uint64(" ^ Printf.sprintf "%Lu" v ^ ")")
            | None   -> unsupported "u64_lit of a non-constant Z (only statically-known uint64 constant expressions are modeled)")
       (* [int_lit z] — a platform-int (GoInt) constant: fold its [Z] literal to a BARE signed
          decimal.  Go's untyped-constant default IS [int], so a bare [N] is correctly [int]-typed in
          an EXPRESSION/index position ([xs[5]], [a + 5]); a TYPED position pins it via [pp_typed_lit]
          -> [int(N)] (the OLD [PrimInt63]-literal position-dependence; review #6 #13). *)
       | MLglob r, [z] when is_int_lit r ->
           (match z_eval z with
            | Some v -> str (Int64.to_string v)
            | None   -> unsupported "int_lit of a non-constant Z (only statically-known platform-int constant expressions are modeled)")
       (* Inlined binary operator (bool / float / nat / int63 / signed int63),
          printed with Go operator precedence.  At top level there is no
          surrounding operator, so the operands are printed at this op's level
          (left) and one tighter (right, for left-associativity); [pp_prec] adds
          parens only where genuinely needed. *)
       | MLglob r, [a; b] when Option.has_some (binop_of r) ->
           let (p, opstr) = Option.get (binop_of r) in
           (match arith_force_go_type r with
            | Some ty when not (operand_is_runtime a || operand_is_runtime b) ->
                (* all-constant float arith would constant-fold under Go's (non-IEEE) constant
                   rules — force runtime via a typed IIFE *)
                str (Printf.sprintf "func(x %s, y %s) %s { return x%sy }(" ty ty ty opstr)
                ++ pp_expr state env a ++ str ", " ++ pp_expr state env b ++ str ")"
            | _ ->
                pp_prec state env p a ++ str opstr ++ pp_prec state env (p + 1) b)
       (* native whole-struct equality [struct_eqb eqb a b] → [a == b]; the comparability
          witness [eqb] is dropped (it discharged the side condition).  Comparison level 3. *)
       | MLglob r, [_eqb; a; b] when is_struct_eqb_ref r ->
           pp_prec state env 3 a ++ str " == " ++ pp_prec state env 4 b
       (* complex-number builtins → Go's predeclared [complex]/[real]/[imag] *)
       | MLglob r, [re; im] when is_go_complex_ref r ->
           str "complex(" ++ pp_expr state env re ++ str ", " ++ pp_expr state env im ++ str ")"
       | MLglob r, [c] when is_go_real_ref r ->
           str "real(" ++ pp_expr state env c ++ str ")"
       | MLglob r, [c] when is_go_imag_ref r ->
           str "imag(" ++ pp_expr state env c ++ str ")"
       (* string <-> byte-slice conversions → Go's [[]byte(s)] / [string(b)] *)
       | MLglob r, [s] when named "str_to_bytes" r ->
           str "[]byte(" ++ pp_expr state env s ++ str ")"
       | MLglob r, [b] when named "str_from_bytes" r ->
           str "string(" ++ pp_expr state env b ++ str ")"
       (* string <-> rune-slice conversions → Go's [[]rune(s)] / [string(rs)] (UTF-8) *)
       | MLglob r, [s] when named "str_to_runes" r ->
           str "[]rune(" ++ pp_expr state env s ++ str ")"
       | MLglob r, [rs] when named "runes_to_str" r ->
           str "string(" ++ pp_expr state env rs ++ str ")"
       (* single rune → string: [string(rune(r))] (explicit rune cast, not [string(int)]) *)
       | MLglob r, [c] when named "rune_to_str" r ->
           str "string(rune(" ++ pp_expr state env c ++ str "))"
       (* comparable-witness equality [cw_eqb w a b] → native Go [a == b] (the witness [w], an
          erased dictionary, is dropped; Go's [comparable] supplies [==] structurally). *)
       | MLglob r, [_; a; b] when is_cw_eqb_ref r ->
           pp_atom state env a ++ str " == " ++ pp_atom state env b
       (* call to a comparable-constraint function: drop the erased [ComparableW] witness args. *)
       | MLglob r, args when Hashtbl.mem comparable_witness (global_path r) ->
           let drop = Hashtbl.find comparable_witness (global_path r) in
           let kept = List.filteri (fun i _ -> not (List.mem i drop)) args in
           str (go_export (global_basename r)) ++ str "(" ++
           prlist_with_sep (fun () -> str ", ") (pp_expr state env) kept ++ str ")"
       (* method call [m recv a1 … an] → [recv.M(a1, …, an)] (value receiver).
          The first visible arg is the receiver, pulled out before the dot. *)
       | MLglob r, (recv :: rest) when is_method r ->
           let dot = pp_atom state env (peel_embedded recv) ++ str "." ++ str (go_export (global_basename r)) in
           let applied = 1 + List.length rest in
           let arity = match Hashtbl.find_opt method_arity (global_path r) with
                       | Some a -> a | None -> applied in
           (* under-applied (only the receiver, not the full args) → a Go METHOD VALUE
              [recv.M] (a first-class closure with the receiver bound); else a full call. *)
           if applied < arity then dot
           else
             (* review #4 P1 #4: a narrow PARAM of a METHOD needs the destination cast too.  The
                method's registered param types are [receiver :: param-types]; the receiver is a
                struct/named type (never a sub-64 narrow), so the non-receiver params are the TAIL,
                aligned 1:1 with [rest] when no erased args (else decline → bare, as before). *)
             let ptl = match Hashtbl.find_opt func_param_types (global_path r) with
               | Some (_ :: tl) when List.length tl = List.length rest -> tl
               | _ -> [] in
             let pp_arg i a =
               let gt = match Option.bind (List.nth_opt ptl i) narrow_dest_conv with
                 | Some _ as g -> g | None -> value_narrow_conv env a in
               match gt with
               | Some gt -> str (gt ^ "(") ++ pp_expr state env a ++ str ")"
               | None    -> pp_expr state env a in
             dot ++ str "(" ++
             prlist_with_sep (fun () -> str ", ") (fun x -> x) (List.mapi pp_arg rest) ++ str ")"
       | _ ->
           (* review #4 P1 #4: a wide value into a NARROW PARAM of a user function needs the
              destination cast ([f(uint8(x))]).  Use the callee's param types when they ALIGN 1:1 with
              the visible args (no erased tag/witness/type args) — else bare [pp_expr] (prior behaviour). *)
           let pts = match head with
             | MLglob r -> (match Hashtbl.find_opt func_param_types (global_path r) with
                            | Some pts when List.length pts = List.length vis -> pts
                            | _ -> [])
             | _ -> [] in
           (* declared-narrow param → cast; else the VALUE's own narrow type (generic type-param
              position, where Go would otherwise infer the wrong type argument; also the erased-arg
              fallback — a raw narrow value is cast regardless of alignment, an any-box is not). *)
           let pp_arg i a =
             let gt = match Option.bind (List.nth_opt pts i) narrow_dest_conv with
               | Some _ as g -> g | None -> value_narrow_conv env a in
             match gt with
             | Some gt -> str (gt ^ "(") ++ pp_expr state env a ++ str ")"
             | None    -> pp_expr state env a in
           pp_atom state env head ++ str "(" ++
           prlist_with_sep (fun () -> str ", ")
             (fun x -> x) (List.mapi pp_arg vis) ++
           str ")")

  | MLlam _ as e ->
      let ids, body = collect_lam e in
      let vis = List.filter (fun id -> not (is_dummy id)) ids in
      let new_env = List.rev ids @ env in
      str "func(" ++
      prlist_with_sep (fun () -> str ", ")
        (fun id -> pp_mlident id ++ str " any") vis ++
      str ") any {" ++ fnl () ++
      str "\treturn " ++ pp_expr state new_env body ++ fnl () ++
      str "}"

  | MLglob r ->
      if is_bool_true r     then str "true"
      else if is_bool_false r then str "false"
      (* review R4(a): a BARE (unapplied) map constructor has no key/value tags to render a typed
         map, and `make(map[any]any)` is the wrong Go type (not the model's typed map[K]V) — fail loud. *)
      else if is_map_make_ref r || is_map_make_typed_ref r then
        unsupported "a bare (unapplied) map constructor — `make(map[any]any)` loses the key/value types; apply `map_make_typed <keytag> <valtag>` so the typed `map[K]V` can be emitted"
      (* a method used as a BARE value (no application) is Go's method EXPRESSION [T.M]
         (a [func(T, …) …] whose first arg is the receiver) — emit [RecvType.Method]. *)
      else (match (if is_method r then Hashtbl.find_opt method_recvtype (global_path r) else None) with
            | Some recvty -> str recvty ++ str "." ++ str (go_export (global_basename r))
            | None -> str (go_export (global_basename r)))

  | MLfix (i, names, _) ->
      str (go_export (go_safe (Id.to_string names.(i))))

  | MLletin (_id, e1, e2) ->
      (* review R3: a value-position let must NOT become `(func() any { x := e1; return e2 })()` — the
         `any` return is a non-compiling type in a typed context (e.g. inside int64 arithmetic, where the
         old form gave `int64(any)`).  An expression-position let is PURE (referentially transparent), so
         INLINE it via [ast_subst] (`e2[Rel 1 := e1]`); the surrounding context then types the result
         correctly.  No IIFE, no `any`.  (Duplicates e1 if x is used >1×, but it is pure, so sound.) *)
      pp_expr state env (ast_subst e1 e2)

  (* [MkVariadic xs _] (an inlined [vararg xs]) in a call-arg position → Go's variadic args
     (multi-value for a slice literal, else [xs...]).  Checked BEFORE the generic record-ctor
     arm (which would emit a struct literal). *)
  | MLcons (_, r, (xs :: _)) when is_vararg_ref r ->
      (match xs with
       | MLapp (MLglob r2, [_; lst]) when is_slice_of_list_ref r2
             && Option.has_some (unfold_list [] lst) ->
           prlist_with_sep (fun () -> str ", ") (pp_expr state env)
             (Option.get (unfold_list [] lst))
       | _ -> pp_expr state env xs ++ str "...")

  (* DEFINED-TYPE ctor [MkMyT v _tag] → the cast [MyT(v)] (the value field cast to the named
     defined type; the GoTypeTag phantom arg is dropped).  Before the struct-literal arm. *)
  | MLcons (_, r, (v :: _)) when is_record_ctor r
        && Hashtbl.mem defined_prim_under (record_ctor_tyname r) ->
      str (record_ctor_tyname r) ++ str "(" ++ pp_expr state env v ++ str ")"

  (* record constructor [MkT a1 … an] → Go struct literal [T{a1, …, an}].  A
     field whose type is a function (an interface METHOD dictionary entry) and
     whose argument is a lambda is emitted as a TYPED Go closure, so the literal
     matches the field type [func(A) R] rather than the generic [func(any) any].
     This is what makes the dictionary's method entries well-typed. *)
  | MLcons (cty, r, args) when is_record_ctor r ->
      let ftypes = (try Hashtbl.find record_ctor_ftypes (global_basename r) with Not_found -> []) in
      let fields = (try Hashtbl.find record_ctor_fields (global_basename r) with Not_found -> []) in
      let rec pp_vals ts args = match ts, args with
        | _, [] -> []
        | (Tarr _ as t) :: ts', (MLlam _ as a) :: args' ->
            pp_typed_closure state env t a :: pp_vals ts' args'
        (* narrow field [V uint8 := <int64-carried expr>] needs the destination cast [uint8(…)]
           (review #4 P1 #4) — else [T{V: x & 0xff}] is invalid Go.  Cast off the DECLARED field type,
           else the VALUE's own narrow type (a generic field [Box[T]{V}] at a narrow instance, where
           the declared type is a type variable).  Idempotent on a value already of that narrow type. *)
        | t :: ts', a :: args' ->
            let gt = match narrow_dest_conv t with Some _ as g -> g | None -> value_narrow_conv env a in
            (match gt with
             | Some gt -> str (gt ^ "(") ++ pp_expr state env a ++ str ")"
             | None    -> pp_expr state env a) :: pp_vals ts' args'
        | [],       a :: args' -> pp_expr state env a :: pp_vals [] args'
      in
      let vals = pp_vals ftypes args in
      (* GENERIC STRUCT literal needs EXPLICIT type arguments — Go does not infer them for a
         composite literal ([Box[int64]{…}], not [Box{…}]).  Take them from the constructed
         type [Tglob(T, instanceargs)] (the MLcons type field): inside a generic function the
         instance is the enclosing type param ([Box[T1]{…}]); at a concrete use it is the
         concrete type ([Box[int64]{…}]).  A non-generic record has no args → no brackets. *)
      let tyargs = match cty with
        | Tglob (_, (_ :: _ as ta)) ->
            str "[" ++ prlist_with_sep (fun () -> str ", ") (pp_type state) ta ++ str "]"
        | _ -> mt () in
      (* KEYED struct literal [T{Field: v, …}] (field-order-independent, self-documenting —
         what Go style prefers) when a field name is known per value; positional fallback. *)
      let entries =
        if List.length fields = List.length vals
        then List.map2 (fun f v -> str f ++ str ": " ++ v) fields vals
        else vals in
      str (record_ctor_tyname r) ++ tyargs ++ str "{" ++
      prlist_with_sep (fun () -> str ", ") (fun x -> x) entries ++ str "}"

  | MLcons _ as e when Option.has_some (decode_go_string e) ->
      (* Coq [string] literal → Go string literal (byte-faithful). *)
      str (go_string_lit (Option.get (decode_go_string e)))

  | MLcons _ as e when Option.has_some (z_value e) ->
      (* Coq [Z] literal (a full-width [GoI64]/[GoU64] literal erases its single-field
         wrapper to this bare [Z]).  [z_value] preserves the 64-bit pattern but
         [Int64.to_string] reads it SIGNED.  Discriminate by the SOURCE Z's sign: a
         [Zpos] whose 64-bit value has the high bit set (negative as [Int64]) cannot be
         an [int64] (its range stops at 2^63-1), so it is an unsigned [uint64] value in
         [2^63, 2^64) and must print via [%Lu]; everything else (incl. genuine [Zneg]
         int64 negatives) prints signed. *)
      let v = Option.get (z_value e) in
      if z_is_nonneg e && Int64.compare v 0L < 0
      then str (Printf.sprintf "%Lu" v)
      else str (Int64.to_string v)

  | MLcons _ as e ->
      (match nat_value e with
       | Some n -> str (string_of_int n)
       | None   ->
           match e with
           | MLcons (_, r, []) when is_unit_tt r    -> mt ()
           | MLcons (_, r, []) when is_bool_true r  -> str "true"
           | MLcons (_, r, []) when is_bool_false r -> str "false"
           | MLcons (_, r, []) when is_list_nil r   -> str "nil"
           (* nullary ENUM constructor → the iota const name *)
           | MLcons (_, r, []) when is_enum_ctor r  -> str (go_export (global_basename r))
           (* [any v] (existT) in value position → its payload [v], which Go boxes to
              [any] from the surrounding context (a func arg / slot of interface type).
              [any_payload] strips the existT (and any nested pair) to the value; a sub-64
              narrow payload is converted to its real Go type at this boundary (break #7). *)
           | MLcons (_, r, _) when is_existT_ref r ->
               let v = pp_expr state env (any_payload e) in
               (match narrow_conv_of env e with
                | Some gt -> str (gt ^ "(") ++ v ++ str ")"
                | None    -> v)
           (* numeric-wrapper constructor [MkU8 v] → [v] (erase the wrapper) *)
           | MLcons (_, r, [v]) when is_numint_ctor r || is_goint_ctor r -> pp_expr state env v
           (* GoFloat32 constructor [mkF32 v] → [v] (the proof field is erased; the carrier
              IS the float32).  Should only appear inside by-name-lowered ops, but emit
              identity for safety. *)
           | MLcons (_, r, [v]) when is_f32_ctor r -> pp_expr state env v
           (* platform-uint constructor [MkUint v] → [uint(v)] (typed conversion; the proof field is
              erased).  The [uint(...)] makes the value Go [uint] in EVERY position — crucially at an
              [any]-box, where a bare [v] would box as Go [int] and disagree with the [TUint] tag
              (review #4 P0 #1).  [uint_lit n] (a Definition) renders the same via its MLglob arm above. *)
           | MLcons (_, r, [v]) when is_uint_ctor r -> str "uint(" ++ pp_expr state env v ++ str ")"
           (* spec_float LITERAL (the GoFloat64 carrier, review #6 #13→zero-axioms): the IEEE-754
              inductive value emits as an EXACT Go float literal.  A finite [S754_finite s m e]
              (= ±m·2^e) is the Go HEX float [±0x<m>p<e>] — Go parses it bit-for-bit and prints the
              same as the decimal, so runtime output is preserved.  [S754_zero false] is [0.0].
              ±Inf/NaN have no Go constant (need math.Inf/NaN), so a LITERAL one fails loud — they
              are produced at runtime (1.0/0.0, 0.0/0.0). *)
           | MLcons (_, r, [s; m; e]) when String.equal (global_basename r) "S754_finite" ->
               let sign_opt = (match s with
                 | MLcons (_, rs, []) when is_bool_true  rs -> Some true
                 | MLcons (_, rs, []) when is_bool_false rs -> Some false
                 | _ -> None) in
               (match sign_opt, pos_value m, z_eval e with
                | Some sign, Some mv, Some ev ->
                    str (Printf.sprintf "%s0x%Lxp%Ld" (if sign then "-" else "") mv ev)
                | _ -> unsupported "a spec_float S754_finite literal with a non-constant sign/mantissa/exponent")
           | MLcons (_, r, [s]) when String.equal (global_basename r) "S754_zero" ->
               (match s with
                | MLcons (_, rs, []) when is_bool_false rs -> str "0.0"
                | _ -> unsupported "a spec_float negative-zero literal (Go has no -0.0 constant; use math.Copysign at runtime)")
           | MLcons (_, r, _) when String.equal (global_basename r) "S754_infinity"
                               || String.equal (global_basename r) "S754_nan" ->
               unsupported "a spec_float ±Inf / NaN LITERAL (no Go Inf/NaN constant without math.Inf/math.NaN — produce it at runtime, e.g. 1.0/0.0 or 0.0/0.0)"
           | MLcons (_, r, _) as lst ->
               (* review R4: a non-empty list literal in VALUE position.  The old `append(nil, v1, …)`
                  is INVALID Go — `append`'s first argument must be a TYPED slice, and `nil` here is
                  untyped, so `go build` rejects it ("first argument to append must be a slice; have
                  untyped nil").  The element type is also erased by extraction, so we cannot synthesize
                  the `[]T` literal here.  Fail loud and direct the user to `slice_of_list <tag> [v1; …]`,
                  which carries the element type and lowers to a typed `[]T{v1, …}`. *)
               (match unfold_list [] lst with
                | Some _ ->
                    unsupported "a non-empty list literal in value position — `append(nil, …)` is invalid Go (append needs a TYPED slice, not untyped `nil`) and the element type is erased; use `slice_of_list <tag> [v1; …]` (carries the element type → a typed `[]T{…}`)"
                | None when is_list_cons r ->
                    unsupported "a list with a non-literal spine (cons whose tail is not statically known)"
                | None ->
                    unsupported ("constructor " ^ global_basename r ^
                                 " (only nat/bool/list/option constructors are modeled)"))
           | _ -> unsupported "a non-constructor value in constructor position")

  | MLmagic e -> pp_expr state env e
  | MLdummy _ -> str "_"
  | MLfloat f ->
      let s = Float64.to_string f in
      (* Ensure Go sees a float literal: add .0 if no decimal or exponent. *)
      let s = if String.contains s '.' || String.contains s 'e'
                 || String.contains s 'E' || String.contains s 'n'
              then s else s ^ ".0" in
      str s
  | MLexn s   -> str ("panic(" ^ Printf.sprintf "%S" s ^ ")")
  | MLaxiom s -> str ("panic(\"axiom: " ^ String.escaped s ^ "\")")
  | MLuint n  ->
      (* [int] is SIGNED (Sint63): a 63-bit value with its sign bit (bit 62) set
         — i.e. [>= 2^62] — denotes a NEGATIVE number ([value - 2^63]).  The raw
         unsigned [Uint63.to_string] would print e.g. [-1] as 9223372036854775807,
         a different value.  Emit the signed decimal instead.  (Positive literals
         [< 2^62] are unchanged.) *)
      let i64 = Uint63.to_int64 n in
      let s =
        if Int64.compare i64 0x4000000000000000L >= 0   (* sign bit set (>= 2^62) *)
        then Int64.to_string (Int64.add i64 Int64.min_int)  (* i64 - 2^63 (two's comp.) *)
        else Int64.to_string i64 in
      str s
  | _         -> unsupported "this expression (unmodeled MiniML node in value position)"

and pp_atom state env e =
  match e with
  | MLrel _ | MLglob _ | MLcons _ | MLuint _ | MLfloat _ | MLmagic _ ->
      pp_expr state env e
  (* a folded [int_lit N] (GoInt literal) is atomic like a bare [MLuint] — no operand parens
     (Go's unary [-] binds tighter than the binary operators it appears under); review #6 #13. *)
  | MLapp (MLglob r, [_]) when is_int_lit r -> pp_expr state env e
  | _ ->
      str "(" ++ pp_expr state env e ++ str ")"

(* Print [e] as an operand inside a binary operator whose context requires
   precedence [>= ctx].  If [e] is itself a binary operator of lower precedence,
   wrap it in parens; otherwise emit it bare (atoms and calls bind tightest, so
   they never need parens — unlike [pp_atom], which parenthesises every non-atom).
   A binop at the same precedence as [ctx] does NOT need parens on the left (Go's
   operators are left-associative); the caller passes [p+1] for the right
   operand to force parens there. *)
and pp_prec state env ctx e =
  match strip_magic e with
  | MLapp (h, args) ->
      let h2, all = collect_app h args in
      let vis = List.filter (fun a -> not (is_erased a)) all in
      (match h2, vis with
       | MLglob r, [a; b] when Option.has_some (binop_of r) ->
           let (p, opstr) = Option.get (binop_of r) in
           (match arith_force_go_type r with
            | Some ty when not (operand_is_runtime a || operand_is_runtime b) ->
                str (Printf.sprintf "func(x %s, y %s) %s { return x%sy }(" ty ty ty opstr)
                ++ pp_expr state env a ++ str ", " ++ pp_expr state env b ++ str ")"
            | _ ->
                let inner = pp_prec state env p a ++ str opstr ++ pp_prec state env (p + 1) b in
                if p < ctx then str "(" ++ inner ++ str ")" else inner)
       | _ -> pp_expr state env e)
  | _ -> pp_expr state env e

(* An integer/float literal carries no Go type, so where the target type must be
   fixed (a value boxed into [any], a typed cell/accumulator, …) wrap a bare
   literal as [int64(..)] / [float64(..)].  Non-literals already have a type. *)
and pp_typed_lit state env e =
  match strip_magic e with
  | MLuint _  -> str "int("     ++ pp_expr state env e ++ str ")"
  (* platform-int [GoInt] literal [int_lit z] — bare in [pp_expr], so pin [int(N)] in a typed
     position (a [:=] binding / box), exactly as the old [MLuint] arm did (review #6 #13). *)
  | MLapp (MLglob r, [_]) when is_int_lit r -> str "int(" ++ pp_expr state env e ++ str ")"
  | MLfloat _ -> str "float64(" ++ pp_expr state env e ++ str ")"
  (* A full-width [GoI64]/[GoU64] LITERAL erases (single-field record) to a bare [Z]
     decimal, which Go defaults to [int]; in a typed position it mismatches the
     [int64]/[uint64] context, so pin the type.  Signedness from the source-[Z] sign
     (same discrimination as the bare-[Z] value arm): a [Zpos] >= 2^63 is [uint64].
     (When the tag is known — e.g. a map default — [pp_typed_lit_tagged] is preferred.) *)
  | MLcons _ as z when Option.has_some (z_value z) ->
      let v = Option.get (z_value z) in
      let goty = if z_is_nonneg z && Int64.compare v 0L < 0 then "uint64" else "int64" in
      str goty ++ str "(" ++ pp_expr state env e ++ str ")"
  | _         -> pp_expr state env e

(* Like [pp_typed_lit] but the target Go type is KNOWN (from a [GoTypeTag] in the
   caller, e.g. a map's value tag): pin a bare literal to exactly that type, so a
   [uint64] default reads [uint64(0)], not the [int64(0)] [pp_typed_lit] would guess.
   A non-literal already carries its type, so it is emitted unwrapped. *)
and pp_typed_lit_tagged state env tagdoc e =
  match strip_magic e with
  | MLuint _ | MLfloat _ -> tagdoc ++ str "(" ++ pp_expr state env e ++ str ")"
  | MLapp (MLglob r, [_]) when is_int_lit r -> tagdoc ++ str "(" ++ pp_expr state env e ++ str ")"
  | MLcons _ as z when Option.has_some (z_value z) ->
      tagdoc ++ str "(" ++ pp_expr state env e ++ str ")"
  | _ -> pp_expr state env e

(* review #4 P1 #4: a value at a tag-typed destination (pointer cell, channel, map key/value).  If the
   destination is a sub-64 narrow numeric Go type the value (int64-carried) needs an explicit cast
   ([uint8(…)]) — else invalid Go or a model/runtime tag mismatch; otherwise [fallback ()], the SITE's
   existing non-narrow emission (so non-narrow writes stay byte-identical).  Keyed off the [GoTypeTag]. *)
and pp_narrow_or state env tag e fallback =
  match narrow_go_name (go_type_of_tag (strip_magic tag)) with
  | Some gt -> str (gt ^ "(") ++ pp_expr state env e ++ str ")"
  | None    -> fallback ()
(* The pointer/ref-write specialisation: narrow cast, else [pp_typed_lit] (the prior behaviour). *)
and pp_payload_at_tag state env tag e =
  pp_narrow_or state env tag e (fun () -> pp_typed_lit state env e)
(* For destinations whose non-narrow emission is bare [pp_expr] — a map KEY, a channel payload. *)
and pp_narrow_or_expr state env tag e =
  pp_narrow_or state env tag e (fun () -> pp_expr state env e)

(* Emit a lambda as a TYPED Go closure [func(x A) R { return body }] using a known
   function type [A1 -> … -> R] (a method dictionary entry).  Unlike the generic
   [MLlam] arm (which emits [func(x any) any]), this matches the struct field type,
   so the dictionary literal type-checks.  The binders stay in the de Bruijn env. *)
and pp_typed_closure state env ftype lam =
  let rec split_arrows = function
    | Tarr (a, b) -> let (args, ret) = split_arrows b in (a :: args, ret)
    | t -> ([], t)
  in
  let argtypes, rettype = split_arrows ftype in
  let ids, body = collect_lam lam in
  let rec zip ids ts = match ids, ts with
    | [], _ -> []
    | id :: ids', t :: ts' -> (id, t) :: zip ids' ts'
    | id :: ids', []       -> (id, Tunknown) :: zip ids' []
  in
  let pairs = zip ids argtypes in
  (* drop [unit]-typed params from the SIGNATURE (a nullary method's [unit] trigger) — they
     stay in [new_env] for de Bruijn, but the Go closure is nullary [func() R]. *)
  let is_unit_arg t = (match t with Tglob (r, []) -> is_unit_type r | _ -> false) in
  let sig_pairs = List.filter (fun (_, t) -> not (is_unit_arg t)) pairs in
  let new_env = List.rev ids @ env in
  str "func(" ++
  prlist_with_sep (fun () -> str ", ")
    (fun (id, t) -> pp_mlident id ++ str " " ++ pp_type state t) sig_pairs ++
  str ") " ++ pp_type state rettype ++ str " {" ++ fnl () ++
  str "\treturn " ++ pp_expr state new_env body ++ fnl () ++
  str "}"

(*s CFG blocks (the goto model).  A block is an [IO Next]: it does its effects
    then transfers control via [ret (Jump n)] (goto block n) or [ret Done]
    (return).  [block_targets] collects the Jump targets (so only used labels
    are emitted — Go rejects unused ones); [emit_block] prints one block. *)

and block_targets b acc =
  let h, args = collect_app b [] in
  let vis = List.filter (fun a -> not (is_erased a)) args in
  match h, vis with
  | MLglob r, [_action; k] when is_bind_ref r ->
      let _ids, kbody = collect_lam k in block_targets kbody acc
  | MLglob r, [next] when is_ret_ref r ->
      (match next with
       | MLcons (_, c, [n]) when is_jump_ctor c ->
           (match nat_value n with Some v -> v :: acc | None -> acc)
       | _ -> acc)
  | _ ->
      (match b with
       | MLcase (_, _, branches) ->
           Array.fold_left (fun a (_, _, body) -> block_targets body a) acc branches
       | _ -> acc)

(* Does any tail of this block [ret Done] (return)?  A block can both return on
   one branch and jump on another, so this is independent of [block_targets].
   Used to give every returning block an edge to the virtual exit node when
   computing post-dominators for the structurer. *)
and block_has_done b =
  let h, args = collect_app b [] in
  let vis = List.filter (fun a -> not (is_erased a)) args in
  match h, vis with
  | MLglob r, [_action; k] when is_bind_ref r ->
      let _ids, kbody = collect_lam k in block_has_done kbody
  | MLglob r, [next] when is_ret_ref r ->
      (match next with MLcons (_, c, []) when is_done_ctor c -> true | _ -> false)
  | _ ->
      (match b with
       | MLcase (_, _, branches) ->
           Array.exists (fun (_, _, body) -> block_has_done body) branches
       | _ -> false)

(* Loop-block-local variables (bound by a [bind] inside a block) are hoisted to
   [var x T] before the blocks so their declaration dominates all uses and is
   never re-run on a backward jump.  Only tagged reads ([ref_get tag r]) are
   hoistable here, since that is where the Go type is known. *)
and block_hoists b acc =
  let h, args = collect_app b [] in
  let vis = List.filter (fun a -> not (is_erased a)) args in
  match h, vis with
  | MLglob r, [action; k] when is_bind_ref r ->
      let ids, kbody = collect_lam k in
      let acc' =
        (match List.filter (fun id -> not (is_dummy id)) ids with
         | [x] ->
             let ah, aargs = collect_app action [] in
             let avis = List.filter (fun a -> not (is_erased a)) aargs in
             (match ah, avis with
              | MLglob rr, [tag; _rf] when is_ref_get_ref rr ->
                  (x, go_type_of_tag tag) :: acc
              | _ -> acc)
         | _ -> acc) in
      block_hoists kbody acc'
  | _ ->
      (match b with
       | MLcase (_, _, branches) ->
           Array.fold_left (fun a (_, _, body) -> block_hoists body a) acc branches
       | _ -> acc)

(* Emit one [bind] action (the non-control part of a block) as a Go statement.
   [ids] are the continuation's binders (for the [x =] lhs of a value read).
   - a [defer]/[go] closure captures the hoisted loop-temps BY VALUE
     ([kw func(iv T){ body }(iv)]) so each iteration's value is fixed here;
     capturing the shared hoisted cell by reference would give every closure the
     last value (the goto form of Go's loop-variable capture; the structured
     [for] lowering gets this free on Go 1.22+).  A closure body is not part of
     the CFG, so it terminates with [raw_term] (return).
   - a value read ([ref_get]) goes to its hoisted var with [=]; a void action
     (println / ref_set, whose [fun _] may extract as a named var) is just the
     statement. *)
and emit_action state hoists tab env ids action =
  let emit_closure kw body =
    let sep = prlist_with_sep (fun () -> str ", ") (fun x -> x) in
    let params = List.map (fun (x, t) -> pp_mlident x ++ str " " ++ str t) hoists in
    let args   = List.map (fun (x, _) -> pp_mlident x) hoists in
    str tab ++ str kw ++ str " func(" ++ sep params ++ str ") {" ++ fnl () ++
    emit_block false state hoists raw_term (tab ^ "\t") env body ++
    str tab ++ str "}(" ++ sep args ++ str ")" ++ fnl ()
  in
  let ah, aargs = collect_app action [] in
  let avis = List.filter (fun a -> not (is_erased a)) aargs in
  match ah, avis with
  | MLglob rr, [dbody] when is_defer_call_ref rr -> emit_closure "defer" dbody
  | MLglob rr, [gbody] when is_go_spawn_ref rr -> emit_closure "go" gbody
  | _ ->
      let action_is_value =
        (match ah with MLglob rr -> is_ref_get_ref rr | _ -> false) in
      let lhs = match List.filter (fun id -> not (is_dummy id)) ids with
        | [x] when action_is_value -> pp_mlident x ++ str " = "
        | _   -> mt () in
      str tab ++ lhs ++ pp_expr state env action ++ fnl ()

(* [term] handles the block's terminator (a [Next] value) — see [raw_term] /
   [loop_term].  This is what lets one emitter print raw goto or structured
   continue/break. *)
and emit_block terminating state hoists term tab env b =
  let h, args = collect_app b [] in
  let vis = List.filter (fun a -> not (is_erased a)) args in
  match h, vis with
  | MLglob r, [action; k] when is_bind_ref r ->
      let ids, kbody = collect_lam k in
      let new_env = List.rev ids @ env in
      emit_action state hoists tab env ids action
      ++ emit_block terminating state hoists term tab new_env kbody
  | MLglob r, [next] when is_ret_ref r -> term tab next
  | _ ->
      (match b with
       | MLcase (_, scrut, branches) ->
           (match Array.to_list branches with
            (* multiple-return DESTRUCTURE: [let '(x,y) := f … in body] (single pair
               branch, 2 binders) → [x, y := f(…)] then the rest. *)
            | [ (ids, pat, body1) ]
              when (match pat with Pcons (r, _) | Pusual r -> is_pair_ref r | _ -> false)
                   && List.length ids = 2 ->
                let flat_ids, fbody, fenv = flatten_destructure ids body1 env in   (* N-ary: x, y, z := f() *)
                str tab ++ prlist_with_sep (fun () -> str ", ") (pp_destr_binder fenv fbody) flat_ids
                ++ str " := " ++ pp_expr state env scrut ++ fnl ()
                ++ emit_block terminating state hoists term tab fenv fbody
            | [ (_, p1, body1); (_, p2, body2) ] ->
                let cref p = (match p with Pusual r | Pcons (r, _) -> Some r | _ -> None) in
                (match cref p1, cref p2 with
                 | Some c1, Some c2
                   when (is_bool_true c1 && is_bool_false c2)
                     || (is_bool_false c1 && is_bool_true c2) ->
                     let tb, eb = if is_bool_true c1 then body1, body2 else body2, body1 in
                     str tab ++ str "if " ++ pp_expr state env scrut ++ str " {" ++ fnl () ++
                     emit_block terminating state hoists term (tab ^ "\t") env tb ++
                     str tab ++ str "} else {" ++ fnl () ++
                     emit_block terminating state hoists term (tab ^ "\t") env eb ++
                     str tab ++ str "}" ++ fnl ()
                 (* review R1: a 2-branch match that is NOT bool true/false would silently become a
                    bare `return`, DROPPING both arms — fail loud (was the raw emitter's silent hole). *)
                 | _ -> unsupported "a raw-CFG/closure block whose 2-branch match is not on a bool (true/false) — the raw emitter only lowers boolean if/else; a non-bool match would silently become a bare `return`, truncating the block's control flow")
            (* review R1: a match with neither 1 (pair destructure) nor 2 (bool if/else) branches would
               silently become a bare `return`, dropping the other arms — fail loud. *)
            | _ -> unsupported "a raw-CFG/closure block match with neither 1 (pair destructure) nor 2 (bool if/else) branches — would silently become a bare `return`, dropping the other arms")
       (* review R1: a non-bind/ret/case block.  TERMINATING context (a run_blocks block — must end in a
          goto/return): a bare expression drops the control terminator → fail loud.  NON-terminating
          context (a defer/go closure body): a single void action with no terminator is valid Go, so emit it. *)
       | _ -> if terminating
              then unsupported "a raw-CFG run_blocks block of an unrecognized shape (not bind / ret / pair-destructure / boolean if-else) — a bare expression would have NO control terminator, silently dropping its control flow"
              else str tab ++ pp_expr state env b ++ fnl ())

(*s IO body emitter — shared by pp_function (for IO-returning fns) and pp_main_body. *)

let pp_io_body ?(ret_val=false) state tab env body =
  let is_void_call e =
    match collect_app e [] with
    | MLglob r, _ when is_print_ref r || is_println_ref r || is_panic_ref r
                       || is_map_set_ref r || is_map_del_ref r || is_map_clear_ref r
                       || is_send_ref r || is_close_chan_ref r || is_ref_set_ref r -> true
    | _ -> false
  in
  let is_terminating e =
    match collect_app e [] with
    | MLglob r, _ when is_panic_ref r -> true
    | _ -> false
  in
  let rec pp_stmts tab env = function
    | MLapp _ as e ->
        let head, all_args = collect_app e [] in
        let vis = List.filter (fun a -> not (is_erased a)) all_args in
        (match head, vis with
         | MLglob r, [m; f] when is_bind_ref r ->
             (match strip_magic m with
              (* IO-valued branch: [bind] distributes over the match, turning it
                 into an if/switch statement rather than a value. *)
              | MLcase (typ, scrut, branches) ->
                  (match collect_lam f with
                   (* RESULT DISCARDED ([fun _ => rest], bound var unused): the
                      arms need no continuation — each arm's effects run and both
                      fall through to [rest], emitted ONCE after the if/else.  This
                      avoids exponential blow-up: chaining N inline [if]s in one
                      [bind] otherwise duplicates the tail into 2^(N-1) copies. *)
                   | ([id], fbody) when is_dummy id || db1_free fbody ->
                       emit_case tab env typ scrut branches (fun _nb b -> b) ++
                       pp_stmts tab (id :: env) fbody
                   (* RESULT USED: thread the continuation into every arm (lifting
                      it past each arm's pattern binders so its de Bruijn indices
                      stay correct), so each arm's value flows into it. *)
                   | _ ->
                       emit_case tab env typ scrut branches
                         (fun nb b -> MLapp (head, [b; ast_lift nb f])))
              | _ ->
             let ids, body = collect_lam f in
             let new_env = List.rev ids @ env in
             (* go_spawn: bind (go_spawn goroutine_body) f → go func(){body}(); f.
                Strip [MLmagic] coercions so a wrapped action (e.g. a nested
                [bind]) is still recognised by its head. *)
             let h_m, all_m = collect_app (strip_magic m) [] in
             let vis_m = List.filter (fun a -> not (is_erased a)) all_m in
             (match h_m, vis_m with
              | MLglob r2, [goroutine_body] when is_go_spawn_ref r2 ->
                  str tab ++ str "go func() {" ++ fnl () ++
                  pp_stmts (tab ^ "\t") env goroutine_body ++
                  str tab ++ str "}()" ++ fnl () ++
                  pp_stmts tab new_env body
              | MLglob r2, [defer_body] when is_defer_call_ref r2 ->
                  str tab ++ str "defer func() {" ++ fnl () ++
                  pp_stmts (tab ^ "\t") env defer_body ++
                  str tab ++ str "}()" ++ fnl () ++
                  pp_stmts tab new_env body
              (* the action is itself a compound IO computation (a nested [bind],
                 e.g. a fused map_get_opt/match) whose result is discarded — emit
                 it as statements, then the continuation.  ([fun _ =>] may extract
                 as a named-but-unused binder, so check [db1_free], not [is_dummy]). *)
              | MLglob r2, _ when is_bind_ref r2
                              && (List.filter (fun id -> not (is_dummy id)) ids = []
                                  || db1_free body) ->
                  pp_stmts tab env (strip_magic m) ++ pp_stmts tab new_env body
              (* a type_switch / val_switch ACTION whose unit result is discarded (the only sensible
                 use — its cases are IO unit) → emit it as a STATEMENT (the switch arms below handle
                 it), then the continuation.  Without this a [type_switch … >>' rest] (a non-final
                 switch) falls to VALUE position, where the tag constructors fail to extract. *)
              | MLglob r2, _ when (is_type_switch_ref r2 || is_val_switch_ref r2)
                              && (List.filter (fun id -> not (is_dummy id)) ids = []
                                  || db1_free body) ->
                  pp_stmts tab env (strip_magic m) ++ pp_stmts tab new_env body
              | MLglob r2, [xs; bodyfn] when is_for_each_ref r2 ->
                  emit_for_each tab env xs bodyfn ++
                  pp_stmts tab new_env body
              | MLglob r2, [s; bodyfn] when is_str_range_ref r2 ->
                  emit_range2 tab env s bodyfn ++
                  pp_stmts tab new_env body
              | MLglob r2, [xs; bodyfn] when is_for_each_idx_ref r2 ->
                  emit_range2 tab env xs bodyfn ++
                  pp_stmts tab new_env body
              | MLglob r2, [n; bodyfn] when is_int_range_ref r2 ->
                  emit_range2 tab env n bodyfn ++
                  pp_stmts tab new_env body
              (* bind (map_get_or k def m) (fun hit => body) → the value at k or
                 def, via comma-ok.  [hit] takes the default's (typed) value, so
                 it has the map's value type (not [any]); the if-block overwrites
                 it on a hit. *)
              | MLglob r2, [kt; vt; k; def; mm] when is_map_get_or_ref r2 ->
                  let hit = match List.filter (fun id -> not (is_dummy id)) ids with
                    | [id] -> pp_mlident id | _ -> str "_g" in
                  (* pin the default to the map's VALUE type (from [vt]) — for a bare full-width literal
                     that would otherwise default to Go [int] and mismatch an [int64]/[uint64] value slot;
                     a NARROW value type also needs the [uint8(…)] cast so [hit] is inferred narrow (so
                     the later [hit = _v] from the narrow map value type-checks) — review #4 P1 #4. *)
                  let vn = go_type_of_tag (strip_magic vt) in
                  str tab ++ hit ++ str " := " ++
                  pp_narrow_or state env vt def (fun () -> pp_typed_lit_tagged state env (str vn) def) ++ fnl () ++
                  str tab ++ str "if _v, _ok := " ++ pp_expr state env mm ++
                  str "[" ++ pp_narrow_or_expr state env kt k ++ str "]; _ok {" ++ fnl () ++
                  str (tab ^ "\t") ++ hit ++ str " = _v" ++ fnl () ++
                  str tab ++ str "}" ++ fnl () ++
                  pp_stmts tab new_env body
              (* bind (map_get_opt k m) (fun o => match o with Some v => A | None => B)
                 → comma-ok if/else.  No [option] value is built; the bound [o] is
                 consumed by the match, so [v] is bound directly from m[k]. *)
              | MLglob r2, [kt; _vt; k; mm] when is_map_get_opt_ref r2 ->
                  let fallback () =
                    unsupported "a map_get_opt result that is not immediately matched (the option has no Go representation; bind it directly to a Some v / None match)" in
                  (match ids, strip_magic body with
                   | [o_id], MLcase (_, scrut, branches)
                     when (match strip_magic scrut with MLrel 1 -> true | _ -> false) ->
                       (match Array.to_list branches with
                        | [ (bids1, p1, b1); (bids2, p2, b2) ] ->
                            let cref p = (match p with Pusual r | Pcons (r, _) -> Some r | _ -> None) in
                            (match cref p1, cref p2 with
                             | Some c1, Some c2
                               when (is_some_ctor c1 && is_none_ctor c2)
                                 || (is_none_ctor c1 && is_some_ctor c2) ->
                                 let some_ids, some_body, none_body =
                                   if is_some_ctor c1 then bids1, b1, b2
                                   else bids2, b2, b1 in
                                 let v_doc = match some_ids with
                                   | [vid] when not (is_dummy vid) -> pp_mlident vid
                                   | _ -> str "_" in
                                 str tab ++ str "if " ++ v_doc ++ str ", _ok := "
                                   ++ pp_expr state env mm ++ str "["
                                   ++ pp_narrow_or_expr state env kt k ++ str "]; _ok {" ++ fnl () ++
                                 pp_stmts (tab ^ "\t") (List.rev some_ids @ (o_id :: env)) some_body ++
                                 str tab ++ str "} else {" ++ fnl () ++
                                 pp_stmts (tab ^ "\t") (o_id :: env) none_body ++
                                 str tab ++ str "}" ++ fnl ()
                             | _ -> fallback ())
                        | _ -> fallback ())
                   | _ -> fallback ())
              | _ ->
             let vis_ids = List.filter (fun id -> not (is_dummy id)) ids in
             let lhs = match vis_ids with
               | _ when is_void_call m   -> mt ()
               | _ when db1_free body    ->
                   (* If the head is a known value-returning builtin (slice_get,
                      type_assert, etc.), discard with _ =.
                      User-defined functions have unknown return — assume void. *)
                   let head_m, _ = collect_app m [] in
                   (match head_m with
                    | MLglob r when is_slice_get_ref r || is_type_assert_ref r
                                 || is_map_len_ref r || is_map_get_or_ref r
                                 || is_len_ref r || is_cap_ref r
                                 || is_recv_ref r || is_make_chan_ref r || is_make_chan_buf_ref r
                                 || Option.has_some (classify_nat_op r)
                                 || Option.has_some (classify_int63_op r)
                                 || Option.has_some (classify_float_op r) -> str "_ = "
                    | _                              -> mt ())
               | [id]                    -> pp_mlident id ++ str " := "
               | _                       -> mt ()
             in
             (* Bare MLglob in statement position is a zero-arg IO function
                call — needs () in Go.  Builtins handle their own emission. *)
             let emit_m =
               match m with
               | MLglob r when not (is_bool_true r || is_bool_false r
                                    || is_unit_tt r || is_map_make_ref r) ->
                   pp_expr state env m ++ str "()"
               | _ -> pp_expr state env m
             in
             str tab ++ lhs ++ emit_m ++ fnl () ++
             (if is_terminating m then mt ()
              else pp_stmts tab new_env body)))
         | MLglob r, [v] when is_ret_ref r ->
             (* TAIL [ret v].  The [ret] case is only reached at a tail (an intermediate
                [ret] is the action of a [bind], emitted via [emit_m]); and a tail's [ret]
                argument is [tt] (unit) IFF the enclosing scope is void.  So [ret tt] → no
                statement (the function/goroutine is void), and [ret <non-unit>] → [return v]
                (a value-returning IO function's tail) — NO ret-mode threading needed, since
                the value distinguishes the two.  (Bare value-OP tails, not wrapped in [ret],
                are handled by pp_io_body's top-level [ret_tail].) *)
             (match strip_magic v with
              | MLcons (_, r2, []) when is_unit_tt r2 -> mt ()
              | v' -> str tab ++ str "return " ++ pp_expr state env v' ++ fnl ())
         (* for_each in tail position (not inside a bind) *)
         | MLglob r, [xs; bodyfn] when is_for_each_ref r ->
             emit_for_each tab env xs bodyfn
         (* str_range / for_each_idx in tail position (two-variable range) *)
         | MLglob r, [s; bodyfn] when is_str_range_ref r ->
             emit_range2 tab env s bodyfn
         | MLglob r, [xs; bodyfn] when is_for_each_idx_ref r ->
             emit_range2 tab env xs bodyfn
         | MLglob r, [n; bodyfn] when is_int_range_ref r ->
             emit_range2 tab env n bodyfn
         (* run_blocks start [b0; b1; …] → Go labels + goto.  Only Jump-target
            labels are emitted (Go rejects unused labels); the label sits one
            indent left of its block, as gofmt wants. *)
         | MLglob r, [start; blocks] when is_run_blocks_ref r ->
             (match unfold_list [] blocks with
              | Some bs ->
                  (* Distinct goto-blocks can reuse the same Rocq binder name
                     (each is a closed term, so Rocq does not alpha-rename across
                     them); they become one reused Go variable, assigned before
                     each read, so collapse same-named hoists to a single [var]. *)
                  let hoists =
                    let raw = List.fold_left (fun a b -> block_hoists b a) [] bs in
                    let key = function Id v | Tmp v -> Id.to_string v | Dummy -> "_" in
                    let seen = ref [] in
                    List.filter (fun (x, _) ->
                      let k = key x in
                      if List.mem k !seen then false else (seen := k :: !seen; true)) raw in
                  let start_v = match nat_value start with
                    | Some v -> v
                    | None -> unsupported "run_blocks with a non-literal start block index (the CFG entry label must be statically known; defaulting to block0 would silently run a DIFFERENT program)" in
                  (* hoisted declarations: var x T, dominating every block *)
                  let hoist_doc =
                    prlist
                      (fun (x, t) ->
                         str tab ++ str "var " ++ pp_mlident x ++ str " "
                         ++ str t ++ fnl ())
                      (List.rev hoists) in
                  let blocks = Array.of_list bs in
                  let nblk = Array.length blocks in
                  (* review #4 P1 3: validate the entry + every literal Jump target is IN RANGE before any
                     labelling / dominator processing.  An out-of-range entry/jump would otherwise become an
                     undefined Go label (raw path) or an out-of-bounds [blocks.(i)] in the structurer — not a
                     controlled abort. *)
                  let () =
                    if start_v >= nblk then
                      unsupported (Printf.sprintf "run_blocks entry block index %d is out of range (there are only %d blocks): `goto block%d` would be an undefined Go label" start_v nblk start_v);
                    List.iter (fun t ->
                      if t >= nblk then
                        unsupported (Printf.sprintf "run_blocks Jump to block index %d is out of range (there are only %d blocks): an undefined Go label / out-of-bounds block access" t nblk))
                      (List.sort_uniq compare (List.fold_left (fun a b -> block_targets b a) [] bs)) in
                  let exit_node = nblk in
                  let inter a b = List.filter (fun x -> List.mem x b) a in
                  let targets_of i =
                    List.sort_uniq compare (block_targets blocks.(i) []) in
                  (* raw labels + goto: the always-correct fallback.  Only Jump-
                     target labels are emitted (Go rejects unused ones); a label
                     sits one indent left of its block, as gofmt wants. *)
                  let emit_raw () =
                    let targets =
                      (* review #4 P1 3: a NONZERO entry needs its OWN label — `initial` gotos it, so it
                         must be in the label set (the Jump targets alone miss an entry no block jumps to). *)
                      let jt = List.fold_left (fun a b -> block_targets b a) [] bs in
                      if start_v = 0 then jt else start_v :: jt in
                    let is_target i = List.mem i targets in
                    let lbl_indent =
                      if String.length tab >= 1
                      then String.sub tab 0 (String.length tab - 1) else "" in
                    let initial =
                      if start_v = 0 then mt ()
                      else str tab ++ str (Printf.sprintf "goto block%d" start_v) ++ fnl () in
                    let rec emit_all i = function
                      | [] -> mt ()
                      | bi :: rest ->
                          (if is_target i
                           then str lbl_indent ++ str (Printf.sprintf "block%d:" i) ++ fnl ()
                           else mt ()) ++
                          emit_block true state hoists raw_term tab env bi ++
                          emit_all (i + 1) rest
                    in
                    hoist_doc ++ initial ++ emit_all 0 bs in
                  if nblk = 0 then emit_raw () else begin
                  (* Unified structurer (a relooper) — lifts the goto-CFG back to
                     Go spec "If statements" / "For statements" / "Break/Continue" /
                     "Goto statements" / "Return statements".  Build dominators and
                     post-dominators, identify natural loops, then recursively emit:
                     - a loop header → [for { <body> }] with back-edges becoming
                       loop-around (fall-through) or [continue], the single exit
                       becoming [break];
                     - a conditional → [if]/[else] whose branches run up to their
                       merge (the post-dominator), the merge emitted once after.
                     Anything that breaks the structured assumptions (non-zero
                     entry, a multi-exit loop, improperly nested loops) falls back
                     to raw labels+goto, which is always correct. *)
                  let succ_exit i =
                    let ts = targets_of i in
                    if block_has_done blocks.(i) then exit_node :: ts else ts in
                  (* post-dominators (iterative fixpoint; valid with cycles) *)
                  let full = Array.to_list (Array.init (nblk + 1) (fun i -> i)) in
                  let pdom = Array.make (nblk + 1) full in
                  pdom.(exit_node) <- [exit_node];
                  let pchanged = ref true in
                  while !pchanged do
                    pchanged := false;
                    for i = nblk - 1 downto 0 do
                      let common = match succ_exit i with
                        | [] -> [exit_node]
                        | s0 :: rest ->
                            List.fold_left (fun acc x -> inter acc pdom.(x)) pdom.(s0) rest in
                      let d = List.sort_uniq compare (i :: common) in
                      if d <> pdom.(i) then (pdom.(i) <- d; pchanged := true)
                    done
                  done;
                  (* dominators (iterative fixpoint) *)
                  let preds = Array.make nblk [] in
                  for i = 0 to nblk - 1 do
                    List.iter (fun t -> if t >= 0 && t < nblk then preds.(t) <- i :: preds.(t))
                      (targets_of i)
                  done;
                  let alln = Array.to_list (Array.init nblk (fun i -> i)) in
                  let domA = Array.make nblk alln in
                  if start_v >= 0 && start_v < nblk then domA.(start_v) <- [start_v];
                  let dchanged = ref true in
                  while !dchanged do
                    dchanged := false;
                    for i = 0 to nblk - 1 do
                      if i <> start_v then begin
                        let d = match preds.(i) with
                          | [] -> domA.(i)
                          | p0 :: rest ->
                              List.fold_left (fun acc p -> inter acc domA.(p)) domA.(p0) rest in
                        let d = List.sort_uniq compare (i :: d) in
                        if d <> domA.(i) then (domA.(i) <- d; dchanged := true)
                      end
                    done
                  done;
                  let dominates a b =
                    a >= 0 && a < nblk && b >= 0 && b < nblk && List.mem a domA.(b) in
                  (* back-edges (n → h with h dominating n) and natural loops *)
                  let back_edges = ref [] in
                  for n = 0 to nblk - 1 do
                    List.iter (fun h -> if dominates h n then back_edges := (n, h) :: !back_edges)
                      (targets_of n)
                  done;
                  let headers = List.sort_uniq compare (List.map snd !back_edges) in
                  let loop_body h =
                    let body = ref [h] in
                    let stack = ref [] in
                    List.iter (fun (n, hh) ->
                      if hh = h && not (List.mem n !body) then (body := n :: !body; stack := n :: !stack))
                      !back_edges;
                    while !stack <> [] do
                      let n = List.hd !stack in stack := List.tl !stack;
                      List.iter (fun p ->
                        if not (List.mem p !body) then (body := p :: !body; stack := p :: !stack))
                        preds.(n)
                    done;
                    List.sort_uniq compare !body in
                  let bodies = List.map (fun h -> (h, loop_body h)) headers in
                  let body_of h = try List.assoc h bodies with Not_found -> [h] in
                  let loop_exit_of h =
                    let b = body_of h in
                    let ex = ref [] in
                    List.iter (fun n ->
                      List.iter (fun t ->
                        if not (List.mem t b) && not (List.mem t !ex) then ex := t :: !ex)
                        (targets_of n)) b;
                    !ex in
                  (* Loop nesting.  [a] is an ancestor of [h] when its body
                     strictly contains h's.  An exit of [h] that targets an
                     ancestor's header (a labeled [continue]) or an ancestor's
                     exit (a labeled [break]) escapes the enclosing loop; the one
                     remaining exit, if any, is h's *primary* exit, emitted after
                     the [for].  (A jump that escapes more than the innermost loop
                     is exactly Go's labeled break/continue.) *)
                  let subset a b = List.for_all (fun x -> List.mem x b) a in
                  let ancestors_of h =
                    List.filter (fun a ->
                      a <> h
                      && subset (body_of h) (body_of a)
                      && List.length (body_of a) > List.length (body_of h)) headers in
                  let ancestor_targets h =
                    List.concat (List.map (fun a -> a :: loop_exit_of a) (ancestors_of h)) in
                  let primary_exits h =
                    List.filter (fun e -> not (List.mem e (ancestor_targets h)))
                      (loop_exit_of h) in
                  let deeper_blocks h =
                    List.concat (List.map (fun h2 ->
                      if h2 <> h && subset (body_of h2) (body_of h)
                         && List.length (body_of h2) < List.length (body_of h)
                      then body_of h2 else []) headers) in
                  (* a header needs a Go label iff a break/continue from a *nested*
                     loop targets it (its header, for continue; its primary exit,
                     for break) *)
                  let label_of h = "L" ^ string_of_int h in
                  let needs_label h =
                    let pe = primary_exits h in
                    let deeper = deeper_blocks h in
                    List.exists (fun n ->
                      List.mem n deeper &&
                      (List.mem h (targets_of n)
                       || List.exists (fun e -> List.mem e (targets_of n)) pe))
                      alln in
                  (* Reducibility: removing back-edges (target dominates source)
                     must leave a DAG.  An irreducible CFG — a cycle with no
                     dominating back-edge, so no detected loop header — would make
                     [emit_region] recurse forever (nothing stops the cycle), so
                     it must take the raw-goto fallback. *)
                  let forward_succ n =
                    List.filter (fun t -> t >= 0 && t < nblk && not (dominates t n))
                      (targets_of n) in
                  let reducible =
                    let color = Array.make nblk 0 in
                    let ok = ref true in
                    let rec dfs n =
                      color.(n) <- 1;
                      List.iter (fun t ->
                        if color.(t) = 1 then ok := false
                        else if color.(t) = 0 then dfs t) (forward_succ n);
                      color.(n) <- 2 in
                    for n = 0 to nblk - 1 do if color.(n) = 0 then dfs n done;
                    !ok in
                  (* structurable: single entry at 0, reducible, ≤1 primary exit
                     per loop (extra exits are labeled escapes to enclosing loops),
                     loops properly nested (bodies disjoint or one inside other) *)
                  let structurable =
                    start_v = 0 && reducible &&
                    List.for_all (fun h -> List.length (primary_exits h) <= 1) headers &&
                    List.for_all (fun h1 ->
                      List.for_all (fun h2 ->
                        h1 = h2 ||
                        (let b1 = body_of h1 and b2 = body_of h2 in
                         inter b1 b2 = [] || subset b1 b2 || subset b2 b1))
                        headers) headers in
                  (* merge of a conditional = the *immediate* (closest) block post-
                     dominating both branches' leaf successors (exit for returning
                     leaves).  The common post-dominators form a chain ordered by
                     post-domination; the immediate one is the [m] that every other
                     common post-dominator also post-dominates.  (Min-index is wrong
                     under cycles: a back-edge can make a low-index header appear in
                     the set even though the real merge is a higher-index block.) *)
                  let merge_of tb eb =
                    let leaves bb =
                      let ts = List.sort_uniq compare (block_targets bb []) in
                      if block_has_done bb then exit_node :: ts else ts in
                    match List.sort_uniq compare (leaves tb @ leaves eb) with
                    | [] -> exit_node
                    | s0 :: rest ->
                        let common =
                          List.fold_left (fun acc x -> inter acc pdom.(x)) pdom.(s0) rest in
                        (match List.filter
                                 (fun m -> List.for_all
                                             (fun x -> x = m || List.mem x pdom.(m)) common)
                                 common with
                         | m :: _ -> m
                         | [] -> exit_node) in
                  (* a branch that is a bare unconditional jump to j (no statements) *)
                  let bare_jump bb =
                    match collect_app bb [] with
                    | MLglob r, args when is_ret_ref r ->
                        (match List.filter (fun a -> not (is_erased a)) args with
                         | [MLcons (_, c, [nn])] when is_jump_ctor c -> nat_value nn
                         | _ -> None)
                    | _ -> None in
                  (* Each block is a separate term in the *call-site* de Bruijn
                     context [env]; its free variables (e.g. the enclosing [Ref])
                     are relative to that, not to whatever block we jumped from.
                     So [emit_region] always (re)starts a block with [env]; only
                     [walk] extends the environment, and only for that block's own
                     binders.
                     [loopctx]: enclosing loops, innermost first, each (header,
                     exit option).  [tail]: reaching the loop-around here is the
                     body's natural end (so a back-edge falls through, else it is
                     an explicit [continue]). *)
                  let rec emit_region entry stop loopctx tail tab =
                    if entry = stop || entry = exit_node then mt ()
                    else if List.mem entry headers then begin
                      let ex = match primary_exits entry with [e] -> Some e | _ -> None in
                      let bctx = (entry, ex) :: loopctx in
                      let lbl_indent =
                        if String.length tab >= 1
                        then String.sub tab 0 (String.length tab - 1) else "" in
                      let lbl =
                        if needs_label entry
                        then str lbl_indent ++ str (label_of entry) ++ str ":" ++ fnl ()
                        else mt () in
                      lbl
                      ++ str tab ++ str "for {" ++ fnl ()
                      ++ walk blocks.(entry) entry bctx true (tab ^ "\t") env
                      ++ str tab ++ str "}" ++ fnl ()
                      ++ (match ex with
                          | Some e -> emit_region e stop loopctx tail tab
                          | None -> mt ())
                    end
                    else walk blocks.(entry) stop loopctx tail tab env
                  and handle_edge j stop loopctx tail tab =
                    (* find which enclosing loop this edge targets (innermost
                       first): the header (continue) or the primary exit (break).
                       Depth 0 is the innermost loop → unlabeled; deeper → labeled. *)
                    let rec scan depth = function
                      | (hd, ex) :: rest ->
                          if j = hd then Some (`Continue, depth, hd)
                          else if ex = Some j then Some (`Break, depth, hd)
                          else scan (depth + 1) rest
                      | [] -> None in
                    match scan 0 loopctx with
                    | Some (`Continue, 0, _) ->
                        if tail then mt () else str tab ++ str "continue" ++ fnl ()
                    | Some (`Continue, _, hd) ->
                        str tab ++ str ("continue " ^ label_of hd) ++ fnl ()
                    | Some (`Break, 0, _) -> str tab ++ str "break" ++ fnl ()
                    | Some (`Break, _, hd) ->
                        str tab ++ str ("break " ^ label_of hd) ++ fnl ()
                    | None ->
                        if j = stop then mt ()
                        else emit_region j stop loopctx tail tab
                  and walk b stop loopctx tail tab benv =
                    let h, args = collect_app b [] in
                    let vis = List.filter (fun a -> not (is_erased a)) args in
                    match h, vis with
                    | MLglob r, [action; k] when is_bind_ref r ->
                        let ids, kbody = collect_lam k in
                        emit_action state hoists tab benv ids action
                        ++ walk kbody stop loopctx tail tab (List.rev ids @ benv)
                    | MLglob r, [next] when is_ret_ref r ->
                        (match next with
                         | MLcons (_, c, [nn]) when is_jump_ctor c ->
                             (match nat_value nn with
                              | Some j -> handle_edge j stop loopctx tail tab
                              | None -> unsupported "a structured run_blocks Jump to a non-literal block target (the goto label must be statically known; it would silently become `return`, truncating control flow)")
                         | MLcons (_, c, []) when is_done_ctor c -> str tab ++ str "return" ++ fnl ()
                         | _ -> unsupported "a run_blocks block terminator (structured emitter) that is neither Jump nor Done — an unrecognized Next value would silently become `return`")
                    | _ ->
                        (match b with
                         | MLcase (_, scrut, branches) ->
                             (match Array.to_list branches with
                              | [ (_, p1, body1); (_, p2, body2) ] ->
                                  let cref p =
                                    (match p with Pusual r | Pcons (r, _) -> Some r | _ -> None) in
                                  (match cref p1, cref p2 with
                                   | Some c1, Some c2
                                     when (is_bool_true c1 && is_bool_false c2)
                                       || (is_bool_false c1 && is_bool_true c2) ->
                                       let tb, eb =
                                         if is_bool_true c1 then body1, body2 else body2, body1 in
                                       let m_raw = merge_of tb eb in
                                       (* a merge that is the loop-around (any
                                          enclosing header) or an enclosing loop's
                                          primary exit is reached by [handle_edge]
                                          as a (labeled) continue/break, so there is
                                          no in-region merge to emit after the if *)
                                       let escapes =
                                         List.exists (fun (hh, ee) -> hh = m_raw || ee = Some m_raw)
                                           loopctx in
                                       let m = if m_raw = stop || escapes then stop else m_raw in
                                       let then_empty = bare_jump tb = Some m in
                                       let else_empty = bare_jump eb = Some m in
                                       let branch_tail = tail && (m = stop || m = exit_node) in
                                       let bw bb = walk bb m loopctx branch_tail (tab ^ "\t") benv in
                                       let cond = pp_expr state benv scrut in
                                       let arms =
                                         if then_empty && else_empty then mt ()
                                         else if then_empty then
                                           str tab ++ str "if !" ++ pp_atom state benv scrut ++ str " {" ++ fnl ()
                                           ++ bw eb ++ str tab ++ str "}" ++ fnl ()
                                         else if else_empty then
                                           str tab ++ str "if " ++ cond ++ str " {" ++ fnl ()
                                           ++ bw tb ++ str tab ++ str "}" ++ fnl ()
                                         else
                                           str tab ++ str "if " ++ cond ++ str " {" ++ fnl ()
                                           ++ bw tb ++ str tab ++ str "} else {" ++ fnl ()
                                           ++ bw eb ++ str tab ++ str "}" ++ fnl () in
                                       arms ++ emit_region m stop loopctx tail tab
                                   | _ -> unsupported "a run_blocks conditional whose 2-branch match is not on a bool (true/false) — the structured emitter only lowers boolean if/else; a non-bool match would silently become `return`")
                              | _ -> unsupported "a run_blocks conditional whose match does not have exactly 2 branches — would silently become `return`, dropping the other arms")
                         | _ -> unsupported "a run_blocks block of an unrecognized shape (not bind / ret / boolean if-else) — would be emitted as a bare expression with NO control terminator, silently dropping its control flow")
                  in
                  if structurable
                  then hoist_doc ++ emit_region start_v exit_node [] true tab
                  else emit_raw ()
                  end
              | None ->
                  unsupported "run_blocks with a non-literal block list (the CFG blocks must be statically known; emitting only a comment would silently DROP all control flow)")
         | MLglob r, [m; h] when named "catch" r ->
             let ids, h_body = collect_lam h in
             let new_env = List.rev ids @ env in
             let p_id = match List.filter (fun id -> not (is_dummy id)) ids with
               | [id] -> pp_mlident id
               | _    -> str "_p"
             in
             let t = str tab in
             t ++ str "func() {" ++ fnl () ++
             t ++ str "\tdefer func() {" ++ fnl () ++
             t ++ str "\t\tif " ++ p_id ++ str " := recover(); " ++ p_id ++ str " != nil {" ++ fnl () ++
             pp_stmts (tab ^ "\t\t\t") new_env h_body ++
             t ++ str "\t\t}" ++ fnl () ++
             t ++ str "\t}()" ++ fnl () ++
             pp_stmts (tab ^ "\t") env m ++
             t ++ str "}()" ++ fnl ()
         (* go_spawn body (not inside bind — uncommon but valid) *)
         | MLglob r, [goroutine_body] when is_go_spawn_ref r ->
             str tab ++ str "go func() {" ++ fnl () ++
             pp_stmts (tab ^ "\t") env goroutine_body ++
             str tab ++ str "}()" ++ fnl ()
         (* defer_call f → defer func(){ f }() (function-scoped, like Go's defer) *)
         | MLglob r, [defer_body] when is_defer_call_ref r ->
             str tab ++ str "defer func() {" ++ fnl () ++
             pp_stmts (tab ^ "\t") env defer_body ++
             str tab ++ str "}()" ++ fnl ()
         (* recv_ok tag ch (fun x ok => body) → x, ok := <-ch; body.  An UNUSED
            result slot is rendered "_", detected by de Bruijn FREENESS of the body
            (NOT by [is_dummy]/name): the extractor may leave an unused binder named —
            e.g. it reuses the value binder's name for an anonymous [ok], which would
            emit the uncompilable [x, x := <-ch].  [ok] is the innermost binder
            (de Bruijn 1 in the body), [x] the next (de Bruijn 2). *)
         | MLglob r, [_tag; ch; kont] when is_recv_ok_ref r ->
             let ids, k_body = collect_lam kont in
             let new_env = List.rev ids @ env in
             (match ids with
              | [x_id; ok_id] ->
                  let x_unused  = dbn_free 2 k_body in
                  let ok_unused = dbn_free 1 k_body in
                  let x  = if x_unused  then str "_" else pp_mlident x_id in
                  let ok = if ok_unused then str "_" else pp_mlident ok_id in
                  (if x_unused && ok_unused
                   then str tab ++ str "<-" ++ pp_expr state env ch ++ fnl ()
                   else str tab ++ x ++ str ", " ++ ok ++ str " := <-"
                          ++ pp_expr state env ch ++ fnl ())
                  ++ pp_stmts tab new_env k_body
              (* review R2: the continuation is not an inline 2-arg lambda (a named /
                 separately-extracted / eta-reduced handler), so [collect_lam] did not
                 expose its [x ok] binders.  Emitting `_, _ = <-ch` DISCARDS the
                 continuation body — fail loud instead of silently dropping it. *)
              | _ ->
                  unsupported "a comma-ok receive (recv_ok) whose continuation is not an inline `fun x ok => …` lambda (e.g. a named or separately-extracted handler) — its body cannot be lowered inline; emitting `_, _ = <-ch` would silently DISCARD it.  Inline the handler as a literal two-argument lambda")
         (* select_recv2 ta ch1 k1 tb ch2 k2 → Go select with two recv cases.
            Each continuation [fun x => body] becomes [case x := <-ch: body].
            The non-deterministic CHOICE is Go's at runtime (faithful lowering). *)
         | MLglob r, [_ta; ch1; k1; _tb; ch2; k2] when is_select_recv2_ref r ->
             let recv_case ch kont =
               let ids, body = collect_lam kont in
               (match ids with
                | [v] ->   (* exactly one lambda peeled: an inline `fun v => …` *)
                    let new_env = List.rev ids @ env in
                    let x = if is_dummy v then str "_" else pp_mlident v in
                    str (tab ^ "\t") ++ str "case " ++ x ++ str " := <-"
                      ++ pp_expr state env ch ++ str ":" ++ fnl () ++
                    pp_stmts (tab ^ "\t\t") new_env body
                | _ ->
                    unsupported "select_recv2 receive continuation is not an inline single-argument `fun v => …` lambda (e.g. a NAMED handler Coq did not inline): the received value would not reach it.  Inline the handler (review #4 P0 2)") in
             str tab ++ str "select {" ++ fnl () ++
             recv_case ch1 k1 ++ recv_case ch2 k2 ++
             str tab ++ str "}" ++ fnl ()
         (* select_recv_default ta ch1 k1 d → Go select with one recv + default
            (the non-blocking form: recv-and-k1 if ready, else run d). *)
         | MLglob r, [_ta; ch1; k1; d] when is_select_recv_default_ref r ->
             let ids, body = collect_lam k1 in
             (match ids with
              | [v] ->   (* exactly one lambda peeled: an inline `fun v => …` *)
                  let new_env = List.rev ids @ env in
                  let x = if is_dummy v then str "_" else pp_mlident v in
                  str tab ++ str "select {" ++ fnl () ++
                  str (tab ^ "\t") ++ str "case " ++ x ++ str " := <-"
                    ++ pp_expr state env ch1 ++ str ":" ++ fnl () ++
                  pp_stmts (tab ^ "\t\t") new_env body ++
                  str (tab ^ "\t") ++ str "default:" ++ fnl () ++
                  pp_stmts (tab ^ "\t\t") env d ++
                  str tab ++ str "}" ++ fnl ()
              | _ ->
                  unsupported "select_recv_default receive continuation is not an inline single-argument `fun v => …` lambda (e.g. a NAMED handler Coq did not inline): the received value would not reach it.  Inline the handler (review #4 P0 2)")
         (* slice_at_ok tag xs i (fun v ok => body) → bounds-checked index:
              var v T
              ok := i < int64(len(xs))   (* i is uint63 >= 0; only upper bound *)
              if ok { v = xs[i] }
              body
            Each binder is emitted only when used; v defaults to its zero value
            when out of bounds, so no panic is possible. *)
         | MLglob r, [tag; xs; i; kont] when is_slice_at_ok_ref r ->
             let ids, k_body = collect_lam kont in
             let new_env = List.rev ids @ env in
             (match ids with
              | [v_id; ok_id] ->
                  let go_t = go_type_of_tag tag in
                  let xs_d = pp_expr state env xs in
                  let i_d  = pp_expr state env i in
                  (* [int] is SIGNED (Sint63), so a negative index is possible and
                     Go panics on it — the bounds check must cover BOTH ends, else
                     a negative [i] passes [i < len] and [xs[i]] panics, breaking
                     the "cannot panic out of bounds" guarantee. *)
                  let cond = i_d ++ str " >= 0 && " ++ i_d
                             ++ str " < len(" ++ xs_d ++ str ")" in
                  let v_decl =
                    if is_dummy v_id then mt ()
                    else str tab ++ str "var " ++ pp_mlident v_id ++ str " "
                         ++ str go_t ++ fnl () in
                  let ok_bind =
                    if is_dummy ok_id then mt ()
                    else str tab ++ pp_mlident ok_id ++ str " := " ++ cond ++ fnl () in
                  let guard = if is_dummy ok_id then cond else pp_mlident ok_id in
                  let if_block =
                    if is_dummy v_id then mt ()
                    else
                      str tab ++ str "if " ++ guard ++ str " {" ++ fnl () ++
                      str (tab ^ "\t") ++ pp_mlident v_id ++ str " = "
                        ++ xs_d ++ str "[" ++ i_d ++ str "]" ++ fnl () ++
                      str tab ++ str "}" ++ fnl () in
                  v_decl ++ ok_bind ++ if_block ++ pp_stmts tab new_env k_body
              | _ ->
                  unsupported "comma-ok CPS intrinsic (slice_at_ok / arr_get_ok / ptr_get_ok / str_at_ok / type_assert_safe) whose continuation is not an inline two-argument `fun v ok => …` lambda (e.g. a NAMED or separately-extracted handler that Coq did not inline): emitting only the continuation body would silently DROP the checked operation and its v/ok results.  Inline the handler as a literal two-argument lambda (review #4 P0 2 — the recv_ok continuation-loss class)")
         (* ptr_get_ok tag p (fun v ok => body) → nil-checked deref (Phase B1b):
              var v T
              ok := p != nil
              if ok { v = *p }
              body
            Mirrors slice_at_ok; [v] defaults to the zero value when [p] is nil, and
            the deref [*p] runs only in the [ok] branch — so the nil-deref panic is
            unreachable (the ok=false case is forced on the caller). *)
         | MLglob r, [tag; p; kont] when is_ptr_get_ok_ref r ->
             let ids, k_body = collect_lam kont in
             let new_env = List.rev ids @ env in
             (match ids with
              | [v_id; ok_id] ->
                  let go_t = go_type_of_tag (strip_magic tag) in
                  let p_d = pp_atom state env p in
                  let cond = p_d ++ str " != nil" in
                  let v_decl =
                    if is_dummy v_id then mt ()
                    else str tab ++ str "var " ++ pp_mlident v_id ++ str " "
                         ++ str go_t ++ fnl () in
                  let ok_bind =
                    if is_dummy ok_id then mt ()
                    else str tab ++ pp_mlident ok_id ++ str " := " ++ cond ++ fnl () in
                  let guard = if is_dummy ok_id then cond else pp_mlident ok_id in
                  let if_block =
                    if is_dummy v_id then mt ()
                    else
                      str tab ++ str "if " ++ guard ++ str " {" ++ fnl () ++
                      str (tab ^ "\t") ++ pp_mlident v_id ++ str " = *" ++ p_d ++ fnl () ++
                      str tab ++ str "}" ++ fnl () in
                  v_decl ++ ok_bind ++ if_block ++ pp_stmts tab new_env k_body
              | _ ->
                  unsupported "comma-ok CPS intrinsic (slice_at_ok / arr_get_ok / ptr_get_ok / str_at_ok / type_assert_safe) whose continuation is not an inline two-argument `fun v ok => …` lambda (e.g. a NAMED or separately-extracted handler that Coq did not inline): emitting only the continuation body would silently DROP the checked operation and its v/ok results.  Inline the handler as a literal two-argument lambda (review #4 P0 2 — the recv_ok continuation-loss class)")
         (* str_at_ok s i (fun b ok => body) → bounds-checked byte index:
              var b int64
              ok := i >= 0 && i < int64(len(s))
              if ok { b = int64(s[i]) }   (* s[i] is byte; widen to the int64 carrier *)
              body
            Mirrors slice_at_ok; the byte is a GoByte (= uint8, GoU8) erased to
            int64.  Cannot panic out of range (the ok-branch is forced). *)
         | MLglob r, [s; i; kont] when is_str_at_ok_ref r ->
             let ids, k_body = collect_lam kont in
             let new_env = List.rev ids @ env in
             (match ids with
              | [b_id; ok_id] ->
                  let s_d = pp_expr state env s in
                  let i_d = pp_expr state env i in
                  let cond = i_d ++ str " >= 0 && " ++ i_d
                             ++ str " < len(" ++ s_d ++ str ")" in
                  let b_decl =
                    if is_dummy b_id then mt ()
                    else str tab ++ str "var " ++ pp_mlident b_id ++ str " int64" ++ fnl () in
                  let ok_bind =
                    if is_dummy ok_id then mt ()
                    else str tab ++ pp_mlident ok_id ++ str " := " ++ cond ++ fnl () in
                  let guard = if is_dummy ok_id then cond else pp_mlident ok_id in
                  let if_block =
                    if is_dummy b_id then mt ()
                    else
                      str tab ++ str "if " ++ guard ++ str " {" ++ fnl () ++
                      str (tab ^ "\t") ++ pp_mlident b_id ++ str " = int64("
                        ++ s_d ++ str "[" ++ i_d ++ str "])" ++ fnl () ++
                      str tab ++ str "}" ++ fnl () in
                  b_decl ++ ok_bind ++ if_block ++ pp_stmts tab new_env k_body
              | _ ->
                  unsupported "comma-ok CPS intrinsic (slice_at_ok / arr_get_ok / ptr_get_ok / str_at_ok / type_assert_safe) whose continuation is not an inline two-argument `fun v ok => …` lambda (e.g. a NAMED or separately-extracted handler that Coq did not inline): emitting only the continuation body would silently DROP the checked operation and its v/ok results.  Inline the handler as a literal two-argument lambda (review #4 P0 2 — the recv_ok continuation-loss class)")
         (* type_assert_safe tag x (fun v ok => body) → v, ok := x.(T); body
            Go's native two-value assertion: ok=false (no panic) on mismatch. *)
         | MLglob r, [tag; x; kont] when is_type_assert_safe_ref r ->
             let go_t = go_type_of_tag tag in
             let inner =
               match strip_magic x with
               | MLcons (_, r2, _) when is_existT_ref r2 ->
                   (* fresh box: materialize as any(<narrow-converted payload>) — see type_assert above *)
                   let pv = pp_expr state env (any_payload x) in
                   let conv = (match narrow_conv_of env x with Some gt -> str (gt ^ "(") ++ pv ++ str ")" | None -> pv) in
                   str "any(" ++ conv ++ str ")"
               | _ -> pp_expr state env (any_payload x)
             in
             let ids, k_body = collect_lam kont in
             let new_env = List.rev ids @ env in
             (match ids with
              | [v_id; ok_id] ->
                  (* blank an UNUSED binder ([_]) so Go doesn't reject a "declared and not used" local —
                     the comma-ok idiom [_, ok := x.(T)] (check the type, ignore the value) is common.  In
                     [k_body] the innermost binder [ok_id] is de Bruijn 1, [v_id] is 2. *)
                  let v_used  = not (is_dummy v_id)  && not (dbn_free 2 k_body) in
                  let ok_used = not (is_dummy ok_id) && not (db1_free k_body) in
                  if not v_used && not ok_used then
                    (* both results discarded — the two-value assertion is pure (no panic), so drop it *)
                    pp_stmts tab new_env k_body
                  else
                    str tab ++ (if v_used then pp_mlident v_id else str "_")
                    ++ str ", " ++ (if ok_used then pp_mlident ok_id else str "_")
                    ++ str " := " ++ inner ++ str ".(" ++ str go_t ++ str ")" ++ fnl ()
                    ++ pp_stmts tab new_env k_body
              | _ ->
                  unsupported "comma-ok CPS intrinsic (slice_at_ok / arr_get_ok / ptr_get_ok / str_at_ok / type_assert_safe) whose continuation is not an inline two-argument `fun v ok => …` lambda (e.g. a NAMED or separately-extracted handler that Coq did not inline): emitting only the continuation body would silently DROP the checked operation and its v/ok results.  Inline the handler as a literal two-argument lambda (review #4 P0 2 — the recv_ok continuation-loss class)")
         (* type_switchN a t1 k1 … tN kN d → Go's native type switch:
              switch _tsv := <a>.(type) {
              case T1: v1 := _tsv; <k1 body>   …   case TN: vN := _tsv; <kN body>
              default: <d> }
            CPS like type_assert_safe but multi-case, ANY arity: the visible args after
            [a] are (tag, continuation) PAIRS then the default [d] (so an odd count ≥ 3).
            Each kᵢ binds the case-typed value, rebound from the shared guard [_tsv] (so
            distinct binder names need no env renaming).  The guard var is omitted if no
            case uses its value (else Go errors "_tsv declared but not used"). *)
         | MLglob r, (a :: rest)
            when is_type_switch_ref r && not (is_type_switch_or_ref r)
                 && List.length rest >= 3 && List.length rest mod 2 = 1 ->
             (* the scrutinee must be an INTERFACE for [.(type)].  A bare variable [a:GoAny] already
                is; an INLINE [any x] (existT) would strip to its raw payload (a non-interface), so
                RE-BOX it as [any(<narrow-converted payload>)] — exactly the [type_assert] handling. *)
             let payload =
               match strip_magic a with
               | MLcons (_, r2, _) when is_existT_ref r2 ->
                   let pv = pp_expr state env (any_payload a) in
                   let conv = (match narrow_conv_of env a with
                               | Some gt -> str (gt ^ "(") ++ pv ++ str ")" | None -> pv) in
                   str "any(" ++ conv ++ str ")"
               | _ -> pp_expr state env (any_payload a) in
             let n = List.length rest in
             let d = List.nth rest (n - 1) in
             let rec chunk = function t :: k :: tl -> (t, k) :: chunk tl | _ -> [] in
             let pairs = chunk (List.filteri (fun i _ -> i < n - 1) rest) in
             let case_used kont =
               match collect_lam kont with ([v], _) -> not (is_dummy v) | _ -> false in
             let any_used = List.exists (fun (_, k) -> case_used k) pairs in
             let emit_case (tagd, kont) =
               let go_t = go_type_of_tag tagd in
               let ids, kbody = collect_lam kont in
               (match ids with
                | [v_id] ->
                    str tab ++ str "case " ++ str go_t ++ str ":" ++ fnl () ++
                    (if is_dummy v_id then mt ()
                     else str (tab ^ "\t") ++ pp_mlident v_id ++ str " := _tsv" ++ fnl ()) ++
                    pp_stmts (tab ^ "\t") (v_id :: env) kbody
                | _ -> unsupported "type_switch case (expected exactly one value binder)")
             in
             str tab ++ str "switch " ++
             (if any_used then str "_tsv := " else mt ()) ++
             payload ++ str ".(type) {" ++ fnl () ++
             prlist emit_case pairs ++
             str tab ++ str "default:" ++ fnl () ++
             pp_stmts (tab ^ "\t") env d ++
             str tab ++ str "}" ++ fnl ()
         (* {int,str}_switchN x v1 k1 … vN kN d → Go's native EXPRESSION switch:
              switch <x> { case v1: <k1> … case vN: <kN> default: <d> }
            args after the scrutinee are (value, body) PAIRS then the default (odd count
            ≥ 3); each case body is a plain thunk (no binder), so [pp_stmts] directly. *)
         | MLglob r, (x :: rest)
            when is_val_switch_ref r
                 && List.length rest >= 3 && List.length rest mod 2 = 1 ->
             let scrut = pp_expr state env x in
             let n = List.length rest in
             let d = List.nth rest (n - 1) in
             let rec chunk = function v :: k :: tl -> (v, k) :: chunk tl | _ -> [] in
             let pairs = chunk (List.filteri (fun i _ -> i < n - 1) rest) in
             str tab ++ str "switch " ++ scrut ++ str " {" ++ fnl () ++
             prlist (fun (v, k) ->
               str tab ++ str "case " ++ pp_expr state env v ++ str ":" ++ fnl () ++
               pp_stmts (tab ^ "\t") env k) pairs ++
             str tab ++ str "default:" ++ fnl () ++
             pp_stmts (tab ^ "\t") env d ++
             str tab ++ str "}" ++ fnl ()
         (* type_switch_orN a t1 … tN k d → multi-type case (Go's [case T1, …, TN:]).  The
            value is not narrowed, so no guard var / per-case rebind: the body [k] is a
            plain thunk run when the type is T1 OR … OR TN.  Args after [a] are the N tags
            (N ≥ 2), then the thunk, then the default. *)
         | MLglob r, (a :: rest)
            when is_type_switch_or_ref r && List.length rest >= 4 ->
             let payload = pp_expr state env (any_payload a) in
             let n = List.length rest in
             let k = List.nth rest (n - 2) in
             let d = List.nth rest (n - 1) in
             let tags = List.filteri (fun i _ -> i < n - 2) rest in
             str tab ++ str "switch " ++ payload ++ str ".(type) {" ++ fnl () ++
             str tab ++ str "case " ++
             prlist_with_sep (fun () -> str ", ")
               (fun t -> str (go_type_of_tag t)) tags ++ str ":" ++ fnl () ++
             pp_stmts (tab ^ "\t") env k ++
             str tab ++ str "default:" ++ fnl () ++
             pp_stmts (tab ^ "\t") env d ++
             str tab ++ str "}" ++ fnl ()
         (* run_session client server → _sess_ch := make(chan any);
                                        go func(){ <server> }(); <client>
            The indexed-monad roles share one [chan any]; each role's body is
            lowered by [pp_sess_stmts].  ([Proto] index is present, so the two
            roles are the last two visible args.) *)
         | MLglob r, vis when is_run_session_ref r && List.length vis >= 2 ->
             let n = List.length vis in
             let client = List.nth vis (n - 2) in
             let server = List.nth vis (n - 1) in
             str tab ++ str "_sess_ch := make(chan any)" ++ fnl () ++
             str tab ++ str "go func() {" ++ fnl () ++
             pp_sess_stmts (tab ^ "\t") env server ++
             str tab ++ str "}()" ++ fnl () ++
             pp_sess_stmts tab env client
         | _ ->
             str tab ++ pp_expr state env e ++ fnl ())
    | MLletin (id, e1, e2) ->
        let new_env = id :: env in
        record_narrow_binding env id e1;
        (* map_get_or k default m → two-statement pattern: no IIFE *)
        let head1, all_args1 = collect_app e1 [] in
        let vis1 = List.filter (fun a -> not (is_erased a)) all_args1 in
        (match head1, vis1 with
         | MLglob r, [kt; vt; k; def; m] when is_map_get_or_ref r && not (is_dummy id) ->
             let lhs = pp_mlident id in
             (* narrow value default boxes as its narrow Go type (else it would box as int64 and a
                later [.(uint8)] would disagree with the model); narrow KEY cast too — review #4 P1 #4. *)
             str tab ++ str "var " ++ lhs ++ str " any = "
               ++ pp_payload_at_tag state env vt def ++ fnl () ++
             str tab ++ str "if _v, _ok := " ++ pp_expr state env m ++
             str "[" ++ pp_narrow_or_expr state env kt k ++ str "]; _ok {" ++ fnl () ++
             str tab ++ str "\t" ++ lhs ++ str " = _v" ++ fnl () ++
             str tab ++ str "}" ++ fnl () ++
             pp_stmts tab new_env e2
         (* let acc := slice_fold xs init step in body →
              acc := init
              for _, x := range xs { acc = step acc x }
              body
            The accumulator reuses the let-bound Go variable [id]; [step]'s two
            binders map to (acc=id, x=loop var). *)
         | MLglob r, [xs; init; step] when is_slice_fold_ref r && not (is_dummy id) ->
             let step_ids, step_body = collect_lam step in
             (match step_ids with
              | [_acc; x_id] ->
                  let acc = pp_mlident id in
                  (* step_body env: MLrel 1 = x, MLrel 2 = acc (= id); any captured
                     outer vars sit past both binders in [env]. *)
                  let loop_env = x_id :: id :: env in
                  str tab ++ acc ++ str " := " ++ pp_typed_lit state env init ++ fnl () ++
                  str tab ++ str "for _, " ++ pp_mlident x_id ++ str " := range "
                    ++ pp_expr state env xs ++ str " {" ++ fnl () ++
                  str (tab ^ "\t") ++ acc ++ str " = "
                    ++ pp_expr state loop_env step_body ++ fnl () ++
                  str tab ++ str "}" ++ fnl () ++
                  pp_stmts tab new_env e2
              | _ ->
                  str tab ++ pp_mlident id ++ str " := " ++ pp_expr state env e1
                    ++ fnl () ++ pp_stmts tab new_env e2)
         | _ ->
             let lhs = if is_dummy id || is_void_call e1 then mt ()
                       else pp_mlident id ++ str " := " in
             str tab ++ lhs ++ pp_expr state env e1 ++ fnl () ++
             pp_stmts tab new_env e2)
    (* Case analysis in statement position (tail of an IO body). *)
    | MLcase (typ, scrut, branches) ->
        (match Array.to_list branches with
         (* multiple-return DESTRUCTURE [let '(x,y) := f … in body] (single pair branch,
            2 binders) → [x, y := f(…)] then the rest. *)
         | [ (ids, pat, body1) ]
           when (match pat with Pcons (r, _) | Pusual r -> is_pair_ref r | _ -> false)
                && List.length ids = 2 ->
             let flat_ids, fbody, fenv = flatten_destructure ids body1 env in   (* N-ary: x, y, z := f() *)
             str tab ++ prlist_with_sep (fun () -> str ", ") (pp_destr_binder fenv fbody) flat_ids
             ++ str " := " ++ pp_expr state env scrut ++ fnl ()
             ++ pp_stmts tab fenv fbody
         | _ -> emit_case tab env typ scrut branches (fun _nb b -> b))
    | MLcons (_, r, []) when is_unit_tt r -> mt ()
    (* Bare nullary IO function in tail position — needs () to be a call,
       not a function value. *)
    | MLglob r as e
      when not (is_bool_true r || is_bool_false r
                || is_unit_tt r || is_map_make_ref r) ->
        str tab ++ pp_expr state env e ++ str "()" ++ fnl ()
    | e ->
        str tab ++ pp_expr state env e ++ fnl ()

  (* Emit an [MLcase] as Go statements.  [mk_body] transforms each arm's body
     before it is printed: the identity in tail position, or "wrap in [bind …
     k]" when a continuation must be threaded into every arm.  Only [bool]
     matches (i.e. [if]/[else]) are lowered for now; richer inductives and
     type switches follow. *)
  and emit_case tab env typ scrut branches mk_body =
    (* Branch binders are stored in constructor order [arg0; …; argN]; the body
       env is [List.rev ids @ env] (innermost binder = last arg = MLrel 1),
       matching the lambda convention used elsewhere. *)
    let ctor_of (ids, pat, body) =
      match pat with
      | Pusual r | Pcons (r, _)   -> (Some r, ids, body)
      | Pwild | Prel _ | Ptuple _ -> (None, ids, body)
    in
    let unhandled () =
      unsupported "a match of this shape in statement position (only if/bool, option comma-ok, and list nil/cons are modeled)"
    in
    (* Emit a two-arm if/else; [pre] (e.g. an "x, ok :=" init clause) precedes
       the condition, and [tenv]/[fenv] are the branch environments. *)
    let if_else pre cond (tbody, tn, tenv) (fbody, fn, fenv) =
      str tab ++ str "if " ++ pre ++ cond ++ str " {" ++ fnl () ++
      pp_stmts (tab ^ "\t") tenv (mk_body tn tbody) ++
      str tab ++ str "} else {" ++ fnl () ++
      pp_stmts (tab ^ "\t") fenv (mk_body fn fbody) ++
      str tab ++ str "}" ++ fnl ()
    in
    (* (Historical note: a stale [is_string_list] guard excluded a [list GoInt32] from the list/slice
       match — from the OBSOLETE [GoString := list GoRune] model.  [GoString] is now Coq [string] (a
       byte sequence, matched by its own arm) and the [GoInt32] placeholder is RETIRED (review #4
       P0 #1); rune slices are the FAITHFUL [list GoI32] and are LEGITIMATE slices that DO take the
       list lowering.  The guard tested a now-nonexistent type ⇒ structurally always-false ⇒ removed.) *)
    (* ENUM match (all arms are nullary enum constructors, any arity ≥ 2) → Go [switch].
       Checked before the 2-arm shapes so a 2-value enum also switches.  A source `_`
       wildcard needs no special handling: Coq EXPANDS it into the missing constructors
       (→ an all-cases switch), so it never reaches the plugin as a [Pwild] for an enum. *)
    let all_enum_arms brs =
      List.length brs >= 2 &&
      List.for_all (fun b -> match ctor_of b with
        | (Some c, _, _) -> is_enum_ctor c | _ -> false) brs in
    match Array.to_list branches with
    | brs when all_enum_arms brs ->
        str tab ++ str "switch " ++ pp_expr state env scrut ++ str " {" ++ fnl () ++
        prlist (fun b ->
          let (rc, ids, body) = ctor_of b in
          let cname = match rc with Some c -> go_export (global_basename c) | None -> "_" in
          str tab ++ str "case " ++ str cname ++ str ":" ++ fnl () ++
          pp_stmts (tab ^ "\t") env (mk_body (List.length ids) body)) brs ++
        str tab ++ str "}" ++ fnl ()
    | [ b1; b2 ] ->
        let (r1, ids1, body1) = ctor_of b1 in
        let (r2, ids2, body2) = ctor_of b2 in
        (match r1, r2 with
         (* if/else: match on bool *)
         | Some c1, Some c2
           when (is_bool_true c1 && is_bool_false c2)
             || (is_bool_false c1 && is_bool_true c2) ->
             let then_b, else_b =
               if is_bool_true c1 then body1, body2 else body2, body1 in
             if_else (mt ()) (pp_expr state env scrut)
               (then_b, 0, env) (else_b, 0, env)
         (* option: match on [map_get_opt k m] → comma-ok lookup.  The Some arm
            binds the value; the None arm is the absent case.  Only the
            map-lookup producer is fused; a free [option] value is not yet
            representable. *)
         | Some c1, Some c2
           when (is_some_ctor c1 && is_none_ctor c2)
             || (is_none_ctor c1 && is_some_ctor c2) ->
             let some_ids, some_body, none_body =
               if is_some_ctor c1 then ids1, body1, body2
               else ids2, body2, body1 in
             let sh, sargs = collect_app (strip_magic scrut) [] in
             let svis = List.filter (fun a -> not (is_erased a)) sargs in
             (match sh, svis with
              | MLglob r, [kt; _vt; k; m] when is_map_get_opt_ref r ->
                  let v_doc = match some_ids with
                    | [id] when not (is_dummy id) -> pp_mlident id
                    | _ -> str "_" in
                  let pre =
                    v_doc ++ str ", _ok := " ++ pp_expr state env m ++
                    str "[" ++ pp_narrow_or_expr state env kt k ++ str "]; " in
                  if_else pre (str "_ok")
                    (some_body, List.length some_ids, List.rev some_ids @ env)
                    (none_body, 0, env)
              | _ -> unhandled ())
         (* nat: [match n with O => … | S k => …] → [if n == 0 { … } else { k := n - 1; … }].
            Enables structural recursion (a self-calling Go func) and nat case analysis.  [n]
            is a nat → Go [uint]; [O] is the zero test, [S k] binds the predecessor [k := n - 1]
            (reachable only when n != 0, so the [uint] subtraction never underflows). *)
         | Some c1, Some c2
           when (is_nat_zero c1 && is_nat_succ c2)
             || (is_nat_succ c1 && is_nat_zero c2) ->
             let zero_body, succ_ids, succ_body =
               if is_nat_zero c1 then body1, ids2, body2
               else body2, ids1, body1 in
             let pred_id = match succ_ids with [k] -> k | _ -> Dummy in
             let n = pp_expr state env scrut in
             let prefix =
               if is_dummy pred_id then mt ()
               else str (tab ^ "\t") ++ pp_mlident pred_id ++ str " := " ++ n ++ str " - 1" ++ fnl () in
             str tab ++ str "if " ++ n ++ str " == 0 {" ++ fnl () ++
             pp_stmts (tab ^ "\t") env (mk_body 0 zero_body) ++
             str tab ++ str "} else {" ++ fnl () ++
             prefix ++
             pp_stmts (tab ^ "\t") (List.rev succ_ids @ env)
               (mk_body (List.length succ_ids) succ_body) ++
             str tab ++ str "}" ++ fnl ()
         (* list / slice: [match xs with [] | x :: rest] → len / index / reslice.
            A [GoString] (byte sequence) is Coq [string], matched by its own arm above, never here. *)
         | Some c1, Some c2
           when ((is_list_nil c1 && is_list_cons c2)
              || (is_list_cons c1 && is_list_nil c2)) ->
             let nil_body, cons_ids, cons_body =
               if is_list_nil c1 then body1, ids2, body2
               else body2, ids1, body1 in
             let head_id, tail_id = match cons_ids with
               | [h; t] -> h, t
               | _      -> Dummy, Dummy in
             let xs = pp_expr state env scrut in
             let prefix =
               (if is_dummy head_id then mt ()
                else str (tab ^ "\t") ++ pp_mlident head_id ++ str " := "
                     ++ xs ++ str "[0]" ++ fnl ()) ++
               (if is_dummy tail_id then mt ()
                else str (tab ^ "\t") ++ pp_mlident tail_id ++ str " := "
                     ++ xs ++ str "[1:]" ++ fnl ())
             in
             str tab ++ str "if len(" ++ xs ++ str ") == 0 {" ++ fnl () ++
             pp_stmts (tab ^ "\t") env (mk_body 0 nil_body) ++
             str tab ++ str "} else {" ++ fnl () ++
             prefix ++
             pp_stmts (tab ^ "\t") (List.rev cons_ids @ env)
               (mk_body (List.length cons_ids) cons_body) ++
             str tab ++ str "}" ++ fnl ()
         | _ -> unhandled ())
    | _ -> unhandled ()

  (* for_each xs (fun x => body) → for _, x := range xs { body } *)
  and emit_for_each tab env xs bodyfn =
    let ids, body = collect_lam bodyfn in
    let new_env = List.rev ids @ env in
    let hdr =
      match List.filter (fun id -> not (is_dummy id)) ids with
      | [x] -> str "for _, " ++ pp_mlident x ++ str " := range "
      | _   -> str "for range " in
    str tab ++ hdr ++ pp_expr state env xs ++ str " {" ++ fnl () ++
    pp_stmts (tab ^ "\t") new_env body ++
    str tab ++ str "}" ++ fnl ()
  (* Two-variable range [for i, x := range coll { body }] — shared by [str_range] (over a
     string: i = byte offset, x = rune) and [for_each_idx] (over a slice: i = index, x =
     element); the emitted form is identical, the differing Go semantics live in the model.
     An unused binder becomes [_] (Go forbids an unused range variable). *)
  and emit_range2 tab env coll bodyfn =
    let ids, body = collect_lam bodyfn in
    let new_env = List.rev ids @ env in
    let nm id = if is_dummy id then str "_" else pp_mlident id in
    let hdr =
      match ids with
      | [i; x] -> str "for " ++ nm i ++ str ", " ++ nm x ++ str " := range "
      | [i]    -> str "for " ++ nm i ++ str " := range "
      | _      -> str "for range " in
    str tab ++ hdr ++ pp_expr state env coll ++ str " {" ++ fnl () ++
    pp_stmts (tab ^ "\t") new_env body ++
    str tab ++ str "}" ++ fnl ()
  (* ---- Indexed-monad (linear) sessions ----
     A [Sess i j A] fragment lowers to sequential Go against the implicit shared
     channel [_sess_ch] (declared by [run_session]).  [sbind] is sequencing;
     [ssend v] → [_sess_ch <- v]; [srecv tag] → typed receive; [slift io] → the
     IO body; [sret] erases.  The protocol [Proto] index is computationally
     present (it is a [Type]-sorted inductive, not erased), so the real payload
     is the LAST visible argument. *)
  and emit_sess_action tab env ids action =
    let h, args = collect_app action [] in
    let vis = List.filter (fun a -> not (is_erased a)) args in
    let last () = List.nth vis (List.length vis - 1) in
    match h with
    | MLglob r when is_ssend_ref r ->
        str tab ++ str "_sess_ch <- " ++ pp_typed_lit state env (last ()) ++ fnl ()
    | MLglob r when is_srecv_ref r ->
        let go_t = go_type_of_tag (last ()) in
        (match List.filter (fun id -> not (is_dummy id)) ids with
         (* receive then type-assert in one expression — no temp, so consecutive
            receives in one scope don't collide on a [:=] re-declaration *)
         | [x] ->
             str tab ++ pp_mlident x ++ str (" := (<-_sess_ch).(" ^ go_t ^ ")") ++ fnl ()
         | _ -> str tab ++ str "<-_sess_ch" ++ fnl ())
    | MLglob r when is_slift_ref r -> pp_stmts tab env (last ())
    | MLglob r when is_sret_ref r ->
        (match List.filter (fun id -> not (is_dummy id)) ids with
         | [b] -> str tab ++ pp_mlident b ++ str " := " ++ pp_expr state env (last ()) ++ fnl ()
         | _   -> mt ())
    | _ -> pp_sess_stmts tab env action
  and pp_sess_stmts tab env e =
    let h, args = collect_app e [] in
    let vis = List.filter (fun a -> not (is_erased a)) args in
    match h with
    | MLglob r when is_sbind_ref r && List.length vis >= 2 ->
        let n = List.length vis in
        let m = List.nth vis (n - 2) and k = List.nth vis (n - 1) in
        let ids, body = collect_lam k in
        let new_env = List.rev ids @ env in
        emit_sess_action tab env ids m ++ pp_sess_stmts tab new_env body
    | MLglob r when is_ssend_ref r || is_srecv_ref r || is_sret_ref r ->
        emit_sess_action tab env [] e
    | MLglob r when is_slift_ref r ->
        pp_stmts tab env (List.nth vis (List.length vis - 1))
    | _ -> str tab ++ pp_expr state env e ++ fnl ()
  in
  let rec peel_catches tab env b =
    let head, all_args = collect_app b [] in
    let vis = List.filter (fun a -> not (is_erased a)) all_args in
    match head, vis with
    | MLglob r, [m; h] when named "catch" r ->
        let ids, h_body = collect_lam h in
        let new_env = List.rev ids @ env in
        let p_id = match List.filter (fun id -> not (is_dummy id)) ids with
          | [id] -> pp_mlident id
          | _    -> str "_p"
        in
        let t = str tab in
        t ++ str "defer func() {" ++ fnl () ++
        t ++ str "\tif " ++ p_id ++ str " := recover(); " ++ p_id ++ str " != nil {" ++ fnl () ++
        pp_stmts (tab ^ "\t\t") new_env h_body ++
        t ++ str "\t}" ++ fnl () ++
        t ++ str "}()" ++ fnl () ++
        peel_catches tab env m
    | _ -> pp_stmts tab env b
  in
  (* A value-returning IO function ([func … V], V ≠ unit) must [return] its tail value.
     [pp_stmts] emits IO as VOID statements (no return) — correct for [IO unit], but an
     IO method/function that yields a value needs [return].  The COMMON single-expression
     tails are handled here: [ret v] → [return v], and a clean value-read ([map_len]/[len]/
     [cap]) → [return <expr>].  Bind-chains / control-flow value tails are NOT yet handled
     (they fall through to the void emission, which go build rejects — loud, not silent). *)
  let ret_tail =
    if not ret_val then None
    else match collect_app body [] with
      | MLglob r, [v] when is_ret_ref r -> Some (pp_expr state env v)
      | MLglob r, _ when is_map_len_ref r || is_len_ref r || is_cap_ref r
                      || is_gofunc_call_ref r ->  (* gofunc_call f x → return f(x) (review #8) *)
          Some (pp_expr state env body)
      | _ -> None
  in
  match ret_tail with
  | Some d -> str tab ++ str "return " ++ d ++ fnl ()
  | None   -> peel_catches tab env body

