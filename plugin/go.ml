(* Fixed-width unsigned integer ops (builtins.v).  Carrier is int64; each op
   masks back to the width, e.g. [u8_add a b] → [((a + b) & 0xff)], matching Go's
   uint8 wrap.  Comparisons go through [binop_of]; the masked arithmetic and the
   literal/normaliser get explicit cases in [pp_expr]. *)
(* Parse a fixed-width integer op name [u<W>_op] / [i<W>_op] ([u]=unsigned,
   [i]=signed) into (signed, width, op).  One parser serves every width, so adding
   uint16/int16/uint32/… needs only the Rocq definitions — no plugin change.
   Width is capped at 32 (a 64-bit value exceeds the 63-bit carrier — that needs
   the Z-based model and is left to fail loud). *)
let parse_fixed_width n =
  let len = String.length n in
  if len < 4 || not (n.[0] = 'u' || n.[0] = 'i') then None
  else begin
    let i = ref 1 in
    while !i < len && n.[!i] >= '0' && n.[!i] <= '9' do incr i done;
    if !i = 1 || !i >= len || n.[!i] <> '_' then None
    else
      let op = String.sub n (!i + 1) (len - !i - 1) in
      (* only the KNOWN fixed-width ops — else [u8_demo] etc. would falsely match *)
      if not (List.mem op ["lit"; "add"; "sub"; "mul"; "eqb"; "ltb"; "leb"; "norm";
                           "gtb"; "geb"; "neqb";
                           "and"; "or"; "xor"; "andnot"; "not"; "shl"; "shr"; "of_int"; "of_i64";
                           "div"; "mod"])
      then None
      else match int_of_string_opt (String.sub n 1 (!i - 1)) with
        | Some width when width >= 1 && width <= 32 -> Some ((n.[0] = 'i'), width, op)
        | _ -> None
  end
let fixed_width_op r = if from_builtins r then parse_fixed_width (global_basename r) else None
let fw_is r op = match fixed_width_op r with Some (_, _, o) -> String.equal o op | None -> false

(* Break #7 — faithful narrow interface identity.  The sub-64-bit numeric narrows
   (GoU8…GoI32) are int64-CARRIED for arithmetic so the masked ops constant-FOLD cleanly
   (native [uint8(200)+uint8(100)] would be a Go constant-overflow error — Go does not wrap
   constant arithmetic).  But a boxed value's FAITHFUL Go dynamic type is its real narrow
   type: a boxed [GoU8] must answer [v.(uint8)], not [v.(int64)] — else [tag_eq] distinguishes
   what Go cannot.  The [any] tag is resolved through the [Tagged] class and its type index is
   ERASED in extraction (unrecoverable from the term), so we read the width from the PAYLOAD's
   head op: a value built by a fixed-width VALUE op (u8_add/u8_lit/u8_of_i64/…, NOT the bool
   predicates ltb/leb/eqb/gtb/geb/neqb) IS that narrow type, and is converted to it at the box
   ([uint8(x)] / [int8(x)] / …).  Sub-64-typed GLOBALS / vars (e.g. [uc_100_u8 : GoU8]) are
   already rendered at their real Go type by [pp_type], so they box faithfully on their own. *)
let fw_value_type r =
  match fixed_width_op r with
  | Some (signed, width, op)
    when width <= 32 &&
         not (List.mem op ["ltb"; "leb"; "eqb"; "gtb"; "geb"; "neqb"]) ->
      Some (Printf.sprintf "%s%d" (if signed then "int" else "uint") width)
  | _ -> None
(* Head constructor / global ref of a payload value term — peeling the [MLmagic]
   coercions extraction wraps around an [existT] payload (its value type is erased). *)
let rec payload_head t =
  match t with
  | MLmagic e        -> payload_head e
  | MLglob r         -> Some r
  | MLcons (_, r, _) -> Some r
  | MLapp (f, _)     -> payload_head f
  | _                -> None
(* [Some "uint8"] when [any]-wrapped [e] boxes a value produced by a sub-64 narrow op. *)
let any_narrow_conv e =
  Option.bind (payload_head (any_payload e)) fw_value_type

(* ---- P0 #2 (code review): narrow-typed LET-bound variables ----
   A sub-64 narrow value (GoU8…GoI32) is int64-CARRIED, so `let x := <narrow op>` binds x to a masked int64
   expression and Go infers `x : int` — LOSING the narrow type.  A later `any x` then sees only the variable
   (not the producing op), so [any_narrow_conv] returns None and the value boxes as Go `int`; `.(uint8)` then
   FAILS though the model says GoU8 (model/runtime contradiction).  Fix: record x ↦ its narrow Go type at the
   [let] and re-apply the conversion ([uint8(x)]) at the box.  Keyed by the Go var NAME (Coq's extracted names
   are function-locally unique) and RESET per function.  Preserves the int64-carrier model: x stays int-typed,
   only the box adds the cast (`uint8(int)` truncates to the right narrow value). *)
let narrow_var_types : (string, string) Hashtbl.t = Hashtbl.create 16
(* P0 #2 (narrow RETURN boundary): the current function's narrow Go return type, if its return type
   is a sub-64 narrow wrapper.  The body computes an int-CARRIER value, so a bare `return <expr>`
   would emit `return <int-expr>` against a declared `uint8`/`int8`/… signature — a Go type error.
   When [Some gt], [pp_pure_tail]'s return wraps the value in [gt(…)] (e.g. `return uint8(x & 0xff)`),
   value-preserving (the carrier is already masked into range) and giving the result its faithful Go
   type.  Set per function in [pp_function] from its return type; [None] (reset) for everything else. *)
let narrow_ret_type : string option ref = ref None
let mlident_name = function
  | Id v | Tmp v -> Some (go_safe (Id.to_string v))
  | Dummy -> None
(* The narrow Go type a raw (un-boxed) expression produces, if any (its payload head is a narrow VALUE op). *)
let narrow_type_of e = Option.bind (payload_head e) fw_value_type
(* The recorded narrow Go type of a bare variable [t = MLrel i], via [env].  Peel the [MLmagic]
   coercions extraction wraps around a boxed payload — else the [MLrel] is hidden and the var is unfound. *)
let narrow_var_of env t =
  let rec peel = function MLmagic e -> peel e | e -> e in
  match peel t with
  | MLrel i ->
      (try Option.bind (mlident_name (List.nth env (i - 1))) (Hashtbl.find_opt narrow_var_types)
       with _ -> None)
  | _ -> None
(* review #4 P1 #4 / generics: the narrow Go type to cast a value TO when it flows into a destination
   whose DECLARED type is NOT a concrete narrow — a generic type-PARAMETER position ([Box[T]{V}],
   [make_box(v)]).  Without it Go INFERS the type parameter from the int64-masked arg ([Box[int64]] for a
   [uint8] value), diverging model↔runtime.  Uses the VALUE's own narrow type (a narrow op result, or a
   recorded narrow let-var).  EXCLUDES an [existT]/any-box (its inner narrow is cast at the box, not here)
   — so it never mis-fires on a [GoAny] param. *)
let value_narrow_conv env e =
  let rec peel = function MLmagic x -> peel x | x -> x in
  match peel e with
  | MLcons (_, r, _) when is_existT_ref r -> None
  | _ -> (match narrow_type_of e with Some _ as g -> g | None -> narrow_var_of env e)
(* Record `let id := e1` if e1 yields a narrow value — directly (a narrow op) or by aliasing a narrow var. *)
let record_narrow_binding env id e1 =
  let gt = match narrow_type_of e1 with Some _ as g -> g | None -> narrow_var_of env e1 in
  match mlident_name id, gt with
  | Some name, Some g -> Hashtbl.replace narrow_var_types name g
  | _ -> ()
(* Narrow Go conversion for a boxed payload: the producing-op width, or a recorded narrow let-var's type. *)
let narrow_conv_of env e =
  match any_narrow_conv e with
  | Some _ as r -> r
  | None -> narrow_var_of env (any_payload e)

(* Emit the width-[w] wrap of [inner]: mask to [w] bits (Go [& 0x..]); for a
   SIGNED width, additionally sign-extend via [(m ^ 2^(w-1)) - 2^(w-1)].  Matches
   Go's uintW / intW wrap exactly on the int64 carrier. *)
let fw_wrap signed width inner =
  let mask = Printf.sprintf "0x%x" ((1 lsl width) - 1) in
  let masked = str "(" ++ inner ++ str (" & " ^ mask ^ ")") in
  if not signed then masked
  else
    let sbit = Printf.sprintf "0x%x" (1 lsl (width - 1)) in
    str "((" ++ masked ++ str (" ^ " ^ sbit ^ ") - " ^ sbit ^ ")")

let classify_float_op r =
  List.find_map
    (fun (name, op) -> if is_float_op_ref r name then Some op else None)
    float_op_table

(* float32 arithmetic (Fido [f32_add]/… defs, modelled via SpecFloat): recognized BY NAME →
   the native Go [float32] operator.  The SpecFloat body lowers to nothing — its whole drag
   closure is suppressed by module via [is_zarith_helper]. *)
let f32_op_table = [ "f32_add", " + "; "f32_sub", " - "; "f32_mul", " * "; "f32_div", " / " ]
let classify_f32_op r =
  if not (from_builtins r) then None else
  let bn = global_basename r in
