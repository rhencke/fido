(*s Identifier utilities. *)

(** Sanitise a Rocq id for Go: replace prime ['] with underscore. *)
let go_safe s = String.map (function '\'' -> '_' | c -> c) s

let pp_goid id = str (go_safe (Id.to_string id))

(** Capitalise the first character (export in Go). *)
let go_export s =
  if String.length s = 0 then s
  else
    let b = Bytes.of_string s in
    Bytes.set b 0 (Char.uppercase_ascii (Bytes.get b 0));
    Bytes.to_string b

(*s Global-reference helpers. *)

let global_path r =
  try
    DirPath.to_string (Nametab.dirpath_of_global r.glob) ^ "." ^
    Id.to_string (Nametab.basename_of_global r.glob)
  with Not_found -> ""

let global_basename r =
  try Id.to_string (Nametab.basename_of_global r.glob)
  with Not_found -> ""

(* ---- Record (Go struct) support ----
   A Rocq Record is a Go value-struct (both have value/copy semantics, so no
   aliasing model is needed).  A record's [ind_kind] carries its projection refs;
   [collect_records] gathers them up front.  At use sites a projection becomes
   [x.Field] and the constructor a struct literal [T{...}]; the projection
   DEFINITIONS are suppressed (field access replaces them). *)
let record_proj_field : (string, string) Hashtbl.t = Hashtbl.create 16  (* proj path -> field name *)
let record_ctor_names : (string, unit) Hashtbl.t = Hashtbl.create 16    (* constructor basename -> () *)
let record_typenames  : (string, unit) Hashtbl.t = Hashtbl.create 16    (* record type basename -> () *)
let record_ctor_ftypes : (string, ml_type list) Hashtbl.t = Hashtbl.create 16
  (* constructor basename -> field types (for typed-closure dictionary entries) *)
(* Methods: a top-level function whose first visible param is a record (struct) is
   lowered as a Go value-receiver method.  [collect_decls] registers each such
   function by its [global_path]; uses then become [recv.Method(rest)]. *)
let method_paths      : (string, unit) Hashtbl.t = Hashtbl.create 16    (* method proj path -> () *)

let globref_basename g =
  try Id.to_string (Nametab.basename_of_global g) with Not_found -> ""

let is_record_proj r = Hashtbl.mem record_proj_field (global_path r)
let proj_field_name r = go_export (Hashtbl.find record_proj_field (global_path r))
let is_record_ctor r = Hashtbl.mem record_ctor_names (global_basename r)
let is_record_typename s = Hashtbl.mem record_typenames s
let is_method r = Hashtbl.mem method_paths (global_path r)
let record_ctor_tyname r =
  match r.glob with
  | Names.GlobRef.ConstructRef (ind, _) ->
      go_export (globref_basename (Names.GlobRef.IndRef ind))
  | _ -> ""

(* A Fido builtin/combinator is matched by its (unqualified) basename — safe
   because those names are reserved by builtins.v and user theories should not
   shadow them.  Stdlib refs are package-qualified and use [ref_has_suffix]. *)
let named n r = String.equal (global_basename r) n

let ref_has_suffix r suffix =
  let p = global_path r in
  let n = String.length p and m = String.length suffix in
  n >= m && String.equal suffix (String.sub p (n - m) m)

let path_contains r sub =
  let p = global_path r in
  let lp = String.length p and ls = String.length sub in
  let rec scan i = i + ls <= lp && (String.equal (String.sub p i ls) sub || scan (i + 1)) in
  ls > 0 && scan 0

(* [Z]/[positive]/[N] arithmetic helpers (BinInt.Z.*, BinPos.Pos.*, …) pulled in by
   the FULL-WIDTH int64 model's bodies ([wrap64] = [Z.modulo]…).  Those bodies are
   PROOF-ONLY (the ops lower to bare Go int64 by name), so the [Z] helpers are never
   referenced by emitted Go — but extraction still drags their (recursive, match-on-
   constructor) decls into the program.  Suppress them by module so the printer never
   tries to lower a [Z] eliminator.  Safe: nothing in the EMITTED Go calls them. *)
let is_zarith_helper r =
  List.exists (path_contains r) ["BinInt"; "BinPos"; "BinNat"; "BinNums"; "PArith"; "NArith"]
  (* Generic eliminators leaked from the [Z] bodies but living in
     [Corelib.Init.Datatypes] (so not caught by module): [CompOpp] (from
     [Z.compare]), [fst]/[snd] (from [Z.quotrem], the truncating div/mod).  Only ever
     reachable from the suppressed [GoI64] bodies, so never emitted. *)
  || List.mem (global_basename r) ["CompOpp"; "comparison"; "fst"; "snd"]

