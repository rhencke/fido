(** Emit [func main() { _ = EntryFn() }] for the given entry-point name. *)
let pp_main_call fn_name =
  fnl () ++
  str "func main() {" ++ fnl () ++
  str ("\t" ^ go_export fn_name ^ "()") ++ fnl () ++
  str "}" ++ fnl ()

(** Emit [func main()] with the entry body inlined directly. *)
(** Emit [func main()] body as flat sequential statements.
    [MLletin] chains become [x := rhs] assignments rather than IIFEs. *)
(** Emit the body of any IO-returning function as flat sequential statements.
    Used by both [pp_main_body] and [pp_function] for IO-typed functions. *)
let pp_main_body state body =
  str "func main() {" ++ fnl () ++
  pp_io_body state "\t" [] body ++
  str "}" ++ fnl ()

(*s Declaration printer. *)

let pp_decl state decl =
  match decl with
  | Dterm (r, _, _) when is_inlined_ref r ->
      mt ()

  (* Record projections are emitted as field access at use sites, not as functions. *)
  | Dterm (r, _, _) when is_record_proj r ->
      mt ()

  (* Suppress top-level definitions whose result type is Proto — protocol
     descriptors are proof-only witnesses with no runtime representation. *)
  | Dterm (_, _, Tglob (rt, _)) when is_proto_type rt ->
      mt ()

  | Dterm (r, body, typ) ->
      let name = global_basename r in
      (match body with
       | MLlam _ ->
           pp_function state name body typ ++ fnl ()
       | _ ->
           match typ with
           | Tglob (r, []) when is_unit_type r ->
               str "func " ++ str (go_export name) ++ str "() {" ++ fnl () ++
               str "\t" ++ pp_expr state [] body ++ fnl () ++
               str "}" ++ fnl () ++ fnl ()
           | Tglob (r, [inner]) when is_IO_type r ->
               let go_ret = match inner with
                 | Tglob (r2, []) when is_unit_type r2 -> mt ()
                 | t -> str " " ++ pp_type state t
               in
               str "func " ++ str (go_export name) ++ str "()" ++ go_ret ++ str " {" ++ fnl () ++
               pp_io_body state "\t" [] body ++
               str "}" ++ fnl () ++ fnl ()
           | _ ->
               str "var " ++ str (go_export name) ++ str " " ++
               pp_type state typ ++ str " = " ++
               pp_expr state [] body ++ fnl () ++ fnl ())

  | Dfix (refs, bodies, types) ->
      if Array.exists is_inlined_ref refs then mt ()
      else
        prvecti
          (fun i _ -> pp_function state (global_basename refs.(i)) bodies.(i) types.(i))
          refs

  | Dind mi ->
      (* A record (single-constructor inductive with projections) → a Go struct;
         other inductives are still suppressed (nat/bool are builtins). *)
      (match mi.ind_kind with
       | Record projs when Array.length mi.ind_packets > 0
           && not (is_erased_record_typename (Id.to_string mi.ind_packets.(0).ip_typename)) ->
           let pkt = mi.ind_packets.(0) in
           let ftypes = (try pkt.ip_types.(0) with _ -> []) in
           let field proj t =
             let fname = match proj with
               | Some g -> go_export (global_basename g) | None -> "F" in
             str "\t" ++ str fname ++ str " " ++ pp_type state t ++ fnl () in
           str "type " ++ str (go_export (Id.to_string pkt.ip_typename)) ++
           str " struct {" ++ fnl () ++
           prlist (fun x -> x) (List.map2 field projs ftypes) ++
           str "}" ++ fnl () ++ fnl ()
       | _ -> mt ())

  | Dtype (r, _, _) when is_uint63_type r || is_go_prim_type r || is_float64_type r
    || is_IO_type r || is_go_map_type r || is_go_chan_type r
    || is_ref_type r
    || String.equal (global_basename r) "Sess"
    || String.equal (global_basename r) "GoString"
    || String.equal (global_basename r) "GoSlice"
    || List.mem (global_basename r) ["RefCell"; "RefHeap"; "ChanCell"; "ChanHeap";
                                      "MapCell"; "MapHeap"; "Tagged"; "GoTypeTag"] -> mt ()
  | Dtype (_, _, Tglob (r, _)) when is_sigT_ref r    -> mt ()

  | Dtype (r, _, typ) ->
      str "type " ++ str (go_export (global_basename r)) ++
      str " = " ++ pp_type state typ ++ fnl ()

(*s Structure printer. *)

(* The first visible (non-erased) parameter's [ml_type], if any — mirrors
   [pp_function]'s [zip_params] for the first pair (skip dummies, advance types). *)
let first_param_type body typ =
  let ids, _ = collect_lam body in
  let param_types, _ = collect_tarrs typ in
  let rec go ids types =
    match ids, types with
    | [], _ -> None
    | id :: rest, _ when is_dummy id ->
        go rest (match types with _ :: t -> t | [] -> [])
    | _ :: _, t :: _ -> Some t
    | _ :: _, [] -> None
  in
  go ids param_types

(* Gather, BEFORE emitting any decl, two things every use site depends on:
   (1) records — projections (→ field names), constructors, and type names, so a
       projection lowers to [x.Field] and a constructor to [T{…}];
   (2) methods — any function whose first visible param is a (already-registered)
       record, so calls lower to [recv.M(rest)].  Records must be registered first
       (pass 1) because the method test consults [record_typenames] (pass 2). *)
let collect_decls struc =
  let decls = ref [] in
  List.iter (fun (_, sel) ->
    List.iter (function (_, SEdecl d) -> decls := d :: !decls | _ -> ()) sel) struc;
  let decls = List.rev !decls in
  (* pass 1 — records *)
  List.iter (function
    | Dind mi ->
        (match mi.ind_kind with
         | Record projs when Array.length mi.ind_packets > 0
             && not (is_erased_record_typename (Id.to_string mi.ind_packets.(0).ip_typename)) ->
             let pkt = mi.ind_packets.(0) in
             Hashtbl.replace record_typenames (Id.to_string pkt.ip_typename) ();
             List.iter (function
               | Some g -> Hashtbl.replace record_proj_field (global_path g) (global_basename g)
               | None -> ()) projs;
             if Array.length pkt.ip_consnames > 0 then begin
               Hashtbl.replace record_ctor_names (Id.to_string pkt.ip_consnames.(0)) ();
               let ftypes = (try pkt.ip_types.(0) with _ -> []) in
               Hashtbl.replace record_ctor_ftypes (Id.to_string pkt.ip_consnames.(0)) ftypes
             end
         | _ -> ())
    | _ -> ()) decls;
  (* pass 2 — methods (first visible param is a record; projections excluded) *)
  let register_method r body typ =
    match first_param_type body typ with
    | Some t when (is_record_tglob t || is_sptr_record_tglob t)
                  && not (is_record_proj r) && not (is_inlined_ref r) ->
        Hashtbl.replace method_paths (global_path r) ()
    | _ -> ()
  in
  List.iter (function
    | Dterm (r, body, typ) -> register_method r body typ
    | Dfix (refs, bodies, types) ->
        Array.iteri (fun i r -> register_method r bodies.(i) types.(i)) refs
    | _ -> ()) decls

let pp_struct state struc =
  collect_decls struc;
  let pp_mod (mp, sel) =
    State.with_visibility state mp [] (fun state ->
      prlist
        (fun (_, elem) ->
           match elem with
           | SEdecl d      -> pp_decl state d
           | SEmodule _
           | SEmodtype _   -> mt ())
        sel)
  in
  prlist pp_mod struc

